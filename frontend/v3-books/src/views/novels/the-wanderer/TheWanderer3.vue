    seek out the boy, Michael, for the paying of these men will take a long
    time, and I’ve already prepared them for the Deliverer’s coming.”
  </p>
  <p>
    Andy ordered the soldiers to obey Abu el-Kasim and the scribes, that no
    disputes might arise over their pay; then he came with us into the inner
    courtyard where we saw many corpses, and a number of shot holes in the
    marble colonnade.
  </p>
  <p>
    But Andy took us straight through the golden Gateway of Bliss, shoving the
    startled eunuchs aside, then muttered thickly, “Let’s go to the baths, for I
    fancy I have two unopened wine jars there.”
  </p>
  <p>
    With the assurance of a sleepwalker he led us along many labyrinthine
    corridors to the baths, and there kneeling at the brink he fished a jar from
    the water, broke its seal, and drank greedily.
  </p>
  <p>
    I glanced round the place and beheld Selim ben-Hafs’s body lying on a marble
    slab—no lovely sight, for it was more swollen and livid than ever.
  </p>
  <p>
    The eunuchs who had been attending to it melted away like shadows at our
    approach.
  </p>
  <p>
    Mustafa ben-Nakir seated himself cross legged on the bench at the dead man’s
    feet and said, “We must all die, and each moment of our lives is
    preordained.
  </p>
  <p>
    It is also the will of Allah that we should sit in this bathhouse and that
    you should cleanse your conscience so that afterward we may order all things
    for the best.
  </p>
  <p>Speak, therefore, wrestler Antar!”</p>
  <p>
    Andy stared, hiccupped, felt the feathers in his turban and said in hurt
    tones, “I’m no wrestler, but the Sultan’s Aga—if only I could lay hands on
    the Sultan.
  </p>
  <p>
    And all that happened was that evil tongues spread slander about me,
    persuading Selim ben-Hafs that I’d spat in his bed—which is a black lie as
    I’ve never so much as seen his bed.
  </p>
  <p>
    This morning Selim came stark naked to the bathhouse to sweat away the
    opium, and a whole crowd of painted boys came too, to wash him.
  </p>
  <p>When he saw me he began to screech for his scimitar.</p>
  <p>
    His wife Amina, who was wearing no more than was once customary in g y
    Paradise, tried to calm him and at least gain time for me to get my breeches
    on.
  </p>
  <p>
    But at the sight of her the licentious old man was more rabid than ever.
  </p>
  <p>
    Luckily his pretty boys took to their heels when they saw Amina, so I was
    able to bar the door and consider what was next to be done.
  </p>
  <p>
    She said I had no choice but to bring the Selim to a better frame of mind by
    force, so I just took him by the neck with the tips of my fingers, and it
    broke.
  </p>
  <p>My dear Amina was as frightened as myself.”</p>
  <p>
    Andy wiped the tears from his eyes with his thumb, but Mustafa ben-Nakir,
    contemplating his nails, asked, “And then?”
  </p>
  <p>“Then?”</p>
  <p>Andy rubbed his temples to refresh his memory.</p>
  <p>“Yes.</p>
  <p>
    Well, then the lady Amina said it was the will of Allah, but that for our
    own good it would be best to say that Selim had slipped on the smooth floor
    and broken his neck.
  </p>
  <p>
    She then told me that other more important duties awaited her and quickly
    left the room, promising to send the Aga and the eunuchs as witnesses to
    what had occurred.
  </p>
  <p>
    The eunuchs laid Selim on the bench, tied his toes together and proclaimed
    the new sultan, while I took the Aga by the arm and returned with him to the
    barracks, as it seemed to me I had no business in the house of mourning.
  </p>
  <p>
    I thought him a pleasant fellow, yet in that I must have been mistaken,
    since so far as I can remember I’ve just killed him.”
  </p>
  <p>
    He fingered his headdress thoughtfully for a litde, then started and said,
    “Where was I?
  </p>
  <p>Ah, yes.</p>
  <p>
    There was trouble over the new sultan, for Selim ben-Hafs had two sons
    besides Amina’s, and these two were proclaimed sultan simultaneously.
  </p>
  <p>
    The uproar and fighting went on until it was found that Amina had had both
    the elder boys strangled, and their mother, too, for safety’s sake.
  </p>
  <p>
    When I reproached her for this she asked if I would have preferred to see
    her and her son strangled; for it seems it is the custom here for the ruler
    to leave no rival alive.
  </p>
  <p>
    She then hinted broadly that she meant to marry me, so that I might protect
    her son till he grew up.
  </p>
  <p>
    I’ve nothing whatever against Amina—fine woman—but she’s handier with the
    noose than I should like any wife of mine to be.”
  </p>
  <p>
    He began angrily calling for Amina, and was almost too drunk to stand, but
    Mustafa ben-Nakir had heard enough and rose, saying, “Antar, you’ve done
    your part, and need rest.
  </p>
  <p>
    There is no sultan but Suleiman, the Sultan of Sultans, and in his name I
    take possession of this kasbah until the Deliverer comes to reward and
    punish each man according to his deserts.
  </p>
  <p>
    Slave Michael, take your brother’s sword which he’s not in a condition to
    wield and strike off Selim’s head, that it may be set on the top of a pillar
    in a golden dish in the sight of everyone.
  </p>
  <p>
    With him the Hafsid dynasty is at an end; no intriguing women shall rule in
    this city, and the throne shall remain vacant until the coming of the
    Deliverer.”
  </p>
  <p>
    g Mustafa spoke in a voice of such authority that I dared not disobey, and
    grasping Andy’s sword I struck off Selim’s head, disagreeable though the
    task was.
  </p>
  <p>
    But as I was handing back the weapon a crowd of splendidly dressed eunuchs
    and black slaves entered the room.
  </p>
  <p>
    In their midst was a boy in a gorgeous kaftan and with far too large a
    turban on his head.
  </p>
  <p>
    He tripped over the long kaftan as he walked, and held his mother by the
    hand.
  </p>
  <p>Andy, looking much ashamed, greeted this woman by the name of Amina.</p>
  <p>
    When she saw the state he was in she forgot to veil herself, stamped, and
    screamed, “I ought never to have trusted one of the uncircumcised!
  </p>
  <p>Where is the treasure chest?</p>
  <p>Why don’t the soldiers proclaim my son sultan?</p>
  <p>And how could you allow my lord’s body to be thus desecrated?</p>
  <p>
    The best thing I could do would be to have your throat cut, since you use it
    only to defile it in defiance of the Prophet’s law.”
  </p>
  <p>
    “B-blessed be his name,” stammered Andy, swaying and hiccupping, while I
    stood nonplussed with Selim’s head still in my hand; the infuriated woman
    snatched off her red slipper and began beating Andy over the head with it,
    until the Aga’s turban fell off.
  </p>
  <p>
    I hardly know how it would have ended had not Mustafa ben-Nakir stepped
    forward, jingling the bells at his girdle, and cried, “Veil your face,
    shameless woman, and take your bastard back to the harem!
  </p>
  <p>
    We have nothing to say to you, and Allah will punish you for thus treating a
    man who has done you and your son far greater service than you deserve.”
  </p>
  <p>
    His demeanor was so proud and commanding that the woman recoiled and said,
    “Who are you, fair youth, and how dare you use that tone to me, the mother
    of the ruling sultan?”
  </p>
  <p>“I am Mustafa ben-Nakir, son of the angel of death.</p>
  <p>My task is to see that each is rewarded after his deserts.”</p>
  <p>
    Turning to the eunuchs he said, “Take the woman back to the harem, and let
    this drunken swine sleep it off in some obscure corner.
  </p>
  <p>
    Then fetch a kaftan befitting my rank, so that I may take command of the
    city until the coming of the Deliverer.
  </p>
  <p>
    And do all this more swiftly than I can find an appropriate gazel, or many
    of you will find yourselves a head shorter.”
  </p>
  <p>
    He turned his back on Amina, opened his book, and began reading aloud to
    himself in his musical voice, so impressively that none dared to question or
    disturb him, but obeyed his orders.
  </p>
  <p>
    I was greatly relieved to find amid the general confusion at least one man
    who knew his own mind.
  </p>
  <p>
    But my great natural curiosity got the better of me, and I asked, “What
    manner of man are you, Mustafa benNakir, that all obey you?”
  </p>
  <p>He smiled and bent his head.</p>
  <p>
    “I but follow the impulses of my heart, which tomorrow may lead me out into
    the wilderness.
  </p>
  <p>
    Perhaps men obey me because I’m freer than others—so free that I care not
    whether they obey me or not.”
  </p>
  <p>
    The eunuchs soon returned with splendid clothes, which they helped Mustafa
    ben-Nakir to put on.
  </p>
  <p>
    They shod him with jeweled slippers and girded a bright sword about his
    waist; lastly he set upon his well- groomed locks the Aga’s turban.
  </p>
  <p>
    He bade me put Selim ben-Hafs’s head on the golden dish, which the eunuchs
    brought at his order, and then with his hand to his mouth he yawned slightly
    and said, “The money will soon have been distributed among the men, and it
    will be wise to keep them occupied.
  </p>
  <p>
    I fancy nothing will answer the purpose so well as attacking the Spaniards.
  </p>
  <p>
    I must therefore send a Latin- speaking envoy to their fortress to demand
    compensation for all the damage they’ve done.
  </p>
  <p>
    If they refuse, they must be told that the new sultan will not tolerate
    their behavior, and must summon Khaireddin to his support.
  </p>
  <p>This will give us time to take the guns down to the harbor.</p>
  <p>But if you have a better plan, Michael, speak freely.”</p>
  <p>“Whom do you mean by the sultan?”</p>
  <p>I objected.</p>
  <p>“Is little Mohammed ben-Hafs the lawful sultan of Algiers?”</p>
  <p>
    “Ah,” he replied, suppressing another yawn, “we believe in Allah though we
    haven’t seen him; why should the Spaniards doubt the existence of a sultan
    whom they’ve never beheld?
  </p>
  <p>Speak to them of this invisible sultan, and let that suffice them.”</p>
  <p>“Allah, Allah!”</p>
  <p>I gasped.</p>
  <p>“You can’t mean to send me?</p>
  <p>
    Spaniards are cruel men, and even if they leave the head on my shoulders
    they’re likely to remove my nose and ears.”
  </p>
  <p>Mustafa ben-Nakir gently shook his head.</p>
  <p>“I would gladly go myself, for I like to visit new places and people.</p>
  <p>But I lack proficiency in Latin, and have also other things to do.</p>
  <p>You had better stay at the fortress for a time.</p>
  <p>
    Now you must not disturb me, for I’m composing a Turkish poem in the Persian
    manner and must count the syllables.”
  </p>
  <p>
    To comfort me, he ordered the eunuchs to provide me with an exceedingly fine
    kaftan, and I had then no choice but to take Selim’s head on the golden
    charger and follow Mustafa ben-Nakir.
  </p>
  <p>
    The armed Negroes attended us and we paced in solemn procession to the
    forecourt amid the astonished shouts of the soldiers.
  </p>
  <p>
    Abu el-Kasim dashed up to us and fell on his knees before Mustafa to kiss
    his slipper.
  </p>
  <p>
    Seeing this, the eunuch also knelt; Mustafa took the Sultan’s signet ring
    from his hand and fingered it reflectively.
  </p>
  <p>
    Soon the whole court was full of bowing soldiers, who touched forehead and
    ground with their finger tips.
  </p>
  <p>
    Mustafa ben-Nakir summoned the sergeants and arranged for some men to guard
    the gates and others to quench the fires down by the harbor.
  </p>
  <p>But the greater number he ordered to drag the cannon to the shore.</p>
  <p>
    No vessel was to put out for the fortress without his permission, and anyone
    approaching from that direction was to be arrested and brought before him.
  </p>
  <p>
    When he had finished speaking he contemplated his nails and asked whether
    there was anything further the men wished to know.
  </p>
  <p>
    They murmured among themselves until one took courage and yelled, “Driveling
    fop!
  </p>
  <p>Who are you to give orders?”</p>
  <p>
    This was greeted by expectant laughter, but Mustafa ben-Nakir coolly took a
    broad scimitar from a Negro’s hand, advanced to the speaker and looked him
    steadily in the face.
  </p>
  <p>
    The other soldiers made way, and Mustafa with a lightning stroke took off
    the man’s head before he could lift a finger.
  </p>
  <p>
    Without so much as a glance at the headless body Mustafa returned to his
    place, handed the sword back to the Negro, and asked if anyone else had
    anything to say.
  </p>
  <p>
    But the smile had frozen on the lips of the curious, and those standing next
    the dead man contented themselves with stooping to empty his purse.
  </p>
  <p>
    After this the different detachments marched off in good order to the duties
    assigned them.
  </p>
  <p>
    Abu el-Kasim rubbed his hands and said, “We’ve brought the business to a
    happy conclusion, though at considerable expense.
  </p>
  <p>Bitt I have no doubt that the Deliverer will fully reimburse me.</p>
  <p>
    We must now decide what to say to him, and how to say it, so that we may not
    contradict one another when the time comes.”
  </p>
  <p>
    Mustafa ben-Nakir graciously assented, adding, “And it would be well for
    your slave Michael to go at once to the fortress and begin negotiations with
    the Spaniards.”
  </p>
  <p>He turned to me.</p>
  <p>“If you can induce them to leave, so much the better.</p>
  <p>If not, there’s no harm done.”</p>
  <p>
    Having given orders for two soldiers to attend me, he returned to the
    Courtyard of Bliss.
  </p>
  <p>
    There was nothing for it but to curse my fate and betake myself to the
    harbor, where troops were putting out fires, building breastworks, and
    dragging ordnance into position.
  </p>
  <p>
    The boatman had not far to take me, but the round keep and massive walls of
    the fortress seemed to grow ever darker and more menacing as we approached.
  </p>
  <p>
    When we had covered half the distance a shot was fired from a little cannon
    on the wall, and the ball fell so near my boat that the splash of it
    drenched me.
  </p>
  <p>
    In my alarm I began to jump up and down waving the skirts of my kaftan and
    shrieking in my best Latin that I was the Sultan’s messenger.
  </p>
  <p>
    We should certainly have capsized had not the boatman pulled me down again
    on to my seat.
  </p>
  <p>
    But there was no more firing, and as soon as I was within earshot a monk in
    a black habit appeared on the jetty and addressed me in Latin, asking in
    God’s name what had happened, and blessing my arrival, since great anxiety
    prevailed in the fortress.
  </p>
  <p>
    We drew alongside the jetty, and I demanded in the name of the Sultan to
    speak with the garrison commander.
  </p>
  <p>
    While this officer was changing into clothes worthy of the Sultan’s envoy,
    the monk set wine before me, and would have offered me food had supplies
    allowed.
  </p>
  <p>
    But these were dwindling now that purchases in the city had become
    impracticable.
  </p>
  <p>
    So guileless was this good man that he asked me to send my boatman back to
    fetch meat and greenstuff, for the wounded especially were suffering from
    the lack of these victuals.
  </p>
  <p>
    I soon gathered that no one in the fortress had the least idea of what had
    happened in the town.
  </p>
  <p>
    For ten years the garrison had led a lazy, peaceful life, and it was thought
    that I had come to beg forgiveness on the Sultan’s behalf.
  </p>
  <p>
    Selim ben-Hafs had always regarded these Spaniards as his only protection
    against Khaireddin.
  </p>
  <p>
    This situation only increased my dread of the wrath which my errand might
    arouse in Captain de Varga, the Spanish commander, and I sought to stiffen
    my courage with deep draughts of wine.
  </p>
  <p>
    At last Captain de Varga appeared, in shining armor, attended by the Spanish
    consul who had fled from the town with the soldiers.
  </p>
  <p>
    The consul had a bump on his forehead and was in a state of intense
    excitement because his house had been looted.
  </p>
  <p>
    Captain de Varga spoke a little Latin, and was a proud, resolute man; yet in
    consequence of his inactive life he had put on weight, so that the costly
    armor pinched him here and there: a circumstance in no way tending to
    increase his good will toward me.
  </p>
  <p>
    First he asked what had happened in the city, and why the Sultan’s troops as
    well as the townspeople had so treacherously attacked his own almost unarmed
    men and caused such damage to property.
  </p>
  <p>
    At this point the consul, the veins swelling at his temples, shouted that
    the losses he had sustained far exceeded in value the lives of a few
    blockheads of soldiers.
  </p>
  <p>
    He demanded full compensation and a new and better house, for which he had
    already chosen the site.
  </p>
  <p>
    When at last I had a chance to speak I chose my words with care: “Noble
    Captain, most excellent Consul, and Reverend Father!
  </p>
  <p>
    Sultan Selim ben-Hafs, blessed be his name, died this morning by accident.
  </p>
  <p>He slipped and fell in the baths, breaking his neck.</p>
  <p>
    After much discussion among his fatherless sons, the seven-year-old Mohammed
    has assumed the kaftan and ascended the throne.
  </p>
  <p>
    He has secured his position by distributing money among his loyal troops,
    and beside him as counselor stands his wise mother Amina.
  </p>
  <p>
    His elder brothers will not oppose him, for in the course of a meal a
    datestone lodged in each of their throats and choked them.
  </p>
  <p>
    No doubt g the hand of fate thus intervened to prevent disputes over the
    succession.
  </p>
  <p>
    “But,” I went on with a quaking heart, though still looking Captain de Varga
    steadfastly in the eye, “while all this was taking place in accordance with
    the time-honored customs of this city, a horde of pillaging Spaniards
    arrived, bringing artillery with them.
  </p>
  <p>
    I hold you in no way to blame, noble Captain, for this gross infringement of
    national rights.
  </p>
  <p>
    The lawless rabble must have left the fortress without your permission, and
    profited by the ruler’s death to bring disorder to the town.
  </p>
  <p>
    Nevertheless they desecrated the mosque, willfully defiled the holy
    Marabout’s tomb, and then opened fire on the kasbah, no doubt with the
    object of seizing the treasury.
  </p>
  <p>
    The Aga was compelled to dispatch a few cavalrymen to drive them forth with
    as little violence as possible.
  </p>
  <p>
    The Spaniards then overran the city, looting the homes of the faithful and
    ravishing their good wives.
  </p>
  <p>
    To prevent further disorders, the Sultan has been graciously pleased to
    sever communications between fortress and city, lest the people, enraged at
    the pollution of the mosque and tomb, should return evil for evil and attack
    the fort.
  </p>
  <p>
    The Sultan has also ordered the digging of trenches about the harbor, where
    he has set up his artillery, as you may see for yourself.
  </p>
  <p>
    But these measures have been adopted solely to protect the fortress and to
    prevent fresh violence, which might prejudice the friendly relations now
    happily existing between the Emperor of Spain and the Sultan of Algiers.”
  </p>
  <p>Wine had so loosened my tongue that I was moved by my own eloquence.</p>
  <p>
    The consul listened open mouthed, but the Dominican crossed himself
    repeatedly and said in tones of satisfaction, “It’s only proper that our
    Christian soldiers should have desecrated the mosque and tomb of the
    infidels, and I cannot sufficiently praise them.
  </p>
  <p>
    All too often have we seen Moslems trample the Cross underfoot, to enrage
    us.”
  </p>
  <p>
    Captain de Varga bade him hold his tongue, and looking at me darkly he said,
    “You lie.
  </p>
  <p>
    I sent the patrol ashore to discover the reason for the shooting in the
    kasbah, entirely in the interests of Selim ben- Hafs; but my men fell into
    the trap prepared, and only their good discipline saved them from utter
    annihilation.
  </p>
  <p>
    If there has been looting and arson the Moslems themselves have committed
    it, to cover their own misdeeds.”
  </p>
  <p>I bowed low and said, “I have heard you, noble Captain.</p>
  <p>
    All that remains for me to do is to return to the Sultan and inform him that
    you distort the truth, harden your heart, and do your utmost to cloud the
    cordial relations that have hitherto existed between the Hafsids and the
    Emperor, your master.”
  </p>
  <p>“Wait!”</p>
  <p>said Captain de Varga hastily.</p>
  <p>
    Taking a paper from the consul he read it through and went on, “I ask
    nothing better than to see those happy relations restored, and I’m willing
    to forget the whole incident in return for indemnification for damaged
    property and weapons, and for the suffering caused—and also the customary
    compensation to the families of the fallen.
  </p>
  <p>
    I will accept in all the sum of twenty-eight thousand Spanish gold pieces,
    half to be paid before the infidels’ sunset prayer, and the other half
    within three months, as I realize the young Sultan will have other expenses
    to meet at the beginning of his reign.”
  </p>
  <p>
    I exclaimed at the very thought of so fabulous a sum, but Captain de Varga
    raised his hand and continued, “To prevent future misunderstandings I claim
    the right to build an artillery tower in the harbor, near the mosque.
  </p>
  <p>
    Further, the Sultan shall have a Spaniard for his vizier who must be allowed
    an armed bodyguard, to be maintained at the expense of the treasury.”
  </p>
  <p>
    I perceived from these terms that he was a farsighted man who served the
    Emperor well, and was in all respects a worthy foe.
  </p>
  <p>
    Genuine tears came to my eyes as I knelt before him and begged him to strike
    off my head rather than send me back to the Sultan with such a message, for
    the Sultan would certainly not spare me.
  </p>
  <p>
    In so doing I relied on his honor as a nobleman and was not disappointed,
    for he bade me rise, and said, “Serve me faithfully, persuade the Sultan
    that I’m in earnest, and I’ll allow no harm to come to you.
  </p>
  <p>
    Tell him that my gunners stand with smoldering matches, that I mean to
    bombard the city with red-hot shot and that I shall occupy the harbor unless
    I receive a favorable answer by the hour of morning prayer tomorrow.”
  </p>
  <p>“Allah is great,” said I.</p>
  <p>“Since you trust me, let me give you some good advice.</p>
  <p>
    Don’t threaten too much, or the Sultan—moved by wicked counselors and by the
    angry populace—may send word to the great Khaireddin, to make a treaty with
    him, and with his help expel you from your island.”
  </p>
  <p>He laughed.</p>
  <p>“Renegade, you’re a wily fellow!</p>
  <p>
    But even a sevenyear-old boy would hardly be so foolish as to saw off the
    bough he sits on.
  </p>
  <p>If he called Khaireddin he’d get more than he bargained for.</p>
  <p>
    But I’ll listen to any proposals the Sultan may make when he has heard my
    terms.”
  </p>
  <p>
    Notwithstanding his laughter I could see that the very name of Khaireddin
    had startled him; and so I said, “My lord and protector!
  </p>
  <p>You need not send me away, for I bring you the Sultan’s proposals.</p>
  <p>
    He demands nothing but fair compensation for the damage done by the
    Spaniards’ raid, and a thousand gold pieces to buy rose water for the
    purification of the mosque and the Marabout’s tomb.
  </p>
  <p>
    He is willing even to reconsider the question of compensation, provided you
    block up all loopholes commanding the city under supervision of his
    officials.
  </p>
  <p>If you reject these proposals the Sultan.</p>
  <p>
    will be compelled to y j p p p assume an intention on your part to interfere
    in internal affairs, and will then seek help wherever it may be found, to
    prevent further conspiracy.”
  </p>
  <p>“God save us!”</p>
  <p>said Captain de Varga, crossing himself.</p>
  <p>
    “The terms are harder than I expected, but I know how suspicious these
    infidels are; because they plot incessantly they fancy others do the same.
  </p>
  <p>
    But I’m a Castilian; I will die rather than surrender—for surrender it would
    be.
  </p>
  <p>
    My last word is this: let us talk no more of compensation on either side.
  </p>
  <p>We’re all human; we’re all liable to err.</p>
  <p>
    I’ll even punish the culprits who have desecrated the holy places—if indeed
    that tale is true.
  </p>
  <p>But I cannot afford rose water.”</p>
  <p>
    The consul wailed and the monk deplored the punishment of Christian men who
    had deserved reward.
  </p>
  <p>
    But Captain de Varga said, “As you sec my aims are conciliatory, and in that
    respect diametrically opposed to those of my advisers.
  </p>
  <p>Further I cannot go.</p>
  <p>If your lord won’t listen, my guns must speak.</p>
  <p>
    Warn him above all against Khaireddin, for the least approach to that
    godless pirate will be regarded by me as an act of hostility toward my lord
    the Emperor.”
  </p>
  <p>
    He handed me a worn leather purse containing ten gold pieces, and I
    concealed my amazement that the Emperor should allow this loyal young
    officer to languish in such poverty.
  </p>
  <p>
    I was then honorably escorted to the jetty and at my desire—perhaps also to
    persuade me that he had plenty of gunpowder—he ordered a salute to be fired
    as we shoved off.
  </p>
  <p>
    His proud credulity astonished me and caused me to reflect that in all
    negotiation the honest man is bound to come off worst, while bluff wins
    every point.
  </p>
  <p>
    The whole affair had gone better than I could have hoped, and my conscience
    was clear, for I had given him plainly to understand that he had Khaireddin
    to reckon with as an adversary.
  </p>
  <p>
    I stepped ashore well satisfied, and observed that the fires in the harbor
    quarter had been put out and that many of the gun emplacements were
    completed.
  </p>
  <p>
    These works would have been gravely impeded by bombardment from the
    fortress; my negotiations had therefore fulfilled their purpose.
  </p>
  <p>
    On my return to the kasbah I was taken at once to the garden of the
    Courtyard of Bliss where Mustafa ben-Nakir, reclining at ease on a down
    pillow beneath a canopy, was reading Persian poems to my master Abu
    el-Kasim.
  </p>
  <p>
    They mentioned discreetly that Amina was no more, and while I felt no great
    regret I thought anxiously of Andy’s despair when he woke from his drunken
    stupor and learned of his beloved’s death.
  </p>
  <p>Mustafa divined my thoughts and said, “Allah is swift in judgment.</p>
  <p>
    We spoke with the woman and know that she exploited your brother’s
    simplicity for her own wicked ends.
  </p>
  <p>
    She bribed the eunuchs to leave Selim ben-Hafs alone with your brother in
    the bathhouse.
  </p>
  <p>
    So, Michael, you needn’t wonder that in righteous indignation at such
    treachery we arranged for her to be strangled by the eunuchs.
  </p>
  <p>We had your brother’s best interests at heart.”</p>
  <p>“Yes, indeed,” put in Abu el-Kasim.</p>
  <p>
    “But, reflecting that the fruit never falls far from the tree, we had
    Amina’s son removed at the same time.
  </p>
  <p>
    This makes matters simpler for Khaireddin, who might have been
    inconvenienced if the boy had lived and gone over to the Spaniards, thus
    giving them a pretext to interfere in the succession.”
  </p>
  <p>
    I now perceived that Mustafa ben-Nakir had deliberately sent me out of the
    way lest I should hinder these shady doings, and I pitied the little boy who
    had held his mother’s hand and stumbled over the long kaftan, and who had
    now perished in so sorrowful a manner.
  </p>
  <p>I went back then to Abu el-Kasim’s house.</p>
  <p>The stars were already glittering in the heavens.</p>
  <p>
    Many people were awake upon the housetops, and in the still night I heard
    the sound of laughter, of stringed instruments, and of dovelike cooings.
  </p>
  <p>
    My heart was gentle as I stepped into the house and called out that I had
    come home.
  </p>
  <p>
    My dog ran up in the darkness to lick my hand, while Giulia lit the lamp and
    said, “Is that you, Michael, and alone?
  </p>
  <p>Where have you been all this time, and where is Abu?</p>
  <p>I’ve lain awake wondering if something terrible had happened.</p>
  <p>
    There’s been fighting in the town and they say the Deliverer will soon be
    here.
  </p>
  <p>
    And when I came home I found a great hole in the floor and feared that
    robbers had broken in.”
  </p>
  <p>
    Her affectionate anxiety melted my heart still further and I said, “Nothing
    terrible has happened.
  </p>
  <p>Everything indeed is going better than I could have hoped.</p>
  <p>
    The Deliverer will come tomorrow at cockcrow, and for you great
    things—happier things than you can imagine—are in store.
  </p>
  <p>
    So let us make much of one another, for spring is here and we’re alone in
    the house with none to see us save the dog, who need not make us bashful.”
  </p>
  <p>
    Giulia clapped her hands for joy and cried, “How I long to see the great
    Deliverer who rules the seas!
  </p>
  <p>
    Surely he’ll reward me very generously for having so diligently foretold the
    future on his behalf, and prepared the way for his coming.
  </p>
  <p>Perhaps he will allow me to look into the sand alone with him.</p>
  <p>They say his beard is soft, and chestnut brown.</p>
  <p>
    He has certainly all the wives the law allows him, and the mother of his son
    is a direct descendant of the Prophet.
  </p>
  <p>Still, he may incline to me and keep me beside him.”</p>
  <p>
    Her prattle oppressed me, and when I sought to fold her in my arms she
    quickly veiled her face, stamped on my toes and said, “Are you out of your
    mind, Michael, to behave thus in the absence of our master?
  </p>
  <p>Control yourself.</p>
  <p>And where did you get that fine kaftan?</p>
  <p>If you would give it to me I could make a charming jacket out of it.”</p>
  <p>
    She began eagerly feeling the material; in the dim light of the lamp she was
    so marvelously beautiful that I could not resist her, and p y reluctantly I
    let her remove the kaftan, which indeed was the most splendid garment I had
    ever worn.
  </p>
  <p>
    She crushed it in her bare arms, greedily breathing in its pleasant scent of
    musk, and cried, “Will you really give it to me, Michael?
  </p>
  <p>If so you may kiss me, but in all innocence.</p>
  <p>
    I’m a fiery woman and have trouble enough as it is to protect my virtue.”
  </p>
  <p>
    She allowed me to kiss her cheek and even offered me her lips, but when I
    would have taken her in my arms she struggled and threatened to scream and
    stamped on my toes until I had to let her go.
  </p>
  <p>
    As soon as she was free she fled with the kaftan to her alcove, slamming and
    locking the grille and deriding my prayers and tears.
  </p>
  <p>
    As I stood there half-naked shaking the wrought-iron gate I remembered for
    the first time that I had left my slave clothes in the kasbah, and so had
    nothing to put on to greet the Deliverer in the morning.
  </p>
  <p>
    Tossing sleepless on my bed that night I was yet comforted by the thought
    that tomorrow Giulia would be my slave and my lawful property.
  </p>
  <p>
    I resolved to exact full requital for her torment of me, and hoped that she
    was not quite indifferent, since she had shown such anxiety for me and had
    accepted my kaftan as a present.
  </p>
  <p>
    Comforted I fell asleep and did not wake until the cocks of the city began
    to crow, and the joyful voice of the muezzin proclaimed that prayer was
    better than sleep.
  </p>
  <p>
    I looked up and saw to my astonishment that the muezzin was leaping and
    dancing on the balcony of the minaret; now he was proclaiming the coming of
    the Deliverer.
  </p>
  <p>
    Rising hastily, I flung on what garments I could find, grasped Giulia’s hand
    and sped with her up the steep street leading to the palace.
  </p>
  <p>
    The dog followed us with joyous barks, trying to tug at the cloak I had
    thrown over my shoulders.
  </p>
  <p>
    The whole populace was on its feet, some running to the palace, but most
    hastening to the western gates to meet the Deliverer beyond the walls and
    follow him into the city.
  </p>
  <p>
    They laughed and pointed at me and the dog, but I took no notice, reflecting
    that he laughs best who laughs last.
  </p>
  <p>
    We had a setback at the palace gates, however, for the guards flatly refused
    us admission, but fortunately a scared eunuch appeared, who recognized me.
  </p>
  <p>
    Stammering with fear he promised to take me to Abu el-Kasim, and begged me
    in return to say a good word for him.
  </p>
  <p>
    In my extremity I promised all he asked, and he led me through the Courtyard
    of Bliss to a small room where Abu el- Kasim, with red-rimmed eyes and
    clearly in a bad humor, was just finishing his breakfast.
  </p>
  <p>
    A flock of slave women were in attendance, but although they held up one
    magnificent kaftan after another and besought him to make haste and dress
    since Mustafa ben-Nakir and his suite had long since ridden to meet the
    Deliverer, he cut them over the shins with a cane and said, “No!
  </p>
  <p>I’m a poor man p and dislike strutting in borrowed plumage.</p>
  <p>
    Bring my plain spice merchant’s cloak whose smells are familiar to me and
    whose fleas know me.
  </p>
  <p>
    In that garment I have served the Deliverer and in that garment I will meet
    him, that with his own eyes he may behold my poverty.”
  </p>
  <p>
    The slaves wrung their hands and with lamentations brought out the ragged
    old cloak.
  </p>
  <p>
    Abu smelt it joyfully, combed out hair and beard with his fingers and
    allowed the terrified eunuch to help him on with the dreadful garment.
  </p>
  <p>
    Then only did he turn his eyes to me and say angrily, “Where in Allah’s name
    have you been, Michael?
  </p>
  <p>I hope you haven’t lost the golden dish and the Sultan’s head?</p>
  <p>We should have been in the mosque long ago, to meet the Deliverer.”</p>
  <p>
    I had in fact not the remotest idea what had become of these things, and I
    hastened off on a frantic search through the various courtyards.
  </p>
  <p>
    Luckily the friendly eunuch came to my help; he had taken care of both head
    and dish and set them on the top of a pillar.
  </p>
  <p>
    No harm was done, therefore, except that Selim’s head had begun to take on a
    most hideous appearance, and that the dish seemed much smaller than before.
  </p>
  <p>
    With these objects under my arm I returned to Abu el-Kasim, and was
    distressed to behold Giulia embracing and kissing and coaxing that
    remarkably unhandsome man.
  </p>
  <p>
    He wept, but was prevailed upon at last to send the slave women to the store
    chamber of the harem, and they returned with such a wealth of veils and
    slippers that Giulia was hard put to it to decide what pleased her best.
  </p>
  <p>
    To me Abu el-Kasim gave Mustafa ben-Nakir’s mendicant dress, which after
    some hesitation I put on.
  </p>
  <p>
    Being used to garments reaching to the ground, I had the uneasy sensation of
    nakedness from the waist down.
  </p>
  <p>
    But the tunic was of the finest and softest stuff, and with every step I
    took the bells rang so sweetly that Giulia surveyed me wide eyed and assured
    me that I need not be ashamed of my bare knees and shapely calves.
  </p>
  <p>
    She sent for the necessary ointments and rapidly painted my hands and feet
    orange color, and then, since no headdress was worn with this costume, she
    oiled my hair with fine oils and applied blue beneath my eyes so that I
    hardly recognized myself when I looked in the mirror.
  </p>
  <p>
    Before we set forth for the mosque, Abu wanted to see how Andy was faring.
  </p>
  <p>
    He took me to the cellars of the palace, moved aside an iron trap door and
    pointed to Andy, who lay sprawled on the hard stone floor below us, moaning
    in his sleep.
  </p>
  <p>
    His narrow cell was lit by a small window with bars across it as thick as my
    wrist.
  </p>
  <p>
    He was quite naked, and beside him stood a water jar, which was already
    empty.
  </p>
  <p>
    The compassionate Abu ordered the guards to refill it, and to lower a great
    quantity of bread.
  </p>
  <p>
    I pitied Andy deeply, but saw that he must be kept in that bear pit until he
    had quite recovered, or he would seek to p p q combat the effects of his
    drinking bout with a fresh one, and his last state would be worse than the
    first.
  </p>
  <p>
    Lest he should feel lonely when he woke, I left my dog with him in his cell.
  </p>
  <p>
    When we had left the evil-smelling cellars and our eyes had become
    accustomed to the sunlight on the high terrace, we saw the Deliverer just
    riding through the western gate of the city, followed by a large troop of
    cavalry.
  </p>
  <p>
    Weapons flashed in the sun, and the vast crowds, which had come to meet him,
    waved palm branches and shouted and cheered until their voices came to our
    ears like the boom of a distant sea.
  </p>
  <p>
    Through the quivering heat we could see also a number of vessels riding at
    anchor in the farther bay.
  </p>
  <p>We counted nearly twenty of them, all bedecked with flags and pennants.</p>
  <p>
    We hastened down into the city, and with difficulty elbowed our way into the
    packed mosque.
  </p>
  <p>
    We could never have managed it if I had not jingled my bells to make the
    people believe I was a holy man.
  </p>
  <p>
    They would have made way for us readily enough if I had displayed what I
    carried under my arm, but I had covered the golden dish with a cloth, for
    who could tell whether there might not lurk among them some adherent of
    Selim ben-Hafs?
  </p>
  <p>
    Within the mosque an indescribable din prevailed, which reached its climax
    when with drawn swords Khaireddin’s janissaries and renegades appeared in
    the doorway and began to clear a path for their lord.
  </p>
  <p>
    Khaireddin himself advanced among his warriors, sending greetings to right
    and left, and waving his hand.
  </p>
  <p>
    Before him marched a number of standard bearers, and immediately after him
    the whitebearded Faqih and the eldest sons of the merchants who had already
    returned, it seemed, from their important pilgrimage.
  </p>
  <p>
    Mustafa ben- Nakir was also of the suite, clad in a splendid kaftan and the
    Aga’s turban; from time to time he surveyed his well-kept nails.
  </p>
  <p>
    I was disappointed at my first sight of Khaireddin, of whom I had heard so
    much.
  </p>
  <p>He was a man of little majesty, being indeed short and rather fat.</p>
  <p>
    As a mark of dignity he wore a tall felt cap bound with a turban of white
    muslin.
  </p>
  <p>
    Strangely enough the turban was not even clean, though it was adorned in
    front by a crescent of sparkling stones.
  </p>
  <p>He went empty handed and had not even a dagger in his girdle.</p>
  <p>
    His beard was dyed and there was a smile on his round, catlike face as he
    walked with short steps across the floor.
  </p>
  <p>
    When he reached the reader’s place he made a sign to show that he was going
    to pray.
  </p>
  <p>
    He uncovered his head, rolled up his sleeves, and in the sight of everyone
    performed the prescribed ablutions.
  </p>
  <p>
    The Faqih poured water over his hands and the eldest sons of the merchants
    dried his hands, head, and feet.
  </p>
  <p>
    He then replaced the turban on his head, recited the prayers and three suras
    of the Koran, while the assembled people listened attentively.
  </p>
  <p>The Faqih then sat in the reader’s place and intoned certain verses.</p>
  <p>
    He read very p y beautifully; without difficulty he found passages
    appropriate to the coming of the Deliverer, and others enjoining mercy,
    justice, and liberality.
  </p>
  <p>
    When the Faqih had read for so long that the people began to grow restless,
    he at last resigned his place to Khaireddin, who mounted the high seat,
    crossed his legs under him, and with a slight stammer began to expound the
    sacred texts in so easy and entertaining a manner that now and then laughter
    could be heard among his audience.
  </p>
  <p>
    At last he raised his hand gently and said, “My dear children, I have come
    back to you, impelled by an auspicious dream, and I will never abandon you
    again.
  </p>
  <p>
    Henceforth I will protect you as a good father should, and you shall endure
    no more wrongs, for in this city justice shall ever prevail.”
  </p>
  <p>
    Emotion threatened to stifle his voice, but wiping tears from his beard he
    went on, “I would not sadden your hearts by recalling unpleasantness, yet in
    the name of truth I must admit that it was with a sentiment of profound
    disgust that I left this place, after my brother Baba Aroush had fallen in
    the unhappy war with the Sultan of Telmesan.
  </p>
  <p>
    Honesty compels me to add that I was very greatly cast down by the
    ingratitude and deceit with which the inhabitants rewarded my efforts to
    defend them against the unbelievers.
  </p>
  <p>A rancorous man in my position might requite evil with evil.</p>
  <p>
    But I seek only justice, and have often repaid a wrong with a good action,
    as I do today in returning to protect you from the enemy.
  </p>
  <p>
    But I note that no one answers me, and not the meanest present has been
    brought before me in token of your good intentions.
  </p>
  <p>
    Indeed, I fear that I shall again be overcome by repugnance for this town,
    and find it desirable to depart more swiftly than I came.”
  </p>
  <p>
    The people in alarm began loudly beseeching him not to abandon them to the
    Spaniards’ wrath; many fell upon their knees, strong men wept, and old men
    tore their beards to demonstrate their loyalty.
  </p>
  <p>
    Gifts proportionate to the means and standing of the givers were hurriedly
    brought forward, each man being careful to mention his name and his
    offering, that both might be recorded in the books.
  </p>
  <p>
    And now before the raised seat of the reader there rose a mighty mound of
    bales, chests, gold and silver vessels, jewels, baskets of fruit, and a
    quantity of money; even the poorest made shift to offer at least one silver
    coin.
  </p>
  <p>
    But Khaireddin surveyed the growing heap without enthusiasm; indeed his face
    darkened, and at last he raised his hand and said, “I knew that the town of
    Algiers was poor, but I could not have believed it to be as poor as this.
  </p>
  <p>
    In all the heap before me I cannot see one present of the sort that would
    appeal to me.
  </p>
  <p>
    Not that I made such a present a condition for my return; nevertheless I
    believed that you would so far have considered my wishes as to remember it.”
  </p>
  <p>
    The congregation were crestfallen indeed at his words, but Abu el-Kasim
    pinched my arm and together we pushed our way forward to Khaireddin’s
    throne.
  </p>
  <p>
    Abu el-Kasim addressed him, saying, “Poor though I am, I have awaited your
    coming with eager impatience, O lord of the sea!
  </p>
  <p>
    See, I bring you a good gift which I am persuaded will find favor in your
    eyes.
  </p>
  <p>
    Nor do I doubt that you will reward me in a fashion worthy of yourself.”
  </p>
  <p>
    The people were accustomed to look upon Abu as a clown; they wondered what
    this present prank of his might be, and put their hands to their mouths to
    suppress the burst of laughter they had in readiness.
  </p>
  <p>
    But the smile froze on their lips when, at a sign from Abu, I uncovered the
    golden dish and he seized Selim’s swollen head by the hair, holding it up
    for Khaireddin and all the rest to see.
  </p>
  <p>
    Now Selim ben-Hafs had in his time sorely injured Khaireddin, so it was no
    wonder that he laughed arrogantly at the sight of his enemy’s head, and
    clapping his hands together cried, “You’ve divined my innermost thoughts,
    good merchant, and your gift outweighs all the injuries done me in this
    city, which henceforth shall be my capital.
  </p>
  <p>Tell me your name.”</p>
  <p>
    Abu, grimacing in his excitement, gave his name, and Khaireddin contemplated
    his enemy’s head with rapture.
  </p>
  <p>
    With a sweeping gesture he cried, “Take all this rubbish, Abu el-Kasim my
    loyal servant, and share as much of it with your slave as you think fit.
  </p>
  <p>
    The givers of these things shall carry them to your house and so appreciate
    the regard I have for you.”
  </p>
  <p>
    For once Abu el-Kasim stood speechless amid the awed murmur of the throng.
  </p>
  <p>
    Then Khaireddin awoke from his rapture and, with a sideways glance at the
    great heap, added quickly, “Naturally a tenth must be paid into my treasury,
    as in the case of prizes captured at sea.
  </p>
  <p>
    And further—” As by magic Abu el-Kasim regained his speech and sought to
    drown further retractions by loud cries and the invocation of coundess
    blessings upon the head of Khaireddin, wherein I seconded him with all my
    might.
  </p>
  <p>The ruler began to relent, and stroked his dyed beard.</p>
  <p>
    But the Faqih hastily interposed, saying, “Allah blesses the open- handed,
    and you, Abu el-Kasim, shall carry nothing away until the mosque has
    received its fifth of the gold and silver and its tenth of all other wares.
  </p>
  <p>
    That the valuation may be fair and impartial, I call upon the foremost
    merchants of the city to effect it.”
  </p>
  <p>Abu’s jaw dropped.</p>
  <p>
    Looking up reproachfully at Khaireddin he said, “Alas, why did you act with
    such ostentation, O lord of the sea?
  </p>
  <p>
    You might as easily have given me these things when we were alone together,
    without witnesses.
  </p>
  <p>
    I could then have decided for myself, according to my own conscience, what
    my obligations were.”
  </p>
  <p>
    g y y g Delight in the misfortunes of others is of all delights the keenest,
    and Abu el-Kasim’s despairing face aroused exultation in the hearts of
    everyone.
  </p>
  <p>
    He hurled himself madly upon the merchandise and behaved in so eccentric a
    fashion that not even the great Khaireddin could keep a straight face.
  </p>
  <p>
    But at length he wearied of it all, and mindful of his dignity he rose and
    left the mosque attended by his officers, amid the benedictions of the
    crowd.
  </p>
  <p>Outside he distributed liberal alms.</p>
  <p>
    Moved by the general rejoicing, the Turkish janissaries began firing salvos
    with their muskets, while down in the harbor the artillerymen joined in and
    discharged their cannon until we were deafened, and market place and mosque
    were enveloped in smoke.
  </p>
  <p>
    Captain de Varga, the Spanish commander, could hardly be blamed for
    answering the fire, since the guns in the harbor were trained on the
    fortress and their shot tore holes in the wall of the keep.
  </p>
  <p>
    I fancied the noise was all salutes and salvos until something crashed into
    the wall of the mosque.
  </p>
  <p>
    I ran out in a fright, to see the great minaret toppling in a cloud of lime
    dust.
  </p>
  <p>
    Nothing more fortunate for Khaireddin’s purpose could have occurred, for the
    crowds, filled with righteous indignation, accused the Spaniards with
    shrieks of deliberately firing upon the mosque.
  </p>
  <p>
    Captain de Varga himself must have been appalled at what he had done, for
    the firing soon ceased.
  </p>
  <p>
    But Khaireddin proclaimed in a voice of thunder that this sacrilege should
    be the last crime committed by Christians in Algiers.
  </p>
  <p>
    For Abu el-Kasim the incident was a gift from heaven, since the merchants
    were in a hurry to get home, and the Faqih suddenly remembered that it was
    his hour for solitary meditation.
  </p>
  <p>
    The valuation of the goods was therefore very summarily performed and much
    to the advantage of Abu el-Kasim, who professed willingness to remain in the
    mosque all day for the sake of a fair and equitable assessment.
  </p>
  <p>
    Our house in the street of the spice merchants stood at a relatively
    sheltered corner, and Abu el-Kasim hastened, not without difficulty, to
    convey his new possessions home.
  </p>
  <p>
    With the help of a few courageous donkey drivers we at last had everything
    secured behind bolts and bars.
  </p>
  <p>
    I began to feel great anxiety about my brother Andy, and wanted to visit the
    palace and help him in any way I could.
  </p>
  <p>
    At first Abu refused on any account to let me go, saying that the deaf-mute
    could not be left to guard the treasure alone.
  </p>
  <p>
    But when I mocked him for becoming a slave to his own greed, instead of
    trusting to Allah as the best watchman, he cursed and swore indeed.
  </p>
