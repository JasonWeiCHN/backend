    It would flatter his vanity to receive the horsetail, a kaftan of honor, and
    a personal letter from the Sultan.
  </p>
  <p>
    And to my mind such a mark of favor would be a modest price to pay for
    Khaireddin’s mighty fleet and fine seamen.
  </p>
  <p>
    ” Beneath Ibrahim’s dark gaze I felt no wish to resort to fulsome flattery
    or exaggeration, and believed I could best serve Khaireddin’s cause by
    giving my honest opinion of him.
  </p>
  <p>Yet with my whole heart I longed to win the Grand Vizier’s confidence.</p>
  <p>
    So strong was his charm that I desired his favor for its own sake, without
    thought of the advantages it could bring me.
  </p>
  <p>
    He questioned me very thoroughly, and with practical knowledge, on the
    subject of Khaireddin’s building works and other activities, until Master
    Gritti interrupted him, and turning to me asked, “Can this Khaireddin sail
    oceans as well as seas, to crush the Portuguese spice trade and hinder
    Spanish traffic with the New World?
  </p>
  <p>
    ” Ibrahim said, “The Sultan of sultans and lord of all peoples is no spice
    dealer.
  </p>
  <p>
    In furthering the interests of the illustrious Republic, Aloisio Gritti, you
    see no further than your own nose and your immediate advantage.
  </p>
  <p>
    The shortest way to the control of the spice trade is over the Red Sea and
    the Persian Gulf.
  </p>
  <p>
    When once we have conquered Persia, the Ottoman fleet can sail unmolested to
    destroy the Portuguese trading posts in India.
  </p>
  <p>
    Nothing can then prevent us from digging a canal between the Mediterranean
    and the Red Sea, thus rendering pointless the Portuguese discovery of the
    passage round the southern tip of Africa.
  </p>
  <p>
    But there’s a time for everything, and first the Emperor must be defeated.
  </p>
  <p>” Master Gritti, discomfited, was silent.</p>
  <p>
    The Grand Vizier turned to me and went on, “No, we’re not spice dealers, and
    the Sultan has no real enemy but the Emperor, Charles V, for we’re now
    allied with Venice and the French King, and even to some extent with the
    Pope.
  </p>
  <p>
    The King of France is once more in difficulties, and to relieve him the
    Sultan must oppose the Emperor, or at least obtain from him fair terms of
    peace for France.
  </p>
  <p>
    It will be for Khaireddin to block the Imperial sea power when our army
    opens its campaign in the spring.
  </p>
  <p>
    If Allah wills we shall defeat Ferdinand, the Emperor’s brother, and take
    possession of his domains, for so long as the war with France continues
    Charles can send him no help.
  </p>
  <p>
    It’s true that the Emperor is negotiating secretly with Tahmasp, the Shah of
    Persia, and sooner or later the Sultan must fight the Emperor on Persian
    soil as well, at the same time liberating the holy tombs of Islam from the
    hands of the red-haired Shiites.
  </p>
  <p>
    But the cornerstone of Ottoman policy is neither more nor less than the
    blocking of Imperial world dominion which, were it to continue, would
    destroy the freedom of all peoples.
  </p>
  <p>
    Anything therefore which harms the Emperor helps the Sultan, and vice versa.
  </p>
  <p>Grasp this and you grasp all.</p>
  <p>
    ” Master Gritti, who was evidently bored, emptied another cup of wine and
    said, “Master Michael Carvajal—you’ll allow me to address you thus, for I
    happen to know that Master Venier of Venice made out your pass in that name.
  </p>
  <p>
    Well, Master Michael, the Ottoman emblem is the bald-necked vulture that
    appeared to Osman in a dream.
  </p>
  <p>
    Clearly, in order to survey wider expanses than come within the range of
    ordinary mortals, the vulture must mount high in the heavens.
  </p>
  <p>
    I, poor earthbound man, am more interested in the spice trade and the best
    way to protect Venetian merchant shipping against the pirates of Islam.
  </p>
  <p>
    For these are matter-of-fact, everyday problems, and their solution will
    bring many benefits.
  </p>
  <p>
    Our violinist should content himself with capturing Vienna and bestowing the
    crown of Hungary on my friend Zapolya, who has humbly sought the aid of the
    High Porte.
  </p>
  <p>
    For he is the lawfully elected king of the downtrodden Hungarian people,
    whose arrogant lords have accepted King Ferdinand as their ruler.
  </p>
  <p>By law, only a native of Hungary may wear the sacred crown of St.</p>
  <p>
    Stephen, yet the Viennese King’s German men-at-arms are still roistering in
    Buda.
  </p>
  <p>
    The forces of the Crescent ought to have freed Hungary from the German yoke
    as long ago as last summer.
  </p>
  <p>
    ” The Grand Vizier only smiled and drew a few pleasing notes from his
    violin.
  </p>
  <p>
    “Last summer Allah sent heavy rains and floods in our path,” he said, “but
    next summer Vienna shall be captured and the faithful Zapolya shall receive
    his well-earned reward.
  </p>
  <p>
    For as you may know, the Sultan has sworn by the Prophet and by his sword to
    be Zapolya’s true friend and shield him from all his enemies.
  </p>
  <p>
    ” Aloisio Gritti made a wry face and said, “And King Zapolya swore, too,
    through his ambassador.
  </p>
  <p>
    He swore by the living God and Jesus our Saviour, who is God also, that he
    would ever remain a friend to the friends of Sultan Suleiman and an enemy to
    his enemies.
  </p>
  <p>
    But while you were playing your fiddle the greedy landowners and the Germans
    oppressed the people and left them destitute.
  </p>
  <p>” “Allah’s will be done,” returned the Grand Vizier.</p>
  <p>
    To me he said, “You may apply with full confidence to Master Gritti for any
    information regarding the Christian states.
  </p>
  <p>
    Through him we learn not only the secrets of the illustrious Republic, but
    also news from King Zapolya of matters great and small in Germany and the
    Viennese court.
  </p>
  <p>
    ” His face darkened, and springing up he cried, “Crowns and coronations are
    but a mirage to delude the foolish.
  </p>
  <p>Not the crown but the sword confers sovereignty.</p>
  <p>
    Lands trodden by the Sultan’s chargers are forever united with his realms.
  </p>
  <p>
    Therefore I, too, burn with impatience to open the greatest campaign in the
    history of the Ottoman Empire.
  </p>
  <p>
    If afterward Zapolya reigns as king of Hungary, it will be by the Sultan’s
    favor, to ensure free passage through his domains at all times.
  </p>
  <p>
    ” Although I well understood that these preparations for a campaign which
    indirectly threatened the whole of Christendom greatly exceeded in
    importance the affairs I had on hand, yet I strove like Master Gritti to
    keep my feet firmly on the ground; I asked what reception was to be given to
    Khaireddin’s envoy.
  </p>
  <p>
    The Grand Vizier replied, “The Sultan still regards Khaireddin as a common
    pirate who, with his brother, betrayed the trust placed in him by the
    Sultan’s father, Selim.
  </p>
  <p>
    Khaireddin also has the second and third viziers against him, and I advise
    you to provide handsome presents for these men.
  </p>
  <p>
    But foremost among his opponents are the Sultan’s sea pashas who fear and
    envy him.
  </p>
  <p>
    He has a trustworthy supporter in the Chief Pilot, the learned navigator
    Piri-reis.
  </p>
  <p>
    Piri-reis has drawn a chart by whose help anyone may sail the Mediterranean
    with safety; when y p y y y you meet him, praise this work.
  </p>
  <p>
    Since a few copies of it fell into the hands of Christians it has ceased to
    be a secret.
  </p>
  <p>
    Piri-reis is an elderly man who lives among papers and feels no resentment
    for Khaireddin.
  </p>
  <p>
    The only gifts that please him are Christian charts which he likes to
    compare with his own.
  </p>
  <p>
    Tomorrow I intend to take up the question of Khaireddin with the Divan; I
    shall mention the magnificent gifts he has sent, and stress his firm
    intention of turning Algiers into an impregnable naval base.
  </p>
  <p>
    If Allah wills it, the Sultan himself shall receive the deputation in
    person, and the other viziers must accept the position with what grace they
    may.
  </p>
  <p>
    ” After giving me further instructions and addressing a kind word or two to
    Andy, he dismissed us.
  </p>
  <p>
    Master Gritti escorted us past the Negro guards to a side door, and before
    we left him he said, “If you’re indeed a man of education, Master Michael,
    and find time hanging heavily on your hands, come and visit me without fear
    of intruding; I enjoy listening to Seraglio gossip.
  </p>
  <p>
    The Seraglio is if possible a worse hotbed of gossip and intrigue than even
    the Vatican or the Emperor’s Court.
  </p>
  <p>
    I can offer you some unusual enjoyments, too, and acquaint you with vices
    with which because of your youth you’re perhaps still unfamiliar.
  </p>
  <p>
    I regret that this evening I was unable to offer you some young slave girl,
    for I have a number of these in my service, of different races and color and
    all expert in the erotic arts of their own countries.
  </p>
  <p>Indeed, I believe you’ll be astonished.</p>
  <p>
    ” I thanked him politely for his great kindness and promised to call upon
    him as soon as I had news from the West, when we could exchange useful
    information.
  </p>
  <p>
    But in my heart I resolved to keep as far as possible from this false man
    whose masterly intrigues made him dangerous company for me, while for
    Giulia’s sake I dared not even consider his hospitable offers.
  </p>
  <p>
    The silent clerk escorted us back to the shore, spoke to the guards and led
    us onto the quay where our boatman was dozing, half-naked though the autumn
    night was chilly.
  </p>
  <p>
    The crescent moon shone like a drawn scimitar above the great dome of the
    mosque as we glided over the Golden Horn to our ship.
  </p>
  <p>
    We were unmolested, though a couple of janissaries on the shore stared at us
    as we climbed aboard.
  </p>
  <p>
    Next morning I told Torgut-reis and the eunuch all that had happened and
    urged them to await the summons to the Seraglio in all confidence, since by
    diplomacy I had succeeded in winning over the Grand Vizier to Khaireddin’s
    cause.
  </p>
  <p>
    At first the eunuch would not believe that I had met the Grand Vizier in
    person, but while we were yet talking a horseman arrived to bid us make
    ready to appear before the Sultan.
  </p>
  <p>
    Soon afterward cooks and scullions arrived in great numbers, bearing with
    them in Chinese bowls an abundant meal from the Divan kitchen.
  </p>
  <p>
    After the noon prayer a hundred p y mounted spahis clad in purple suddenly
    appeared.
  </p>
  <p>
    Their jeweled weapons flashed in the sun, and their saddle cloths were
    adorned with heavy turquoises.
  </p>
  <p>
    Their Aga presented Torgut with a gift from the Sultan—a magnificent horse
    whose bridle and saddle were ornamented with silver, pearls, and precious
    stones.
  </p>
  <p>
    Overjoyed at this splendid present, Torgut-reis gave me thirty ducats and
    the eunuch added a somewhat smaller sum.
  </p>
  <p>We then set forth in ceremonial procession to the Seraglio.</p>
  <p>
    Huge crowds hailed us as we passed, and called down blessings upon our
    heads.
  </p>
  <p>
    Slaves, black and white, carried Khaireddin’s presents, of which the most
    gorgeous had been uncovered for all the people to behold.
  </p>
  <p>
    Ten beautiful girls and boys carried coins and gold dust in baskets of
    plaited palm leaves, so that we felt deeply thankful for the protection of
    our mounted escort.
