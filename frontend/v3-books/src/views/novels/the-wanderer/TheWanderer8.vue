    Through such agents Charles V sought to feel the Grand Vizier’s pulse on the
    question of the partition of the world.
  </p>
  <p>
    By boasting of his influence with the Sultan, Ibrahim would lead his
    opponent on to reveal his true motives and aims.
  </p>
  <p>
    Yet however warmly he appeared to approve the proposals he was careful never
    to commit himself.
  </p>
  <p>
    The Sultan himself made no pronouncements, and where this subject was
    concerned would have nothing whatever to do with foreign spokesmen.
  </p>
  <p>
    Nevertheless he was always intensely interested to discover through the
    Grand Vizier how far the Emperor was willing to compromise.
  </p>
  <p>
    I believe that both Sultan and Grand Vizier sincerely wished for peace at
    this time, yet all the fumbling conferences came to nothing because neither
    side would trust the other.
  </p>
  <p>
    It was in principle impossible for the Sultan, as Ruler of the Faithful, to
    consider a lasting peace with unbelievers, since the Koran expressly forbade
    such a policy.
  </p>
  <p>
    And for his part the Emperor, like the cynical statesman he was, would
    naturally take the first opportunity of uniting the Christian countries
    against the Sultan regardless of fair promises and secret treaties, because
    he rightly saw in the Ottoman Empire a constant menace to imperial power and
    to Christendom itself.
  </p>
  <p>
    Sorrowfully I now learned the futility of all politics and saw that however
    lofty his motives, man cannot control the march of events.
  </p>
  <p>
    The Grand Vizier required my presence at these meetings so that if necessary
    I could testify that he had ever acted in his lord’s best interests.
  </p>
  <p>
    And as I listened I acquired a widening knowledge of political problems.
  </p>
  <p>
    I learned that one could talk long and eloquently and yet say nothing, and
    all too plainly I beheld the pettiness, selfishness, vanity, and weakness of
    mankind.
  </p>
  <p>
    The company of poets and dervishes had trained me in discerning the
    emptiness of worldly honors.
  </p>
  <p>
    I tried not to set too much store by my position, provided I might keep my
    fortune, for thanks to this, Giulia could live the life she craved and I was
    spared her eternal nagging.
  </p>
  <p>
    She measured success in money and valuables, and in her more amiable moments
    she would even admit that I had not proved so unenterprising as she had
    feared.
  </p>
  <p>
    She would have liked to see me p g stand with folded arms and modestly
    lowered eyes in the colonnaded chamber of the Divan when the kaftans of
    honor were conferred, but happily she found enough food for her vanity among
    the ladies of the harem.
  </p>
  <p>
    Even the Sultan’s mother received her in the Old Seraglio, though she
    suffered a severe heart attack because of Giulia’s prophecies.
  </p>
  <p>
    For I had cautiously guided Giulia’s thoughts in the right direction, and
    she was so rash as to predict that Sultana Khurrem’s son Selim would succeed
    to the Ottoman throne.
  </p>
  <p>
    Strangest of all, Giulia herself implicitly believed her own prophecy and
    began to behave toward Prince Selim with the utmost respect and veneration.
  </p>
  <p>
    From time to time she would bring me news or warnings plainly originating
    with Sultana Khurrem and intended by that guileful woman to reach the Grand
    Vizier through me.
  </p>
  <p>
    But for his part, Ibrahim could not reconcile it with his dignity to enter
    into any communication with the Sultana with Giulia as intermediary.
  </p>
  <p>
    In this he made a great mistake and underrated the Sultana’s terrifying
    strength of will and vigilant ambition.
  </p>
  <p>But who at that time would have done otherwise?</p>
  <p>
    In the courts of the West the Sultana was known as Roxelana, the Russian
    woman.
  </p>
  <p>
    Presents, even from Christian princes, streamed to her through the golden
    portals of the harem; incredible stories were told of her luxurious way of
    life and her gorgeous clothes.
  </p>
  <p>One of her gowns was reputed to have cost a hundred thousand ducats.</p>
  <p>
    There were also tales of her cruel jealousy that made life in the harem a
    hell.
  </p>
  <p>
    If any woman there sought to attract the attention of the Sultan, or if he
    by chance glanced at one of them, Sultana Khurrem laughed gaily and saw to
    it that she disappeared.
  </p>
  <p>
    I cannot say with certainty what gifts were sent her by the Emperor’s secret
    envoys or the King of Vienna, but during those uneasy months she did her
    utmost, Giulia told me, to induce the Sultan to make a treaty with the
    Emperor.
  </p>
  <p>
    Politically, of course, this was madness, for the Emperor had just been
    crowned by the Pope and had concluded peace with France, and thus stood at
    the height of his power.
  </p>
  <p>
    In the Diet of Augsburg he even succeeded in frightening the Protestant
    princes into obedience, and confident of victory was now preparing to make
    war on the Sultan.
  </p>
  <p>
    Indeed, in his quality of Most Catholic Majesty he implicitly obeyed the
    exhortation of Scripture not to let his right hand know what his left hand
    did.
  </p>
  <p>
    While secretly offering his left hand to the Sultan in token of peace, he
    slipped his right into a steel gantlet to deliver a crushing blow.
  </p>
  <p>
    Never before or since can the Ottoman Empire have been in such peril, and
    the Sultan’s desire for peace was easy to understand.
  </p>
  <p>
    Fortunately, the only result of Charles V’s ultimatum to Germany was the
    founding by Philip of Hesse of a league of princes, in support of Luther’s
    teaching.
  </p>
  <p>
    King Zapolya and the King of France certainly had a finger in the pie, but
    the secret and I believe decisive reason for the princes’ defiance was
    Ibrahim’s promise of support in the event of war between them and the
    Emperor.
  </p>
  <p>
    Which of these princes had their religious zeal stimulated by Turkish gold I
    cannot say, but Philip of Hesse at least found means to pay and equip his
    troops in a manner unaccountable to the Christians.
  </p>
  <p>
    I had my own reasons for frequently recalling the thin face and cold blue
    eyes of this man.
  </p>
  <p>
    Compared with the league he had formed, Father Julianus’s harmless
    preachings through Germany were of small significance.
  </p>
  <p>
    Luther and his pastors were now beginning to watch over the purity of their
    doctrine as jealously as ever did Holy Church, and to my sincere grief I
    must record that Father Julianus never returned to claim his bishopric.
  </p>
  <p>He was stoned to death in a small provincial town.</p>
  <p>
    Thanks to the Schmalkald League, we were relieved of the heaviest of our
    anxieties, and the Sultan had no further need to listen to the advocates of
    peace.
  </p>
  <p>
    Grand Vizier Ibrahim on the contrary revived his ambitious plans for the
    conquest of the German states, with the Protestant princes’ support.
  </p>
  <p>
    I disliked war, yet since for the well-being of the army a fresh campaign
    was necessary, it seemed to me that we had much to gain and nothing to lose
    by marching once more on Hungary.
  </p>
  <p>
    Among the mountains and barren wildernesses of Persia even a large army
    could vanish like a needle in a haystack.
  </p>
  <p>
    But in Germany the Schmalkald League bound the Emperor’s hands, and so
    favorable an opportunity might never return.
  </p>
  <p>
    For Andy’s sake above all I looked upon war as something absolutely
    necessary, and I blamed myself for having neglected and forgotten my loyal
    friend for so long.
  </p>
  <p>
    One spring morning, when the tulips in my garden had unfolded their bright
    red and yellow cups, and fresh sea winds swept in from the sparkling
    Bosphorus, Andy knocked at my gate.
  </p>
  <p>
    Hearing the shouts of the porter I hurried up and at first failed to
    recognize my old friend.
  </p>
  <p>
    He came in barefoot with a sack on his back, wearing dirty leather breeches
    and a ragged turban, and I took him for one of the beggars that squatted in
    such numbers about my door.
  </p>
  <p>
    When I saw who it was I cried out in amazement, for Andy’s sturdy legs
    trembled with weariness, and his pale, staring face was twitching.
  </p>
  <p>
    He dropped the sack, pulled off his turban, and having gazed dully at me for
    some moments he said, “In the blessed name of the Prophet, Michael, get me
    something to drink —something strong—or I shall lose the remainder of my
    poor wits.
  </p>
  <p>
    ” I took him to the boathouse, drove out the Negroes who slept there, and
    with my own hands fetched him a keg of rare malmsey from the cellar.
  </p>
  <p>
    Andy knocked out the head of the keg, which he carried to his mouth, and in
    great gulps he drank half of what it contained.
  </p>
  <p>
    Soon the trembling in his limbs ceased and he sagged to the floor with a
    thud that shook the boards and sent dust flying from the joints of the
    walls.
  </p>
  <p>
    Then, hiding his face in his hands, he drew a deep breath and uttered so
    rending, so despairing a sob that I in my turn began to quake for dread.
  </p>
  <p>
    “Michael,” he said, “I don’t know why I should burden you with my sorrows,
    but a man must turn to some friend at such a time.
  </p>
  <p>
    I don’t want to grieve you, but things are bad with me—as bad as they can
    be.
  </p>
  <p>Better if I had never been born into the misery of this world.</p>
  <p>” “What in the name of Allah has happened?</p>
  <p>” I cried, in the deepest agitation.</p>
  <p>“You look as if you’d murdered someone.</p>
  <p>
    ” His bloodshot eyes were upon me as he answered, “I’ve been dismissed from
    the arsenal.
  </p>
  <p>
    They tore the plumes from my turban and kicked me out—they shook their fists
    and threw my belongings after me.
  </p>
  <p>I’m wretched, wretched.</p>
  <p>” Relieved that it was no worse I admonished him, saying, “Is that all?</p>
  <p>You should have known what comes of drinking.</p>
  <p>
    But even if you have lost your pay, you’ve your wife’s fortune to turn to.
  </p>
  <p>
    ” With his head still in his hands he retorted, “I care nothing for the
    arsenal.
  </p>
  <p>
    We had an argument about the cannon and I told them their war galleys were
    only good for firewood.
  </p>
  <p>
    I wanted them to build bigger vessels to carry heavy ordnance, like the
    Venetians and Spaniards.
  </p>
  <p>So I went.</p>
  <p>He laughs best who laughs last.</p>
  <p>
    But I’m a sorrowful man and don’t expect to laugh ever again in this world.
  </p>
  <p>
    ” He seized the keg and poured more wine down his throat before continuing,
    “Your good colleague Master Gritti is behaving like a maniac in Hungary, and
    all the Transilvanian lords are at each other’s throats.
  </p>
  <p>
    But whether Hungarians or Moldavians, Wallachians or Tartars, all are agreed
    that no Mussulman shall own land in Hungary.
  </p>
  <p>
    My deed of conveyance from King Zapolya they put to what they considered its
    fit use before my very eyes, and have long since divided my flocks among
    themselves, slaughtered my cattle, and razed all the buildings to the
    ground.
  </p>
  <p>
    That poor Jew will suffer great loss, and I can’t get back a penny on all my
    lands, though they’re so wide that it takes a day and a night to ride from
    end to end of them.
  </p>
  <p>
    Sweet songs are brief songs, they say, and I own little but the breeches I
    have on.
  </p>
  <p>
    ” “But—but—” I stammered, realizing that I should have to take care of poor
    Andy once more, despite the friction this would cause with Giulia.
  </p>
  <p>
    Then, summoning courage, I clapped him on the shoulders and said, “We’ll
    find some way out, my dear Andy.
  </p>
  <p>But what has your wife to say to all this?</p>
  <p>” “My wife,” said Andy absently.</p>
  <p>He raised the keg and emptied it at a draught.</p>
  <p>“I must have forgotten to tell you.</p>
  <p>The poor little girl is dead.</p>
  <p>And it was not an easy death.</p>
  <p>She suffered for three days before she went.</p>
  <p>” “Jesus, Mary!</p>
  <p>” I cried, striking my hands together.</p>
  <p>“That is, Allah is Allah—Why did you not tell me this at first?</p>
  <p>I feel for you most deeply in your great sorrow.</p>
  <p>How did she die?</p>
  <p>” “In childbirth, in childbirth!</p>
  <p>” said Andy in a tone of wonder.</p>
  <p>“And that was not the worst, for the child died, too.</p>
  <p>” And so at last I learned all that had befallen Andy.</p>
  <p>
    He hid his face in his hands again and broke into such terrible weeping that
    the walls of the boathouse shook.
  </p>
  <p>I could find no words to comfort him in his boundless grief.</p>
  <p>“It was a boy,” he managed to say at last.</p>
  <p>
    Then, enraged at his own weakness, he’ swore for the first time in many a
    long month in his own rough mother tongue, “Per%ele!
  </p>
  <p>
    ” Without a word I returned to the cellar and fetched another barrel of
    wine.
  </p>
  <p>He wiped his eyes with the back of his hand.</p>
  <p>“My little foal!</p>
  <p>Her cheeks were like peaches and her eyes like bilberries.</p>
  <p>I don’t understand.</p>
  <p>
    But even in the early days the Jewish physician advised her to take the
    baths at Bursa, and I’m glad to remember that she made the journey like a
    princess, though at the time I grumbled foolishly at the expense.
  </p>
  <p>
    The physician told me in his learned jargon that her organs had grown askew
    from too much riding as a young girl.
  </p>
  <p>
    And her loins were hard as ash, for young Hungarian ladies are in the wicked
    habit of riding astride like men.
  </p>
  <p>” “Dear Andy, my brother and my friend!</p>
  <p>All these things were written in the stars before your birth.</p>
  <p>
    Sweet songs are brief songs, as you say, and you lived in your happiness so
    long as it pleased Allah.
  </p>
  <p>Who knows?</p>
  <p>She might have lived to weary of you and make eyes at some other man.</p>
  <p>” Andy shook his heavy head.</p>
  <p>
    “Stop chattering, Michael, and tell me—were their deaths sent to punish me
    for deserting the Christian faith?
  </p>
  <p>
    I believe I’m as good a Moslem as any, though I can’t recite all the
    prayers.
  </p>
  <p>
    In my heart I’ve never denied our Lord or His mother— Mussulmans venerate
    them, too—and I’ve been sneered at for never treading the Cross underfoot.
  </p>
  <p>
    But as I was roaming about the city in my anguish I chanced to enter the
    Christian church, and when I heard the intoning of the priest and the
    ringing of the bell I seemed to hear also the devil himself laughing at me
    in mockery, because I’d forsaken God of my free will and at your bidding.
  </p>
  <p>For God’s sake, help me, Michael, and give me peace again.</p>
  <p>
    My son was not baptized and my wife neglected both confession and communion
    after our marriage, though in other ways she was a good Christian.
  </p>
  <p>
    It is frightful to think that because of my falling away they must burn in
    eternal fire.
  </p>
  <p>” I could not but reflect seriously upon what he said.</p>
  <p>
    With trembling hands I raised the wine to my lips and sought in it the
    courage that I lacked.
  </p>
  <p>
    I thought it unfair of Andy to blame me for his defection, and said with
    some heat, “Pray remember that we took the turban independently of one
    another; I never asked you to do it.
  </p>
  <p>
    Though if we must go to hell for our sins I admit we shall most likely go
    side by side—indeed, for once I may be a step ahead of you, being a scholar
    and therefore more answerable for my actions than you.
  </p>
  <p>
    ” Andy replied impatiently, “I’ll account to the Lord for my own actions
    without troubling you.
  </p>
  <p>But why did He strike down my wife and son?</p>
  <p>What sin can my little boy have committed?</p>
  <p>
    I learned as a child how vain it is for a poor man to hope for justice in
    this world, but all the more confidently did I hope for it in the next.
  </p>
  <p>
    ” I know not whether the wine had given me courage or merely clouded my
    judgment, but for the first time in my life I confessed to myself that I was
    the worst of all heretics.
  </p>
  <p>
    “Andy,” I said gravely, “I’m weary of quibbles and of juggling with words.
  </p>
  <p>
    Only in a man’s own heart is God to be found, and no man can save another by
    expounding texts, be they in Latin, Arabic, or Hebrew.
  </p>
  <p>
    If indeed there is an eternal, omnipotent, and omniscient God, would He
    trouble to aim His wrath at a poor worm like you?
  </p>
  <p>
    ” Andy’s head shook and tears rolled into his great hands as he said,
    “Perhaps you’re right, Michael.
  </p>
  <p>Who am I that God’s great cannon should be trained on me?</p>
  <p>
    Give me a truss of straw to lie upon for a few days, Michael, and a little
    bread; I will get over this as best I may and consider how to start life
    afresh.
  </p>
  <p>It’s only in stories that men win a princess and half a kingdom.</p>
  <p>
    In the days of my great happiness I used to fancy that I must be dreaming,
    and soon I think I shall be able to believe it.
  </p>
  <p>
    First I will take the edge off sorrow by getting properly drunk; then in the
    drabness and headache of waking I shall remember the past in all humility as
    a dream too fair for an oaf like me.
  </p>
  <p>
    ” His resignation so deeply moved me that I too wept, and together we
    mourned the sorrows and vanities of life.
  </p>
  <p>
    Andy being already very drunk I fetched a sleeping draught from my medicine
    chest, and mixed enough of it with his wine to stun an ox.
  </p>
  <p>
    Soon he sank back unconscious on the floor, to all appearances dead save for
    a faint whistling in his nose.
  </p>
  <p>
    He slept for two days and nights, and when he woke he took a little to eat.
  </p>
  <p>
    I did not vex him with needless chatter, but left him alone to dangle his
    legs from the jetty and stare at the restless waters of the Bosphorus.
  </p>
  <p>
    Some days later he came to me and said, “I know I’m a burden to you here,
    and especially to your wife, so I shall keep out of the way and live with
    your Negroes in the boathouse, if you’ll let me.
  </p>
  <p>But give me work to do—the heavier the better.</p>
  <p>
    Idleness irks me, and I would like to do something in return for my food and
    sleeping place.
  </p>
  <p>
    ” I was abashed at his words, for Giulia had indeed pointed out somewhat
    sharply that Andy ate at least three aspers’ worth of food a day and used a
    mattress and blanket that properly belonged to the Negroes; she also
    suggested that he should bestir himself a little to earn his keep.
  </p>
  <p>
    And although I should have preferred to see Andy treated as an old friend of
    the family, I summoned Alberto and asked him to find suitable work—a request
    that he seemed to have been expecting.
  </p>
  <p>
    He took Andy at once to the northwest corner of the garden, which had not
    yet been cleared, and told him to break stones and build a terrace there.
  </p>
  <p>
    It was an improvement long planned, and postponed because of the expense.
  </p>
  <p>
    Andy also chopped wood and carried water to the kitchen, and all with such
    good will that even the slaves began leaving him their work to do.
  </p>
  <p>
    He tried to avoid us, but Giulia often purposely stood in his path to gloat
    over his degradation.
  </p>
  <p>
    Nevertheless it seemed to me at times that Andy, as he bowed and shambled
    off to do her bidding, was silently laughing at her; this I took as a sign
    of his recovery.
  </p>
  <p>
    War was again imminent, and this time camel trains set forth months in
    advance, carrying bridge-building materials to the banks of the Danube
    tributaries.
  </p>
  <p>
    Charles V proclaimed the Turkish peril in all the German states, and by thus
    spreading alarm among the people he succeeded also in inflaming them against
    the Protestant princes.
  </p>
  <p>
    I could not but admire his astute use of a situation on which the Grand
    Vizier had based his own hopes for a successful campaign.
  </p>
  <p>
    I observed these things with the impartial eye of an onlooker, and one with
    personal experience of German lands of which Ibrahim, as a Moslem, could
    have only a dim idea.
  </p>
  <p>
    To my great joy Ibrahim thought it best for me to remain in Istanbul in
    charge of his secret business, though I could not tell from his expression
    whether this order was a mark of special favor or a sign of lessening
    confidence.
  </p>
  <p>
    Mustafa ben-Nakir had lately arrived in the Sultan’s capital, having
    journeyed first from Persia to India in company with old Suleiman the
    eunuch, Viceroy of Egypt, and then after countless adventures returned to
    Basra aboard an Arabian smuggler.
  </p>
  <p>
    He had grown thinner and his eyes seemed bigger than before, but he was
    otherwise unchanged.
  </p>
  <p>
    The scent of the costly oils with which he anointed his hair spread
    agreeably through the room, the silver bells jingled at girdle and knee, and
    the book of Persian poems was worn with diligent use, I greeted him as a
    long-lost friend, and Giulia, too, was glad to see him.
  </p>
  <p>
    He sought out Andy and sat for a long time cross legged in the grass
    watching him break stones for the terrace.
  </p>
  <p>
    But although Mustafa ben-Nakir’s sole purpose in coming seemed to be to
    describe in glowing colors the wonders and the wars of India, he had in fact
    secret business with me and took me to meet the renowned eunuch Suleiman.
  </p>
  <p>
    Suleiman the eunuch was at this time a man of some seventy years, and so fat
    that his little eyes had almost disappeared.
  </p>
  <p>
    Four strong slaves were needed to raise him to his feet when once he had sat
    down.
  </p>
  <p>He had attained the viceroyalty of Egypt by his unswerving fidelity.</p>
  <p>
    In the past, otherwise competent viceroys of wealthy, decadent Egypt had
    fallen a prey to all manner of ambitious dreams until it seemed as if a
    curse lay over that ancient land.
  </p>
  <p>
    But because of his enormous bulk and his age, Suleiman was too lazy and also
    too astute to contemplate rebellion against the Sultan, and had of course no
    sons to whom he might be tempted to bequeath a crown, nor any ambitious wife
    to egg him on.
  </p>
  <p>
    And although he looked with delight upon beautiful slave girls and kept two
    of them about him to scratch the soles of his feet, this was, so far as I
    know, his only indulgence.
  </p>
  <p>
    He did not even care to rob the Sultan to any great extent, and punctually
    remitted his yearly tribute without arousing among his subjects the
    customary lamentations.
  </p>
  <p>
    He was thus a most unusual man and because of his independent position was
    almost equal in rank to the Grand Vizier.
  </p>
  <p>It was a great honor for me to be received and addressed by him.</p>
  <p>
    “Deeply though I deplore the needless trouble he puts himself to,” he began,
    “and his restless wandering from place to place, yet because of the beauty
    of Mustafa ben-Nakir’s eyes and his bewitching manner of reading poetry
    aloud I find myself compelled to listen to him.
  </p>
  <p>
    But now, no doubt because of his unpleasant experiences of Portuguese
    pirates in India, he has taken it into his head that the honor of Islam
    requires the liberation of the princes of Diu and Calicut from the
    Portuguese yoke.
  </p>
  <p>
    In the course of his long journeys he has formed useful friendships to this
    end, and has heard from reliable sources that these two unhappy princes
    would gladly welcome the Sultan’s sea janissaries as liberators.
  </p>
  <p>
    ” Mustafa ben-Nakir regarded me with his clear, innocent eyes and said,
    “Those brigands have stopped the Mussulman spice trade and carried away the
    cargos in their own ships round Africa to Europe.
  </p>
  <p>
    They oppress the inhabitants of Diu and rob the Arabian merchants —indeed,
    they rob even their own king by sending inferior spices to Lisbon and
    keeping the pepper to sell to Moslem smugglers at usurers’ prices.
  </p>
  <p>
    The Portuguese have instituted a reign of terror in India that is a disgrace
    to all Islam—to say nothing of the loss of trade, both to the Sultan’s
    dominions and to our true friends the Venetians.
  </p>
  <p>The unhappy Indians yearn for the coming of the Deliverer.</p>
  <p>” “Allah is Allah!</p>
  <p>” I said.</p>
  <p>
    “Let me hear no more of deliverers, Mustafa ben-Nakir; I am older and wiser
    than I was in Algeria, and the word leaves a taste of blood behind it.
