  </p>
  <p>
    The finest building of all, however, was the great mosque by the harbor.
  </p>
  <p>
    The Spanish island fortress commanded the harbor mouth, and Spaniards armed
    with swords and harquebuses rowed freely to and fro, or stalked haughtily
    among the populace, whom they compelled to make way for them.
  </p>
  <p>
    This offended many a devout Moslem, for by the law of the Koran no believer
    ought to step aside for an unbeliever, but should crowd him and jostle him
    out into the street.
  </p>
  <p>
    On our way about the city, Abu el-Kasim gathered together with many
    blessings the jars which, hidden in peasants’ grain baskets, had arrived at
    the houses of his merchant friends, and we carried them back to his house.
  </p>
  <p>
    The city was divided in a very sensible and practical manner into different
    quarters, in which each kind of merchandise and craft had its own street.
  </p>
  <p>
    Thus the coppersmiths kept to one alley, while tailors, tanners, dyers, and
    all other artisans each had theirs.
  </p>
  <p>
    Our own house was in the street of the spice merchants and dealers in drugs.
  </p>
  <p>
    It was one of the more respectable thoroughfares, since wealthy merchants as
    well as poor ones lived there, as could be seen by the crowd of beggars and
    cripples who squatted all day at rich men’s doors in the hope of alms.
  </p>
  <p>
    At noon Abu el-Kasim took us to the mosque, in whose forecourt was a marble
    basin supplied with fresh running water.
  </p>
  <p>
    We performed the prescribed ablutions and entered the mosque, carrying our
    slippers in our hands.
  </p>
  <p>
    There were costly carpets on the floor, many lamps hung from the roof by
    copper and silver chains, and columns of different colors supported the
    great dome.
  </p>
  <p>
    We murmured our purpose in coming and imitated the actions of the reader,
    kneeling when he knelt and bowing down as he did.
  </p>
  <p>
    After the prayers Abu elKasim took us to the madrasseh, or mosque school,
    where youths under the direction of gray-bearded teachers were studying the
    Koran, the duty of man, the traditions, and the law.
  </p>
  <p>
    Abu el-Kasim had given us clean clothes, and he now presented us to an
    elderly man with a white beard, saying, “Venerable Ibrahim ben Adam
    el-Mausili!
  </p>
  <p>
    In the name of the Compassionate I bring you two men who have found the
    faith and desire to follow the true path.”
  </p>
  <p>
    From that day forward, after the evening prayer, we attended the school for
    converts, to learn Arabic and the seven pillars, roots, and branches of
    Islam.
  </p>
  <p>
    Not even Fridays were excepted; for although Moslems leave their labor or
    their business to attend the noon service on that day, yet they count no day
    as a day of rest.
  </p>
  <p>
    In their opinion the Christian and Jewish manner of honoring the Sabbath is
    blasphemous, because it is based on the idea that God, after creating heaven
    and earth, rested upon the seventh day.
  </p>
  <p>
    Moslems g p y acknowledge that God created heaven and earth, but being
    omnipotent He must have done it without effort; the very notion that He
    could be in need of rest is blasphemy to them.
  </p>
  <p>
    When the old teacher Ibrahim ben-Adam observed my genuine desire for
    knowledge he conceived a liking for me and expounded the Koran to the best
    of his ability, and often I stayed on after the others had left until late
    in the evening.
  </p>
  <p>He was very devout and never wearied of reading the sacred writings.</p>
  <p>
    It was from him I learned that Islam has room in it for many paths whose
    followers dispute among themselves.
  </p>
  <p>
    But these questions did not disturb my peace of mind, for I studied the
    Koran with intellectual detachment, and solely from a desire for knowledge.
  </p>
  <p>
    I soon perceived that Christians had little to be proud of in their
    supposedly superior religion, for dogmatic disputes, sanctimoniousness,
    hypocrisy, and the nonobservance of fasts were features common to both
    persuasions.
  </p>
  <p>
    During the day I helped Abu el-Kasim to mix drugs and grind kohl to a fine
    powder for eye black.
  </p>
  <p>
    I also prepared a dye of indigo and henna, which gives a blue-black tinge to
    women’s hair.
  </p>
  <p>
    By kneading indigo leaves to a stiff dough we obtained a substance that
    women used to color their eyebrows dark blue; and Abu el-Kasim told me that
    the fine ladies of Baghdad often shaved off the eyebrows that Allah had
    given them, to replace them by penciled blue lines.
  </p>
  <p>
    But the most important of Abu el-Kasim’s wares were the leaves of henna that
    he obtained from Morocco, where they were gathered three times a year.
  </p>
  <p>
    Women moistened these and kneaded them to a greenish paste which they rubbed
    into their faces to freshen and rejuvenate their complexions; elderly women
    could not live without it.
  </p>
  <p>
    Henna was also used in the preparation of a dye for nails, hands, and feet.
  </p>
  <p>
    Abu el-Kasim had his own methods of making this, which enabled him to sell
    it at prices that varied according to the means of the customer.
  </p>
  <p>
    He taught me to knead a little lemon juice and alum into the henna paste,
    and so produce an orange-colored mixture for coloring the nails.
  </p>
  <p>
    He would mix some of it with rose water or essence of violets, put it into
    different pots under different names and price it according to the lure of
    those names.
  </p>
  <p>
    In this way he could charge many times the price of the original commodity.
  </p>
  <p>
    Vain men dyed their beards with henna, and fair-haired women could use it to
    turn their hair fiery red in the Venetian manner.
  </p>
  <p>
    Other preparations he made entirely himself, including the burning “paradise
    ointment,” which he declared could restore virginity to a prostitute, though
    she had visited all the ports of Africa and reached the age of forty.
  </p>
  <p>
    When I rebuked Abu for his heartlessness in robbing the poor by selling them
    worthless goods he looked at me with his monkey eyes and answered gravely,
    “Michael el-Hakim, you mustn’t blame me, for in selling these things I sell
    much more than their ingredients.
  </p>
  <p>
    I sell dreams, and the poor have greater need of dreams than the rich and
    fortunate.
  </p>
  <p>To aging women I sell youth and self- confidence.</p>
  <p>
    Besides, you’ll have noticed that I sometimes give away henna and rose water
    for some poor girl’s wedding, and so acquire merit.
  </p>
  <p>
    Don’t reproach me for selling dreams to others, though I’ve lost my own.”
  </p>
  <p>
    I give no opinion as to the rights or wrongs of this, and as to whether it
    is better to live unhappy in the truth or happy in a lie.
  </p>
  <p>
    However it may be, I helped Abu el-Kasim in every way I could and was
    flattered when he began to call me el-Hakim, the physician.
  </p>
  <p>It came about when he was seeking an Arabic name for me.</p>
  <p>
    Rearranging the letters of “Michael” or “Mikhael,” he produced to his own
    surprise the words “el-Hakim.”
  </p>
  <p>“There’s an omen indeed!”</p>
  <p>he cried.</p>
  <p>
    “As Michael the angel you did Sinan a service by inducing him to seek
    guidance in the holy book; now as el-Hakim the physician you shall serve me.
  </p>
  <p>May the conjunction be a fortunate one for both of us.”</p>
  <p>
    I first saw Sultan Selim ben-Hafs one Friday as he came riding down the
    steep street from the kasbah to the noon service in the mosque.
  </p>
  <p>
    He was attended by a flock of richly dressed slaves, and by a company of
    bowmen who, with arrows ready fitted to the bowstrings, closely scrutinized
    the lattice windows and flat roofs of the houses.
  </p>
  <p>
    In the forecourt of the mosque Selim made a disdainful gesture, and a
    sackful of square silver coins was flung to the poor.
  </p>
  <p>
    Within, having carelessly rattled off the prayers, he sat cross legged on
    his throne and dozed while passages from the Koran were read aloud.
  </p>
  <p>
    Thus I had a fair opportunity of watching him and studying his face, and I
    cannot say that I was attracted, for it was ravaged by vice and his drooling
    mouth hung open.
  </p>
  <p>
    He was middle aged; his face and his dark beard gleamed with rare ointments,
    and his bloodshot eyes framed in heavy, puffy lids were as lifeless as his
    mouth.
  </p>
  <p>Abu el-Kasim told me he ate opium.</p>
  <p>
    Afterward, on his return to the palace, Selim paused at the entrance to
    witness two executions and the flogging of some young boys who were bound to
    posts on either side of the gate.
  </p>
  <p>
    He let the whipping continue until blood was pouring down their backs, while
    he sat slumped in his saddle with a hanging lip, dully looking on.
  </p>
  <p>
    If the Hafsids had ruled Algiers for three hundred years, I thought, it was
    at least one hundred years too long.
  </p>
  <p>
    I soon came to love Algiers—the street where I lived, and the people who
    talked to me.
  </p>
  <p>
    This foreign city, with the strange smells and colors, the charcoal
    braziers, the fruit trees, and the many ships in the harbor, was like a city
    from some story book.
  </p>
  <p>
    Each day I ate y y y mutton and rich broth; often Abu el-Kasim with a sigh
    would loosen his purse strings and give me a few square silver coins, and I
    would go to the market to buy plump ptarmigan, which Giulia afterward
    dressed with lavish seasoning.
  </p>
  <p>For Giulia had gradually become reconciled to her lot.</p>
  <p>
    Abu elKasim pleased her by taking her to the bazaar and buying her
    beautifully wrought bracelets and anklets of silver.
  </p>
  <p>To my annoyance she dyed her fair hair red.</p>
  <p>
    Her nails, the palms of her hands, and her feet up to the anklebones were
    always orange-red, and she also painted her eyelids and eyebrows after the
    fashion of Algerian women.
  </p>
  <p>
    To her credit be it said that she soon wearied of our housekeeping and took
    it into her own hands, inducing Abu el-Kasim to repair the house and even
    demanding a covered pool in the courtyard, so that he was forced to pay a
    large sum for the right to pipe water from the city water tower.
  </p>
  <p>
    Giulia in short claimed the same amenities as our neighbors, until Abu
    el-Kasim tore his beard and wrung his hands, and in moments of desperation
    ran out into the street to call everyone to witness how the abominable
    soothsayer was plunging him into ruin.
  </p>
  <p>The neighbors stroked their beards and gloated.</p>
  <p>
    Some said, “Abu el-Kasim has grown rich,” and others said, “What a joyful
    day it will be when next Selim ben-Hafs’s taxgatherer visits our street!”
  </p>
  <p>
    Only the most compassionate remarked, “Abu el-Kasim has clearly gone out of
    his mind.
  </p>
  <p>
    It would be a kindness to him and pleasing to Allah to carry him to the
    madhouse and have the evil spirit whipped from his body.”
  </p>
  <p>
    I was not at all surprised at these remarks, for now and then Andy in a
    howling fury would chase the agile Abu round the court, until he fled over
    the wall and hid in the cess pit.
  </p>
  <p>
    For Abu el-Kasim purposely teased and goaded Andy every time he was beaten
    on the wrestling ground behind the mosque.
  </p>
  <p>
    On such days Andy would be in a surly humor, and if on top of this Abu waved
    a wine flask in front of his nose, inviting him to take a pull at it and
    gain a little vigor, it was enough, and I was often afraid that Andy would
    knock Abu to pieces.
  </p>
  <p>
    But when enough interested onlookers had gathered in the yard and Andy’s
    fury had somewhat abated, Abu el-Kasim would creep out of the drain,
    smelling very evilly, and approach Andy with an ingratiating air, to feel
    his calf muscles and assure the neighbors that Andy would yet bring him in a
    fortune by wrestling.
  </p>
  <p>
    When I expostulated with Abu for teasing Andy he looked at me in wonder, and
    said, “Why deny my neighbors a little innocent fun?
  </p>
  <p>
    Besides, it’s good for your brother, for otherwise he’d only sit and sulk
    after a defeat, until he got cramps in the stomach.
  </p>
  <p>
    As it is, he can work off his fury on me and so regain his good fighting
    humor.”
  </p>
  <p>
    This was true, for after such outbreaks Andy quickly cooled, and laughed at
    Abu for a silly old fool.
  </p>
  <p>
    Abu el-Kasim then persuaded him to lie full length on a bench, and massaged
    his arms and legs, oiled his massive body and rubbed healing salves into his
    bruises.
  </p>
  <p>
    Now that Andy had adopted the Moslem faith he had to have a new name, and
    Abu el-Kasim called him Antar, after the great hero of the Arabian tales.
  </p>
  <p>
    In the bazaar he so loudly praised his strength and skill as to arouse
    curiosity and many people gathered behind the mosque to watch him wrestle.
  </p>
  <p>
    At least once a week Abu el-Kasim mounted on Andy’s shoulders and rode thus
    to the market place, issuing loud challenges to all and sundry to try a fall
    with the invincible Antar.
  </p>
  <p>
    There Andy stood up naked save for a pair of leather breeches reaching to
    the knee, while Abu rubbed him with oil and loudly eulogized his muscles.
  </p>
  <p>
    Among the loiterers on the shady side of the market place, and under the
    colonnade of the mosque, there were always some disengaged gureshes, each of
    whom had a patron or master who fed him and wagered money on him.
  </p>
  <p>
    Such wagers were not held to be gambling, which is forbidden by the Koran,
    since the result was determined not by chance but by the strength and skill
    of the wrestlers.
  </p>
  <p>
    These patrons were the idle sons of rich merchants and shipowners, whose
    forebears had built up their fortunes by piracy.
  </p>
  <p>
    But since Selim ben-Hafs, through fear of the great Sultan, had allied
    himself with the Spaniards, piracy had ceased, and so these young men were
    without an occupation.
  </p>
  <p>
    They passed their days at the baths and their nights in secret wine drinking
    in company with dancing girls.
  </p>
  <p>They sought to stimulate their jaded senses by patronizing this sport.</p>
  <p>
    Many of the wrestlers were rough fellows who had chosen this way of life
    from laziness.
  </p>
  <p>
    At times, when they found themselves outmatched, they were apt to sink their
    teeth in an opponent’s ear and tear it off.
  </p>
  <p>
    Therefore Andy had to be on his guard, and despite his lamentations and
    references to Samson’s disastrous fate, Abu shaved his head so that no
    adversary could grasp him by the hair.
  </p>
  <p>
    When first I went with Andy and Abu el-Kasim to the market place I was
    horrified at the sight of these fearful wrestlers, half naked and gleaming
    with sweat, as they made themselves supple by trying holds on one another,
    and forcing one another to the ground.
  </p>
  <p>
    They were big, fat men with bulging muscles, and I fancy any one of them
    could have cracked my ribs with a poke of his forefinger.
  </p>
  <p>
    But Abu el-Kasim made a great commotion, chattering like a monkey and
    screaming, “Is there anyone here who dares wrestle with the invincible
    Antar?
  </p>
  <p>
    His knees are as the pillars in the mosque and his trunk is a very tower.
  </p>
  <p>
    He was bred among idolaters in a land far to the north, and is hardened by
    the snow and ice that covers the country all the year round—ice, which you
    idlers know only as fragments in your sherbet.”
  </p>
  <p>
    After continuing thus for some time he climbed down from Andy’s shoulders,
    spread a piece of cloth upon the ground and threw a square silver coin upon
    it as a reward to the winner, crying aloud to Allah to witness his
    liberality.
  </p>
  <p>
    This provoked a roar of laughter which brought others running to the scene,
    while the wealthy patrons held their sides and cried, “You seem to have
    little faith in your Antar— and no wonder.
  </p>
  <p>He looks as lumbering as an ox.”</p>
  <p>
    But the curious began throwing coins onto the cloth until a little heap of
    silver lay there, and even a small gold coin or two.
  </p>
  <p>
    The wrestlers looked critically from the pile of money to Andy and back
    again, gathered in a ring with their hands on one another’s shoulders and
    chattered, until one of them undertook a “good” bout with Andy.
  </p>
  <p>
    In “good” wrestling, the opponents were not to inflict willful and lasting
    injury on one another, whereas in “hard” wrestling everything was allowed.
  </p>
  <p>
    In “hard” bouts men were apt to lose an eye or an ear, and professional
    wrestlers did not willingly engage in them.
  </p>
  <p>
    Andy and his adversary now tackled one another, and Andy, putting into
    practice the holds that Mussuf the Negro had taught him, flung his man over
    his shoulder to the ground with a resounding thud.
  </p>
  <p>
    To encourage the victim the bystanders flung more money onto the cloth, but
    Andy succeeded in throwing three men in succession —no mean feat for a
    beginner.
  </p>
  <p>
    But with the fourth he had the worst of it, for after a prolonged struggle
    his foot slipped and he fell, so that his opponent could get an arm under
    his shoulder and over the back of his neck and force him down.
  </p>
  <p>
    Abu el-Kasim uttered shrieks of anguish and wept as if he had lost a great
    sum of money instead of the one silver coin he had thrown onto the cloth.
  </p>
  <p>
    But Andy rubbed his aching neck and said, “I only hope Mussuf taught me
    right; I can’t stand up to these slippery fellows, though I’m certainly
    stronger than they are.”
  </p>
  <p>
    He sat with a colored cloth over his shoulders, carefully observing the
    matches that followed.
  </p>
  <p>
    I believe he learned a great deal from them, for encouraged by the
    considerable sum now amassed on the cloth the wrestlers fought their best.
  </p>
  <p>
    The final victor was one Iskender, who looked no more formidable than the
    rest, though his shoulders were as broad as a bread oven, and a lighter man
    could not move him from the spot.
  </p>
  <p>
    Andy surveyed him wide eyed, and said, “That Iskender’s no fool, and he’ll
    be an opponent after my own heart when I get so far.
  </p>
  <p>But I’ve seen enough today to know that I’ve much to learn.”</p>
  <p>
    He did not let his first defeat discourage him, and indeed it proved an
    advantage, for the other gureshes were all the readier to accept him as one
    of themselves.
  </p>
  <p>
    Iskender gave him four silver pieces from the heap he had won and declared
    that Andy had fully earned them; for custom required the victor to share his
    winnings with the other competitors.
  </p>
  <p>
    The stake money was however the least of the sums to change hands on such
    occasions, for large amounts were wagered among the onlookers, whether on
    individual bouts or on the final result; this last was by no means a
    foregone conclusion.
  </p>
  <p>
    Even the most eminent wrestler, having come triumphantly through ten or
    fifteen successive encounters with powerful adversaries, could never be
    confident of wresting the final victory from perhaps a weaker man who came
    fresh to the attack.
  </p>
  <p>
    Wrestlers and their patrons followed a set system for determining the order
    of competitors on different days, which evened out the chances and made the
    final result very uncertain.
  </p>
  <p>
    If therefore a novice wagered solely on the appearance of the men without
    knowing anything of the order in which they were to fight, he might make
    very bad mistakes.
  </p>
  <p>
    Spectators and patrons began to pay greater attention to Andy, and soon it
    was his turn to gather up the pile of money.
  </p>
  <p>On that day Abu el-Kasim’s joy knew no bounds.</p>
  <p>
    He jumped up and down, then flew open armed at Andy and planted a smacking
    kiss on his mouth, so that Andy yelled, spat, and hurled him back among the
    onlookers who, in an ecstasy of mirth, caught him on their outstretched
    arms.
  </p>
  <p>
    Abu el-Kasim at once bestowed the prescribed proportion of his winnings in
    alms, displaying deep emotion at his own munificence.
  </p>
  <p>
    But the rest of the money he quickly knotted into a bundle and clasped to
    his breast, wondering aloud where he could lay hands on an iron chest in
    which to secure it.
  </p>
  <p>
    The sum was negligible in comparison with his real fortune, but he liked to
    feign poverty and entertain the public with his dread of the taxgatherer.
  </p>
  <p>
    And indeed, it was not long before a fat, breathless man arrived at our
    dwelling.
  </p>
  <p>
    He leaned on his staff of office and looked about him greedily from under a
    big, bulging turban, and at the sight of him Abu el-Kasim cringed, wrung his
    hands and said, “O taxgatherer Ali ben-Ismail, why do you persecute me?
  </p>
  <p>Not three moons have passed since your last visit, and I’m a poor man.”</p>
  <p>
    He hastened forward to support Ali ben-Ismail; I took the visitor’s other
    arm and between us we helped him to a seat on the broadest cushion in the
    house.
  </p>
  <p>
    When he had settled himself and got his breath back, he smiled sadly and
    said, “Abu el-Kasim!
  </p>
  <p>
    The ruler of Algiers and of the sea, king of countless Berber tribes,
    Allah’s representative and commander in this city—in short, Sultan Selim
    ben-Hafs—has been pleased to turn his eyes upon you.
  </p>
  <p>
    You’ve become rich; you’ve brought water to your courtyard and refurnished
    your rooms.
  </p>
  <p>
    Costly rugs have been seen here and even silver cups, which are forbidden by
    the Koran.
  </p>
  <p>
    You have bought three new y g slaves: one brings you in enormous sums as a
    wrestler, another is an indescribably lovely woman with eyes of different
    colors, who sees strange things in sand, so that even the women of the harem
    have taken to visiting the public bathhouse to hear her foretell the future.
  </p>
  <p>
    The third earns substantial sums for you as a quacksalver— he’s no doubt
    this goatlike man beside you, who now stands and goggles at me.
  </p>
  <p>
    I’m also told that people come to you from remote villages to bring
    something which you call ‘cheap ambergris.
  </p>
  <p>’ By such false terminology do you swindle your customers.”</p>
  <p>
    Abu el-Kasim warmly denied the charge, but the taxgatherer smote him on the
    head with his staff and said irascibly, “That’s what I’m told, and I should
    pay little attention if the matter had reached no other ears than mine.
  </p>
  <p>
    I’m a good-natured man and because of my bulk I dislike trudging the
    streets.
  </p>
  <p>
    But Sultan Selim ben-Hafs has also heard of it, which has put me in a
    questionable light.
  </p>
  <p>
    I’m vexed with you, Abu el-Kasim; hitherto I’ve been content with ten gold
    pieces a year and you’ve vilely cheated me, setting at nought my friendship
    and protection.
  </p>
  <p>
    Now we’re both in trouble, for the Sultan has laid an extra tax upon you of
    a thousand gold pieces.”
  </p>
  <p>“A thousand!”</p>
  <p>yelped Abu el-Kasim.</p>
  <p>
    Throwing off turban and cloak he began to jump about half-naked, knocking
    over jars and baskets in his frenzy.
  </p>
  <p>“A thousand!</p>
  <p>
    The whole street isn’t worth that sum, and Allah has plainly deprived Selim
    ben-Hafs of the remainder of his wits.
  </p>
  <p>
    By the time I’d collected even the tenth part of it I should be toothless
    with age.”
  </p>
  <p>“Did you say the tenth part?”</p>
  <p>exclaimed the taxgatherer in amazement.</p>
  <p>“A hundred gold pieces?</p>
  <p>
    Then Allah is indeed great and I’ve found for my lord a goose that lays
    golden eggs all unsuspected.
  </p>
  <p>
    You astonish me; for I was only joking, being inquisitive about your growing
    fortunes.”
  </p>
  <p>
    Abu el-Kasim had abrupdy ceased his capering, and now said with a malicious
    glint in his eye,
  </p>
  <p>“So. You were making game of me.</p>
  <p>
    Then I’ll give your wife such an ointment of paradise that after embracing
    her you’ll die in agony, foaming at the mouth.”
  </p>
  <p>
    Taxgatherer Ali ben-Ismail sweated slightly, and his eyes were cold as he
    said in a harsh tone, “Don’t take the jest to heart, my dear Abu.
  </p>
  <p>It’s all part of my duty.</p>
  <p>
    I’ve been ordered to make closer scrutiny of your household because Selim
    ben-Hafs, blessed be his name, needs money to buy another couple of boys.
  </p>
  <p>So let us come to some friendly arrangement, as usual.</p>
  <p>
    You’d gain nothing if I were dismissed and replaced by a leaner, hungrier
    man whom you would have to make fat.”
  </p>
  <p>
    Abu felt grave misgivings on hearing that his fortune was the talk of the
    town.
  </p>
  <p>But all he said was, “Cursed be Selim ben-Hafs.</p>
  <p>
    He has already thirty young boys in his harem and at least as many women.
  </p>
  <p>Am I, poor wretch, to pay for his lascivious amusements?</p>
  <p>Hear now a remarkable dream that I’ve had.</p>
  <p>
    A Deliverer came from the sea, and at his coming the taxgatherers were led
    in bonds through the city and flogged at every street corner.”
  </p>
  <p>
    The fat official sweated more than ever, and raised a finger to silence Abu.
  </p>
  <p>
    “Such dreams are dangerous,” he said, “and I cannot think how it is that so
    many others have been plagued with them.
  </p>
  <p>
    In the name of the Compassionate, dear Abu, refrain from trumpeting your
    dreams abroad!
  </p>
  <p>Remember that even we, the taxgatherers, are poor men.”</p>
  <p>
    After prolonged haggling Ali ben-Ismail agreed to take fifty pieces of gold,
    and said, “I know that you’ll feel the loss of this great sum, and I advise
    you to make up its value in silver coins and cups, and in your slave woman’s
    bracelets.
  </p>
  <p>
    Carry all this to the treasury to be weighed, so that everyone may see how
    I’ve denuded you.”
  </p>
  <p>No suggestion could have been more welcome to Abu.</p>
  <p>
    He gathered up vessels and coins to the value of fifty pieces of gold and
    having helped Ali to his feet they set off.
  </p>
  <p>
    The taxgatherer walked first, leaning on his staff and panting, while sweat
    poured down his fat cheeks.
  </p>
  <p>
    After him scuttled Abu el-Kasim wearing only a dirty turban and a loincloth,
    with the bundle on his back.
  </p>
  <p>
    As he went he shrieked, lamented, and uttered heart-rending appeals to
    Allah, so that even the neighbors were moved.
  </p>
  <p>
    For once his tears were genuine, for fifty gold pieces was a great deal of
    money, even for him.
  </p>
  <p>
    However, before the hour of evening prayer, Abu el-Kasim returned well
    satisfied from the treasury.
  </p>
  <p>
    He washed, put on clean clothes, performed his devotions, and said, “The
    money fell in good soil, for even the clerks pitied me when they saw me
    compelled to surrender my slave woman’s bangles, and this evening the whole
    city’s in an uproar over Selim’s rapacity.
  </p>
  <p>
    Lamps will burn late tonight in all wealthy houses while the owners bury
    their treasure beneath the flagstones.”
  </p>
  <p>
    Nevertheless the treasury had wrought a marvel in extorting fifty gold
    pieces from Abu el-Kasim, and not many days later my whitebearded teacher
    said to me, “I have praised your aptitude for learning, and the Faqih
    himself wishes to behold your face.”
  </p>
  <p>
    This was the greatest honor that could have been done me, for the Faqih was
    the most learned man in the school, and deeply versed in the branches of
    fiqh, or jurisprudence.
  </p>
  <p>
    As mufti he was competent, in all matters relating to the law in which there
    was uncertainty or ambiguity, to issue a decree called a fatwa.
  </p>
  <p>
    He stood in high favor with the ruler, for he had profited by his knowledge
    of the Koran, Sunna, and fiqh to make pronouncements favorable to the Sultan
    in troublesome affairs.
  </p>
  <p>
    Compared with him my teacher was but a poor man whose only merit was to know
    the Koran by heart and be competent to instruct the newly converted.
  </p>
  <p>
    I was alarmed at the thought of meeting this great man, for only gradually
    had I come to appreciate the wealth of the Arabic language and to learn how
    many ways there were of reading the Koran, how many words could be made to
    express one idea and how many interpretations could be put upon one word.
  </p>
  <p>
    My teacher counted fifty words for “camel” and as many as a hundred for
    “sword,” to denote all the varieties of that weapon.
  </p>
  <p>
    The Faqih was sitting with his writing materials before him, in a room
    containing many books and reading desks.
  </p>
  <p>
    He had a jar of dates by him and from time to time he took one out, sucked
    it, and spat the stone onto the floor before me, then licked his fingers and
    took a sip of water from a goblet.
  </p>
  <p>
    Seeing that he was enjoying a period of rest and refreshment, I took courage
    and greeted him reverently.
  </p>
  <p>
    “I have heard,” said he gently, “that you’re a skilled physician from the
    Frankish countries, and are zealously striving to become a good Moslem.
  </p>
  <p>Tell me, therefore, of your Lord, your Prophet, and your rule.”</p>
  <p>
    These things were well known to me and I answered, “Allah the one God is my
    Lord and Mohammed is his Prophet, blessed be his name.
  </p>
  <p>The Koran is my rule, virtue the way of my spirit, Sunna my path.”</p>
  <p>
    He nodded in approval, and stroking the beard which reached to his belt he
    asked, “What is the key to prayer?”
  </p>
  <p>
    This question too was easy, and I answered readily, “The key to prayer is
    devout purification; the key to purification is the profession of the name
    of God; the key to profession is steadfast faith; the key to faith is trust;
    the key to trust is hope; the key to hope is obedience; and the key to
    obedience is: Allah most high is the one God and him do I profess.”
  </p>
  <p>
    Again he nodded, and asked, “How do you perform the purification when
    preparing yourself for prayer?”
  </p>
  <p>
    “I have been taught that there are six requirements for the partial
    ablution: announcement of intention, the washing of the face, hands, and
    arms to the elbows; the drying of the head and the washing of the feet to
    the ankle bones, all in the proper order.
  </p>
  <p>
    But the following ten actions are meritorious—to wash the hands before
    dipping them in the bowl; to rinse the mouth; to rinse the nose by sniffing
    up water; to wash the whole head and cleanse the ears inside and out; to
    comb the beard with the fingers; to spread out fingers and toes when washing
    them; to wash the right hand and right foot before the left hand and left
    foot and to repeat all this thrice in succession.”
  </p>
  <p>The Faqih sucked dates, his eyes half closed.</p>
  <p>“What do you repeat after the ablution?”</p>
  <p>
    “After the ablution I say, ‘I bear witness that there is no God but Allah.
  </p>
  <p>He is one and indivisible and Mohammed is his servant and Prophet.</p>
  <p>
    O Lord, grant me to be among the penitent, grant me to be of the company of
    the pure.
  </p>
  <p>Praise be to Allah!</p>
  <p>To his glory I declare that there is no other God than he.</p>
  <p>Before his face I plead for mercy and repent of my evil deeds.</p>
  <p>
    ’ According to the sacred tradition, the Prophet with his own mouth has
    proclaimed, ‘For him who pronounces these words after every ablution the
    eight gates of Paradise stand open, and he may enter through which one he
    pleases.
  </p>
  <p>
    ’“ So I ended, feeling well satisfied with myself for having been able to
    recite these important prayers.
  </p>
  <p>
    But the Faqih suddenly opened his eyes, spat out a date stone and said
    angrily, “You speak the holy words like a parrot, and prate of Paradise when
    you’re not even circumcised.”
  </p>
  <p>I choked.</p>
  <p>
    Many renegades submitted to circumcision after only an hour or two of
    instruction, to be done with it, but I had hesitated to undergo this
    unpleasing ordeal, and hoped I might succeed in evading it altogether.
  </p>
  <p>
    Having thus thoroughly frightened me, the Faqih went on in triumph, “If
    you’d been speaking from the heart instead of from the hps you would long
    since have become united to Islam through the outward token of circumcision.
  </p>
  <p>
    Islam does not ask after a man’s nation, nor after the color of his skin,
    for all races and colors are united by that sign.
  </p>
  <p>
    But you’re only a slave, and perhaps your master is to blame for the
    omission.
  </p>
  <p>
    I hear that he’s a wealthy dealer in drugs, named Abu el-Kasim, and that he
    owns a Christian woman.
  </p>
  <p>
    Her eyes are said to be of different colors, and therefore she is able to
    see coming events in a bowl of sand.
  </p>
  <p>
    And I am told that the women of the harem run to the public bathhouse to
    meet her, and reward her richly for foretelling the future for them.
  </p>
  <p>Is this true?”</p>
  <p>
    “Venerable and learned Faqih,” I exclaimed, “may Allah of his grace preserve
    me from spying in the bathhouse during women’s bathing hours!”
  </p>
  <p>“Don’t prevaricate!</p>
  <p>I’ve heard that it is so.</p>
  <p>
    But whether the gift is from Allah or the devil, or whether she’s a
    charlatan, your master must have a fatwa for her, or else lock her up.”
  </p>
  <p>
    I was shocked at his greed, and losing all veneration for him I looked up
    and said, “My master may have forgotten, but he’ll no doubt offer you a gift
    proportionate to his means, on receiving from you the necessary fatwa.
  </p>
  <p>
    But the taxgatherer has squeezed him dry, and not even by force could you
    extort from him more than a couple of gold pieces.”
  </p>
  <p>
    Tears of indignation rose to my eyes as I reflected what vile cheats there
    were in the world.
  </p>
  <p>
    But the Faqih raised his head and said, “Before issuing this fatu/a I must
    see the slave woman, but she must not give scandal by coming here.
  </p>
  <p>
    After the evening prayer on Friday I will visit Abu el-Kasim’s house and he
    is not to receive me with the honor befitting my rank.
  </p>
  <p>I shall come secretly, with my face hidden.</p>
  <p>Take this message to Abu el-Kasim.</p>
  <p>
    Perhaps I will be content with fifty pieces of gold, for Allah is merciful
    and gracious.”
  </p>
  <p>
    I wondered what was really in his mind, for I had grown accustomed to the
    habit common to the people of Islam of never speaking out.
  </p>
  <p>
    But Abu el-Kasim was delighted at the message, and said, “Things are going
    better than I could have hoped, and Sinan the Jew was indeed wise to provide
    me with such good bait for my hooks.
  </p>
  <p>
    For this day’s work I’ll give you a new turban and a white robe, and so
    acquire merit.”
  </p>
  <p>
    I asked in surprise, “How can you rejoice that this greedy Faqih comes to
    rob you?”
  </p>
  <p>Abu el-Kasim answered, “Of course he wants money.</p>
  <p>He’s no more than human.</p>
  <p>But his curiosity is as strong as his greed.</p>
  <p>
    He must surely have heard the nature of the visions that our Delilah sees in
    the sand, and now wishes to observe for himself which way the wind blows, so
    as to gather all safely in before the storm.”
  </p>
  <p>
    When Friday came, Abu el-Kasim bade Giulia roast some ptarmigan, and not
    stint pepper, cloves, or nutmeg.
  </p>
  <p>
    I bought sugar cakes from the pastry cook and filled a bowl with fruit and
    sweetmeats, sprinkling over these a white powder that gave them a fiery
    taste and induced thirst.
  </p>
  <p>Abu chilled the drinking water and flavored it with stimulating spices.</p>
  <p>
    Then—most important preparation of all—he told Giulia what to say, warning
    her against giving herself into the power of evil spirits and seeing
    profitless visions.
  </p>
  <p>
    After the evening prayer the Faqih arrived, his face hidden by a corner of
    his cloak, and knocked on the door with his staff.
  </p>
  <p>
    On entering he sniffed the good smells with pleasure, and drawing forth his
    beard which he had tucked under his belt he stroked it and said
    reproachfully, “Prayer is better than savory food, and I would be loath to
    cause you any trouble, Abu el-Kasim.
  </p>
  <p>A fig or two and a bowl of water are enough for me.”</p>
  <p>
    Nevertheless, after much voluble protest he allowed us to set the dishes
    before him, and ate slowly and purposefully until they were empty.
  </p>
  <p>Abu el-Kasim served him and poured water over his hands.</p>
  <p>
    Then he proffered a beautifully embroidered silken bag, saying, “This purse
    contains twenty pieces of gold which I hope you’ll accept as a present.
  </p>
  <p>
    Believe me, they’re all I possess, but I won’t forget to make you further
    free-will offerings when I have more.
  </p>
  <p>
    Now I have a slave woman about whom I would ask your advice, to ensure
    against any infringement of the law.
  </p>
  <p>
    Her eyes are of different colors and she can see strange things in sand.”
  </p>
  <p>
    The Faqih nodded, weighed the bag in his hand, and tucked it thoughtfully
    under his girdle.
  </p>
  <p>
    Abu el-Kasim led in Giulia by the hand, drew aside her veil and held up a
    lamp that the Faqih might see better.
  </p>
  <p>“Allah is great!”</p>
  <p>said the Faqih in wonder.</p>
  <p>“Never have I seen anything of the kind.</p>
  <p>
    But with God all things are possible—and indeed it would amount to a Persian
    heresy to say that evil spirits were more powerful than Allah, and could
    bring about such a miracle against the divine will.”
  </p>
  <p>
    Careless of cost, Abu el-Kasim threw a pinch of genuine ambergris onto the
    brazier; then he poured fine sand into a large copper dish and ordered
    Giulia to stir it with her finger.
  </p>
  <p>
    And as she gazed down into the dish she fell into a trance and began to
    speak in an altered voice.
  </p>
  <p>But I was now used to this, and neither believed nor feared.</p>
  <p>“I see turbulent waters—out of the sea rises the banner of the Prophet.</p>
  <p>
    Indeed, the banner of the Prophet rises from the waves, and the Deliverer
    comes from the sea.”
  </p>
  <p>“Do you speak to me, heathen woman?”</p>
  <p>demanded the Faqih in astonishment.</p>
  <p>
    “I don’t understand you, for the banner of the Prophet is kept in the
    Seraglio of the Great Sultan.”
  </p>
  <p>
    Ignoring this, Giulia went on quickly and earnestly, “Out of the sea come
    ten asses with silver bits and silver bells.
  </p>
  <p>
    Ten camels follow—the camels have golden saddles and are laden with gifts
    for you, O Faqih.
  </p>
  <p>I see the waters full of ships.</p>
  <p>
    They are laden with plunder and sail into the harbor, and from their plunder
    generous alms are brought to you in the mosque; the hunters of the sea offer
    liberally of their booty and build splendid mosques and fountains.
  </p>
  <p>
    The king of the sea founds schools and hospitals and endows them richly, and
    the teacher shall not suffer want under his rule.
  </p>
  <p>But Faqih, Faqih!</p>
  <p>Before all this comes to pass, there is blood.”</p>
  <p>
    The Faqih had been eagerly listening, but now he fingered his beard uneasily
    and said, “Blood?
  </p>
  <p>Foolish woman, do you indeed see blood?</p>
  <p>If so I suspect that an evil spirit speaks through your lips.”</p>
  <p>“I see blood,” she went on.</p>
  <p>
    “A little pool of black, evil blood that doesn’t even soil the hem of your
    cloak—it sticks to your shoes.
  </p>
  <p>
    And so you change your shoes—you throw the old ones away and put on new
    ones—new shoes of red, scented leather.
  </p>
  <p>
    They’re adorned with precious stones—after that day there is no richer Faqih
    than yourself.
  </p>
  <p>
    Your name flies over the seas and the banner of the y Prophet shields you
    from the wrath of the unbelievers.
  </p>
  <p>
    All this I see in the sand, old man, but no more—unless it be a cedarwood
    coffin with a turban upon it, to which pilgrims, remembering the great
    Faqih, come and pray from far distant lands and so acquire merit.”
  </p>
  <p>
    Giulia covered her eyes with both hands and moaned as if she had had a bad
    dream.
  </p>
  <p>
    But the Faqih was in no way dismayed by the talk of a coffin; on the
    contrary the prophecy flattered him, and he said, “These predictions are
    remarkable, but I hardly think we need put much faith in them.
  </p>
  <p>
    They may have more to do with you, Abu elKasim, than with me, and I hardly
    know what to think; for a poor drug merchant would hardly pay twenty gold
    pieces just for advice.
  </p>
  <p>
    Let’s have no more beating about the bush; dismiss your slaves that we may
    talk alone together with none but Allah to hear us.”
  </p>
  <p>
    Abu el-Kasim sent us away at once and locked the door, setting Andy to guard
    the outer gate.
  </p>
  <p>
    The learned Faqih remained until late that night, and when at last he
    departed as secretly as he had come, Abu el-Kasim sent Giulia to bed and
    summoned me.
  </p>
  <p>“The plans are taking shape,” he said.</p>
  <p>
    “Have no fear, Michael elHakim; whatever happens the Faqih won’t betray us.
  </p>
  <p>
    True, he won’t risk burning his fingers by making out a fatwa for Delilah,
    but neither will he interfere, and she may continue plying her trade in the
    bathhouse.”
  </p>
  <p>
    Thus Abu el-Kasim drew cautiously at first one thread and then another,
    knotting them together in a net in whose meshes Selim benHafs would one day
    be entangled.
  </p>
  <p>
    But the Faqih’s threats concerning circumcision had filled me with dread and
    I asked Abu el-Kasim whether Andy and I must indeed submit to so unpleasant
    an operation.
  </p>
  <p>
    He looked at us scornfully, and having rehearsed the many advantages to be
    gained by it he ended, “Why oppose it, when by such a trifle you may win the
    respect of all true believers?
  </p>
  <p>
    On that joyful day you may ride round the city on a white donkey and all
    devout believers will bring you presents and rejoice at your conversion.”
  </p>
  <p>
    I replied crossly that I had not the least wish to ride round the city on a
    white donkey, to be made a public laughingstock, and reminded him that
    Andy’s progress as a wrestler would be seriously hindered if at this stage
    he were compelled to lie up with a slow-healing wound in his tenderest part.
  </p>
  <p>
    And I would never consider undergoing it without him, for we were brothers;
    we hoped to enter Paradise side by side and together enjoy the shade of its
    fruit trees.
  </p>
  <p>Abu el-Kasim was not deceived by my pious words, and said, “Well!</p>
  <p>
    There’s a time for everything, and I shall eagerly await the day when, in
    accordance with the will of Allah, your brother justifies the high hopes I
    have of him.”
  </p>
  <p>Nor did he have to wait long.</p>
  <p>
    Some days later he climbed on Andy’s shoulders and rode to the market place,
    and hardly had the wrestlers gathered in a ring with their arms about each
    other’s shoulders to determine the order of the bouts when a big black
    fellow appeared, attended by a party of soldiers.
  </p>
  <p>
    He threw out his chest and thumped it with his fists as he challenged,
    “Iskender, Iskender!
  </p>
  <p>Come here and have your ears torn off!</p>
  <p>After that I’ll attend to Antar, of whom I’ve heard so much.”</p>
  <p>
    The wrestlers muttered uneasily among themselves, and warned Andy, saying,
    “That is Selim ben-Hafs’s master wrestler.
  </p>
  <p>
    Don’t anger him; let him win and take the money, for then perhaps he’ll
    leave us in peace and not hurt us.
  </p>
  <p>
    But if you win you’ll be summoned to wrestle before the Sultan, and although
    at first you may get the better of all his wrestlers, the day will come when
    you find yourself lying in the sand with a broken neck.”
  </p>
  <p>
    Andy answered warmly, “Your faith seems weak; you forget that Allah has
    preordained all things.
  </p>
  <p>Go, Iskender, and let him beat you!</p>
  <p>
    Then I will tackle him and you shall behold such a match as you’ve never
    seen before.
  </p>
  <p>
    If it be the will of Allah, this shall be my last bout in the market place;
    after that I shall appear only before the Sultan and his court.”
  </p>
  <p>
    At this, great excitement arose among the wrestlers’ patrons, and silver and
    gold pieces showered onto the cloth.
  </p>
  <p>
    The soldiers formed a circle and thrust back the onlookers, while the
    Sultan’s master wrestler, ugly and gleaming with oil, jumped up and down in
    the middle and roared his challenge.
  </p>
  <p>
    Iskender, adjuring him in the name of Allah to observe the rules of “good”
    wrestling, ran in at him, but it was not long before he was tossed into the
    air, and fell with a crash.
  </p>
  <p>
    He lay moaning for some time, feeling his arms and legs, but I think he was
    little harmed by the fall, and behaved thus to flatter his ferocious
    opponent.
  </p>
  <p>
    Two other men stepped forward and the master wrestler threw them without
    difficulty.
  </p>
  <p>
    But when he noticed that he was beginning to sweat and pant, he became
    suspicious and cried, “Where’s that Antar skulking?
  </p>
  <p>He’s the man I came for, and I shan’t stay all day for him.</p>
  <p>My bath awaits me.”</p>
  <p>Ignoring the warnings of the rest Andy at once stepped forward.</p>
  <p>
    It was clear that the black man held him in great respect, for he circled
    watchfully about him for some time before suddenly charging in like a bull
    with his head lowered, meaning to butt Andy in the stomach and wind him.
  </p>
  <p>
    But Andy stepped nimbly aside and getting a mighty grip of the other’s waist
    flung him high in the air.
  </p>
  <p>
    Like the adept he was, however, he landed on his feet, but at once Andy
    struck them from under him and he came headlong to the ground with Andy
    uppermost.
  </p>
  <p>
    Andy took firm hold of the back of his neck and pressing his face to the
    ground, he cried, “Which of us is biting the dust?”
  </p>
  <p>
    The other wrestlers uttered warning yells, for in his extremity the brutal
    black took to “hard” wrestling, and getting his arms round one of Andy’s
    legs he set his teeth in the calf muscles.
  </p>
  <p>
    If Andy had been able to maintain his grip he would certainly have broken
    the fellow’s neck, but now pain forced him to loosen it.
  </p>
  <p>
    Soon they were rolling over and over each other on the ground, and I have
    never in my life seen such a struggle.
  </p>
  <p>
    Now Andy’s head thrust up, now he was down again while the blackamoor jumped
    on his chest, and would have broken all his ribs but for Andy’s massive
    build.
  </p>
  <p>
    Bleeding and with torn ears they at last broke loose from each other, and
    Selim benHafs’s champion had evidently had enough.
  </p>
  <p>
    He was breathless, and letting his arms fall to his sides he spat some blood
    from his mouth and tried to laugh as he said sourly, “You live up to your
    reputation, Antar, and know something of ‘hard’ wresding too; but I’ve no
    right to expose myself to danger in the absence of my master the Sultan.
  </p>
  <p>
    Nor did I fail to notice your underhand stratagem in tempting me to waste my
    strength before I started on you.
  </p>
  <p>So let us continue our match tomorrow, in the presence of the Sultan.</p>
  <p>I don’t doubt that he will richly reward whichever one of us survives.”</p>
  <p>
    Casting an embarrassed glance around him he wiped the blood from his ears to
    gain time for recovery.
  </p>
  <p>
    But the crowd uttered wild cheers and their hatred of Selim ben-Hafs found
    vent in savage abuse of his wrestler.
  </p>
  <p>Andy, breathing hard, yelled, “You bit me in the calf, you swine.</p>
  <p>
    Tomorrow my leg will be swollen and I shan’t be at all surprised if your
    poisonous teeth cause me to run about barking and foaming at the mouth and
    avoiding water.
  </p>
  <p>
    And it was for the sake of water that I became a follower of the Prophet.
  </p>
  <p>
    But you shall learn tomorrow that I too have teeth—and teeth that can crack
    marrow bones!”
  </p>
  <p>
    When the black wresder had gone, followed by his guards, Abu el-Kasim burst
    out into wild lamentations as usual, and smote Andy over the head with his
    staff.
  </p>
