  </p>
  <p>
    But Torgut was incensed, and cried, “Cursed be you, Sinan the Jew, for
    seeking to swindle me!
  </p>
  <p>
    The girl is almost a virgin, the grayeyed Frank is a powerful fellow, and
    the third has the same name as the angel who rules the night and the day.
  </p>
  <p>
    Furthermore he is a skilled physician and a learned man, speaking all the
    Frankish languages, and Latin, too.
  </p>
  <p>
    Ten times that sum would leave me the poorer, and I should never even
    consider so bad a bargain were you not my father and my friend.”
  </p>
  <p>
    Sinan the Jew became annoyed in his turn, and said, “The sun has dried your
    brains.
  </p>
  <p>
    A moment ago you were ready to kill the girl, or at least to put out one of
    her eyes; now you exaggerate her nonexistent charms in order to rob me.
  </p>
  <p>
    If you reject a fair offer, sell these slaves in the open market, and I’m
    ready to make the highest bid, so long as you swear by the Koran not to
    bribe anyone to force up the price.”
  </p>
  <p>Torgut scowled.</p>
  <p>“As if anyone in the bazaar would dare to outbid you!</p>
  <p>
    And you would certainly spread slander about these wretched slaves and so
    lower their price.
  </p>
  <p>
    The Koran has revealed their true value to you, and I submit to its ruling,
    though I lose by it.
  </p>
  <p>Was it not the seventy-first verse of the sixth sura?</p>
  <p>
    Together that amounts to seventy-seven gold ducats—an auspicious number
    which in itself emphasizes Allah’s intention.
  </p>
  <p>Or would you prefer us to add the numerical values of the letters?”</p>
  <p>
    By this time Sinan was tearing his beard, and now he cried out, “No,
    no—perish the thought!
  </p>
  <p>
    It would be waste of time, as not even scholars are agreed on those values.
  </p>
  <p>In any case there was no mention of gold in the sura.”</p>
  <p>“It is unbecoming in you to struggle against the will of Allah.</p>
  <p>
    Were I a more learned man I could point out plenty of characters signifying
    gold; but it is enough for me that the Koran is more precious than gold and
    that each letter contains ten benedictions.
  </p>
  <p>Therefore let us dispute no longer.</p>
  <p>I will be content with seventy-seven gold ducats.”</p>
  <p>
    The end of it was that Sinan the Jew counted out the ducats, sent Giulia to
    the harem, and ordered Andy and me out of his sight.
  </p>
  <p>
    We returned to the outer court, where huge dishes of mutton and rice cooked
    in fat had been brought out for Torgut’s men.
  </p>
  <p>
    These had squatted down in order of rank round the dishes, and were picking
    morsels of meat from them, and pressing the rice into neat balls which they
    put in their mouths.
  </p>
  <p>
    But the slaves and prisoners had gathered behind them, and with famished
    looks followed every mouthful as it disappeared.
  </p>
  <p>
    The sight depressed me very much, but as we drew near Mussuf at once made
    room for us beside him, and offered Andy a fine piece of meat, dripping with
    fat.
  </p>
  <p>I urged Andy to accept this as a peace offering.</p>
  <p>
    The food in the dish now began to dwindle rapidly, and I had difficulty in
    keeping up.
  </p>
  <p>
    The others, seeing Andy’s prowess, looked askance at us and called on Allah,
    and when the dish was empty one of the renegades remarked, “He’s no true
    Moslem.
  </p>
  <p>
    See what manners—to sit on his bottom and stuff his mouth with both hands!”
  </p>
  <p>
    Andy was offended by these words, but I said to the speaker, “We have but
    now found the right road, and stumble along it like blind men, having no one
    to lead us.
  </p>
  <p>Explain to us the points of good behavior.”</p>
  <p>
    Sinan the Jew must after all have been well disposed toward us; I cannot
    otherwise account for the fact that the wrinkled eunuch appeared in the
    courtyard in response to Andy’s roars for more to eat, and ordered the
    servants to refill our dishes.
  </p>
  <p>
    I bade Andy hold his tongue while the Moslems taught us proper table manners
    and good behavior.
  </p>
  <p>Delightedly and all talking at once they began their instruction.</p>
  <p>
    We must always wash our hands before eating, they said, and bless the food
    in the name of Allah.
  </p>
  <p>
    We must sit cross legged before the dish, on the left haunch, using only
    three fingers of the right hand to take food.
  </p>
  <p>
    No knife was to be used, as all was cut beforehand into pieces of suitable
    size, and no more was to be put in the mouth than it would comfortably hold.
  </p>
  <p>
    The rice was to be kneaded together in small lumps, and not shoveled into
    the mouth like porridge.
  </p>
  <p>
    A well- bred man did not stare at his companions but looked straight before
    him and was content with what he had.
  </p>
  <p>
    Finally they recited a phrase or two from the Koran and said, “Ye who
    believe, eat the good things which God hath given you, and offer your thanks
    to Him.”
  </p>
  <p>
    When the food was nearly all gone, they pointed out that no believer quite
    finished what was on the dish, but charitably left some to be distributed
    among the poor.
  </p>
  <p>
    Now also they left many good pieces of meat and some rice, and handed it to
    the slaves and prisoners, who fought savagely over it, for although they
    were Christians they displayed little of the Christian spirit.
  </p>
  <p>The Moslems’ explanation gave me much to think about.</p>
  <p>
    They went on to tell me of the fast of Ramadan and of the pilgrimage to
    Mecca that every believer ought to make at least once in his life.
  </p>
  <p>
    But if prevented by poverty or some other cause his omission was not
    accounted to him for sin.
  </p>
  <p>
    I asked them their views on wine drinking, at which they all sighed deeply
    and answered, “It is written: ‘Ye who believe: wine, games of chance,
    worshiping of stones, and dicing are abominations of the devil.
  </p>
  <p>Shun them, that ye may be blessed.</p>
  <p>
    ’“ But others among them said, “It is also written: ‘Wine drinking is a
    great sin, albeit man may have some good of it.
  </p>
  <p>But the sin is greater g y g g than the good.</p>
  <p>
    ’“ The eunuch, who had stood behind us listening, could contain himself no
    longer.
  </p>
  <p>
    “There’s much to be said for wine, and many poets—especially the
    Persians—have celebrated its best qualities.
  </p>
  <p>
    Persian is the language of poets, as Arabic is the Prophet’s, whereas
    Turkish is spoken only by the dogs of the big cities.
  </p>
  <p>
    And in praising wine the poets have used it as a symbol of the true faith.
  </p>
  <p>Yet even apart from its symbolic aspect, wine is beneficial to health.</p>
  <p>
    It stimulates the kidneys, strengthens the bowels, eases care, and renders a
    man magnanimous and noble.
  </p>
  <p>
    Truly, had not Allah in his inscrutable wisdom forbidden the faithful to
    drink it, it would have no equal on earth.”
  </p>
  <p>
    Hearing this song of praise Andy regarded the eunuch with displeasure, and
    said, “Gelding!
  </p>
  <p>Are you trying to annoy me?</p>
  <p>
    I have taken the turban with the sole purpose of avoiding the curse of wine.
  </p>
  <p>
    Wine runs away with good sense and good money, infects a man with diseases,
    and causes him to see creatures that are not there.
