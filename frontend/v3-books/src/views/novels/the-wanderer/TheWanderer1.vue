  <p>
    Treat me therefore as a newborn child in matters of religion, and I will do
    my best to grasp and receive the new faith.”
  </p>
  <p>
    He said, “You speak wisely and sincerely, and may the almighty God account
    it to you for merit.
  </p>
  <p>
    But you should clearly understand that the Law of the Prophet permits no one
    to be converted by force or cunning.
  </p>
  <p>
    Do you therefore freely renounce all idolatry and confess that Allah is the
    one God and that Mohammed is his Prophet?”
  </p>
  <p>
    I was astounded at his words, and exclaimed, “I don’t understand you, for
    being a Christian I am no idolater.”
  </p>
  <p>
    This greatly incensed him, and he said, “Woe unto you Jews and Christians
    who received the Scriptures but continued in your unbelief, corrupting the
    teachings of Abraham and Jesus, and so p g g departing from the one God!
  </p>
  <p>
    We Moslems acknowledge Abraham, and Jesus who was a holy man, and Mary his
    mother.
  </p>
  <p>
    But we do not worship them as gods, because the omnipotent, omniscient, and
    eternal God is one and indivisible.
  </p>
  <p>
    Christians therefore sin grievously when they worship images in their
    churches, for of God no likeness can be made.
  </p>
  <p>
    Further, it is an abominable error—nay, blasphemy— to say that God has a
    son.
  </p>
  <p>
    Christians see their deity in a threefold form, as a drunken man sees
    double.
  </p>
  <p>
    But this is not to be wondered at, for Christian priests drink wine at the
    sacrifice, whereas the Prophet’s law forbids the use of wine.”
  </p>
  <p>
    When Andy heard this he started, stared wide-eyed at Captain Torgut, and
    said, “Perhaps this is a sign, for my worst misdeeds and sins have ever been
    the result of immoderate wine drinking.
  </p>
  <p>
    I can doubt no longer that God in His inscrutable wisdom has marked me out
    for slavery among the followers of the Prophet, so that I need never again
    fall a victim to my besetting sin.
  </p>
  <p>
    I won’t quibble over the Trinity, for the matter has always been beyond my
    feeble understanding, but if Moslems acknowledge the merciful and gracious
    God, and if your Prophet can really induce you to drink only water, then
    indeed your faith is worth considering.”
  </p>
  <p>
    Captain Torgut was overjoyed and cried, “Will you too freely take the turban
    and submit to the will of God?”
  </p>
  <p>Andy crossed himself and answered, “Kill or cure!</p>
  <p>If it is a great sin, may God forgive me because of my dull wits.</p>
  <p>
    Why shouldn’t I accept the same fate as my brother Michael, who is more
    learned than I ?”
  </p>
  <p>
    Captain Torgut said, “Allah is gracious and merciful if we walk in his ways.
  </p>
  <p>
    He will open for you the gates of Paradise with its rippling water brooks.
  </p>
  <p>He will give you rare fruits to eat, and virgins await you there.</p>
  <p>
    But only God is patient, and I have other things to do than convert my
    slaves.
  </p>
  <p>Repeat quickly what I say and so profess yourselves Moslems.”</p>
  <p>
    We repeated after him as well as we could the Arabic words, “Allah is Allah
    and Mohammed is his Prophet,” after which he recited the first sura of the
    Koran and explained that no agreement between Moslems was binding without
    it.
  </p>
  <p>
    While we were wrestling with the difficult Arabic words, the Negro gathered
    up the lopped-off heads and put them into a leather sack with some handfuls
    of coarse salt.
  </p>
  <p>
    Then Captain Torgut said to us, “Wind the turban about your heads, and from
    now on you are under Allah’s protection, though you will not be true Moslems
    until you have learned Arabic and are familiar with the teachings of the
    Koran.
  </p>
  <p>
    Circumcision is also a custom pleasing to God, which every true Moslem
    willingly complies with.”
  </p>
  <p>
    Andy said hastily, “Not a word has been said about this until now, and the
    step I’ve taken fills me with misgivings.”
  </p>
  <p>
    But I silenced him for fear of vexing the haughty captain, and whispered, “A
    wise man chooses the lesser of two evils.
  </p>
  <p>
    However unpleasant circumcision may be, it is at least preferable to
    beheading.
  </p>
  <p>
    Remember that all holy men in the Bible were circumcised, from the patriarch
    Abraham to the apostle Paul.”
  </p>
  <p>Andy admitted that he had never thought of this.</p>
  <p>
    “But,” he went on, “my manhood revolts against it, and I doubt whether
    afterward I could look a decent woman in the face.”
  </p>
  <p>
    By now the ship was sinking beneath our feet, and so we went aboard the
    narrow Moslem craft which, being built only for speed and combat, was very
    far from roomy.
  </p>
  <p>
    Four of our sailors whose lives had been spared were at once chained to the
    rowers’ benches, but Tor- gut-reis allowed us to remain near him while he
    sat himself down with crossed legs on a cushion before his tent.
  </p>
  <p>His good will emboldened me to ask what was to become of us.</p>
  <p>“How do I know?”</p>
  <p>he replied placidly.</p>
  <p>
    “Man’s fate is in the hands of Allah, and the days of birth and of death are
    predestined.
  </p>
  <p>
    You’re too frail for rowing and too old to be made a eunuch, and so no doubt
    you’ll be sold to the highest bidder in the Jerba market.
  </p>
  <p>
    Your brother, though, is a brawny fellow, and I would willingly have him in
    my crew.”
  </p>
  <p>
    Andy said gravely, “Noble captain, don’t separate me from my brother, for he
    is weak and defenseless and without my protection would soon be devoured by
    wolves.
  </p>
  <p>Sell us like two sparrows, for the same farthing.</p>
  <p>
    Also I’m in no hurry to fight my Christian brothers, having seen how cruelly
    they’re treated.”
  </p>
  <p>Captain Torgut’s face darkened.</p>
  <p>
    “Don’t dare to speak of cruelty, for Christians treat Moslems far more
    savagely.
  </p>
  <p>
    From sheer bloodlust they slay them all, irrespective of age and sex, while
    I kill only from necessity and carefully spare all who can be useful as
    slaves.”
  </p>
  <p>
    To lead the conversation into smoother channels I asked, “Exalted captain,
    do I rightly understand you to serve the great Sultan?
  </p>
  <p>
    How is it then that you attack Venetian vessels, although a state of peace
    and friendship is said to prevail between Venice and the High Porte?”
  </p>
  <p>Captain Torgut answered, “You have much to learn.</p>
  <p>The Ottoman Sultan rules over more lands and peoples than I can count.</p>
  <p>
    And yet more numerous are the countries, cities, and islands that pay him
    tax and enjoy his protection.
  </p>
  <p>
    As Caliph, the Sultan is the radiant sun to all Moslems, with the exception
    of the redheaded Persian heretics, God’s curse be upon them.
  </p>
  <p>
    The Sultan is for true Moslems what the Pope is for Christians, and as the
    Pope rules in Rome, so does p p Suleiman rule in the city of Istanbul, which
    Christians call Constantinople.
  </p>
  <p>
    Thus the Sultan is lord of both halves of the world and is Allah’s shadow on
    earth.
  </p>
  <p>But to what extent I serve him and obey his commands I hardly know.</p>
  <p>
    I obey only Sinan the Jew, Governor of Jerba, and he has his orders from the
    great Khaireddin, whom Christian seafarers have confused with his dead
    brother Baba Aroush and whom in their terror they have named Barbarossa.
  </p>
  <p>
    In Sultan Selim’s day, Baba Aroush captured Algiers, and the Sultan sent two
    war galleys full of janissaries to his relief.”
  </p>
  <p>“Then you are the Sultan’s subject?”</p>
  <p>I persisted, for all these names were so much Hebrew to me.</p>
  <p>“Don’t pester me with difficult questions.</p>
  <p>
    My master Sinan the Jew pays tribute to the Sultan of Tunis; nevertheless
    Sultan Suleiman’s name is mentioned every Friday in the prayers of all the
    mosques in Khaireddin’s domain.
  </p>
  <p>
    But after his brother’s death Khaireddin lost Algiers and the Spaniards have
    built a stout fortress on the island, blocking the entrance to the harbor.
  </p>
  <p>
    Well, the High Porte is far away, and at sea we wage war on all Christians
    without distinction.”
  </p>
  <p>He rose impatiently and gazed out across the sea.</p>
  <p>
    The galley slaves pulled till the timbers creaked and the water foamed at
    the bows, for we were still in pursuit of our convoy.
  </p>
  <p>But the sun was setting, and still no sail was to be seen.</p>
  <p>Torgut cursed savagely, and cried, “Where are my other ships?</p>
  <p>My sword thirsts for Christian blood!”</p>
  <p>
    He glared at Andy and me so ferociously that I felt it wisest to hide at
    once among all the bales and boxes that cluttered the hold, and Andy kept me
    company.
  </p>
  <p>
    But as the crimson sun sank below the horizon Captain Torgut became more
    composed and sent a devout man aloft to call the faithful to prayer.
  </p>
  <p>
    In a harsh voice this man shouted the name of Allah to all four quarters.
  </p>
  <p>
    Silence fell upon the ship, the sail was furled and the oars drawn inboard.
  </p>
  <p>
    Captain Torgut washed his feet, hands, and face in sea water, and his
    example was followed by the Italian renegades and most of the rowers.
  </p>
  <p>
    Then Torgut caused a mat to be spread before his tent and having laid his
    spear on the deck in the direction of Mecca he began as Imam to recite the
    prayers aloud.
  </p>
  <p>
    He grasped his right wrist with the left hand, fell upon his knees, and
    pressed his forehead down on the mat; this he did several times and his men
    did the same so far as the cramped space permitted.
  </p>
  <p>
    With the outlandish words ringing in my ears I felt wretched and defenseless
    and forsaken; I pressed my forehead on the deck and dared not pray even in
    my heart as I had learned to pray in childhood.
  </p>
  <p>
    Nor could I pray to the god of Arabs, Africans, and Turks —that god whom
    they declared to be so merciful and gracious to the faithful.
  </p>
  <p>
    Night fell, but after the fear and suspense that I had undergone I could not
    sleep; I lay listening to the light seas washing along the hull by my ear,
    under a starry sky.
  </p>
  <p>
    Brother Jehan’s frightful curses thundered in my head still, and in my
    horror I repeated them all.
  </p>
  <p>
    Not one of them had I forgotten, for terror had etched them into my heart
    forever.
  </p>
  <p>
    That very morning I had been rich and in full enjoyment of life’s blessings.
  </p>
  <p>
    I believed that in Giulia I had found a friend who like myself was alone,
    and I longed for her, even while I strove to overcome my repugnance.
  </p>
  <p>
    The pilgrimage I had undertaken had freed me from the nightmares of memory.
  </p>
  <p>
    But now I was the poorest of the poor, a slave owning nothing but the rag
    round my loins, a slave whom a purchaser could dispose of as he pleased.
  </p>
  <p>
    Also I had lost Giulia, and I dared not think what would befall her in
    Torgut’s tent.
  </p>
  <p>The pain of losing her was torment enough.</p>
  <p>
    But all this was nothing compared with the betrayal of my faith and my
    refusal to suffer martyrdom, to which the other pilgrims had so humbly
    submitted.
  </p>
  <p>
    For the first time in my life, at the age of twenty-five, having escaped
    many mortal perils, I had been confronted with a clear-cut choice which
    allowed no evasion.
  </p>
  <p>
    I had made my decision and, most shameful of all, I had made it without
    doubt or hesitation.
  </p>
  <p>For once I stood face to face with myself and examined my heart.</p>
  <p>“Michael, of Abo town in faraway Finland!</p>
  <p>Who are you?</p>
  <p>
    Should I not abhor you, shun you, hate you with a bitter hatred—you who
    throughout your life have never been or done anything completely, but have
    always faltered and stopped halfway?
  </p>
  <p>
    You may have meant well, but you never had strength enough to work for good.
  </p>
  <p>
    And whatever your intentions you have done much that was bad; worst of all
    was what you did today, for which there is no forgiveness.”
  </p>
  <p>
    I sobbed as I sought to defend myself, “I never wanted to deny my
    faith—truly, I did not want to, but I was forced.”
  </p>
  <p>
    But my relentless accuser answered me, “The same fate awaited the others,
    but they chose death rather than betrayal.
  </p>
  <p>Was your plight worse than theirs ?</p>
  <p>Think, Michael, and look truth in the eye.”</p>
  <p>
    My terrors were increased; drenched with sweat I stared into the darkness,
    and asked, “Who are you ?
  </p>
  <p>
    Which of us is the true Michael: you who accuse me, or I who breathe and
    live and, despite my anguish, secretly rejoice in every breath I
    draw—rejoice even in the sweat that pours from me and proves that I’m alive?
  </p>
  <p>
    But I confess it: my most heartfelt penitence, my deepest sorrows and my
    bitterest trials, disappointments, and hard-won lessons have poured like
    water off a duck’s back.
  </p>
  <p>When the storms passed I shook myself, p y and was as dry as before.</p>
  <p>
    I donned the pilgrim’s cloak in a desire to believe that all riddles would
    be answered beside our Saviour’s tomb, in the land where He was born, where
    He lived and died.
  </p>
  <p>I wanted to believe it, because it was pleasant.</p>
  <p>
    But now that I look you in the eye, you unknown Michael, I see that it was
    from you I fled.”
  </p>
  <p>I had never made a truer confession than this.</p>
  <p>
    I faced myself honestly, to be stricken at what I saw, for it was a void.
  </p>
  <p>But my accuser was not yet satisfied.</p>
  <p>“And what of your faith, Michael?</p>
  <p>What do you believe?</p>
  <p>
    What was it you denied when you denied your faith, though others were ready
    to die for theirs?”
  </p>
  <p>
    This was the bitterest cup of all, but the unknown Michael saw through me
    and I could only mourn, “You are right.
  </p>
  <p>
    I lost nothing when I denied my faith, for in me there was not faith equal
    to a grain of mustard seed.
  </p>
  <p>If there had been I would have died for it.</p>
  <p>My pilgrim’s cloak was a brazen lie.</p>
  <p>Until today my whole life has been a He.</p>
  <p>But I would rather bite my tongue off than admit this even to myself.</p>
  <p>For what is left of me then?”</p>
  <p>
    When I had said these words I felt for the first time a hint of peace in my
    soul.
  </p>
  <p>
    The stern judge within me said more gently, “Now at last we have reached the
    kernel of the matter, my poor boy.
  </p>
  <p>But let us go yet a step further, if we can bear to do it.</p>
  <p>Perhaps after all we can be friends.</p>
  <p>Look into yourself, Michael, and confess.</p>
  <p>In your heart, are you really as unhappy as you make out?”</p>
  <p>
    When he had said this I looked again into that inner emptiness and marveled
    to feel a dim, uncertain yet most glorious happiness dawning in the void.
  </p>
  <p>
    It was a happiness of the soul, because I had searched myself, cleansed
    myself, and was preparing now to begin again from the beginning.
  </p>
  <p>And so I answered meekly, “You are right, unknown man within me.</p>
  <p>
    Now that you have crushed and ground me to powder I am no longer so very
    unhappy.
  </p>
  <p>
    In fact I have never known such spiritual joy before, or even thought it
    possible.
  </p>
  <p>
    But now, destitute as I am, renegade to my faith, with nothing to look
    forward to but the fetters of slavery, I am reconciled with you and
    therefore I am happy.
  </p>
  <p>But whether you are of God or of Satan I dare not guess.”</p>
  <p>
    The unseen judge was wroth at this, and demanded, “Michael, Michael, what do
    you know of God or Satan?”
  </p>
  <p>
    To preserve my new-found peace I confessed hurriedly, “Nothing, truly,
    nothing, incorruptible Michael.
  </p>
  <p>But who are you?”</p>
  <p>He answered, “I am.</p>
  <p>This you know, and it is enough.”</p>
  <p>
    The words bowed me to the earth and filled me with such overwhelming
    happiness that I thought my heart would burst.
  </p>
  <p>Tears of joy rose to my eyes and I said, “You are within me.</p>
  <p>This I know, and it is enough.</p>
  <p>
    Be it so; the one incorruptible judge of all I am and do has his dwelling in
    my own heart, and stands above all g y understanding and all knowledge.
  </p>
  <p>
    For swift as thought you answer my questions with a voice that cannot be
    stifled; nor will I seek to stifle it, although hitherto I have so
    consistently turned a deaf ear.”
  </p>
  <p>Still happier was I to feel my dog softly creeping into my arms.</p>
  <p>
    He had gnawed through the thong with which Torgut had tethered him, and
    having found me he licked my ear, pressed his nose to my cheek, and curled
    up comfortably with a long sigh of content.
  </p>
  <p>I too sighed deeply and fell asleep.</p>
  <p>
    We cruised all next day, combing the coastal waters until we sighted a sail
    similar to our own.
  </p>
  <p>
    On approaching we beheld a vessel whose hull and rigging had been badly
    damaged by roundshot.
  </p>
  <p>
    We could hear the cries of the wounded while yet at some distance; there was
    little life left among the rowers, and of the rest of the crew hardly more
    than half were fit for combat.
  </p>
  <p>
    The captain had been killed and his body thrown into the sea, and a
    terrified renegade had taken command.
  </p>
  <p>
    This man now touched his forehead, bowed low before Torgut and said, “By the
    favor of Allah you had three ships under your command, Torgut-reis.”
  </p>
  <p>
    “Allah a\bar” returned Torgut impatiently, and although he guessed what had
    happened and his limbs trembled with rage, he controlled himself.
  </p>
  <p>“So it was written.</p>
  <p>Speak!”</p>
  <p>
    It appeared that when the gale had separated Torgut from his other two
    ships, these found each other, and together attacked a merchantman of the
    convoy.
  </p>
  <p>
    But the roar of her guns brought the war galley speeding to her assistance,
    and one of the pirate vessels, which had not had time to cast off from her,
    had been crushed between the two hulls.
  </p>
  <p>“And you?</p>
  <p>What did you do to help?”</p>
  <p>inquired Torgut with deceptive gentleness.</p>
  <p>“Lord,” answered the renegade frankly.</p>
  <p>
    “I freed the grapnels and we made off as quickly as the oars could take us.
  </p>
  <p>
    Under Allah you have only me and my presence of mind to thank for saving
    even one ship, for the war galley followed in pursuit and fired its
    frightful guns at us.
  </p>
  <p>Judge from our condition how hard we fought.</p>
  <p>
    We fled not to avoid battle but to find you and confer as to what had best
    be done.”
  </p>
  <p>Torgut was no fool.</p>
  <p>
    He put a good face on it, and repeated several times, “Allah is great,”
    after which he embraced the frightened renegade and spoke to him kindly.
  </p>
  <p>
    And although he would clearly have preferred to kick him overboard he
    praised his resourcefulness in the hearing of all.
  </p>
  <p>
    Then, having bestowed upon him many beautiful presents, and distributed
    silver coin among the crew, he caused both vessels to set a course for the
    island of Jerba, off the African coast.
  </p>
  <p>
    After this he retired to his tent and for two days and nights he never
    showed his face, even at the hours of prayer.
  </p>
  <p>
    During this time the crew also were dejected, no doubt dreading their
    return; for they had lost one of three ships and suffered severe damage to
    another, while the spoils they brought were not worth mentioning.
  </p>
  <p>
    Soon they must meet Sinan the Jew, the Governor of Jerba, and render an
    account to him of all they had done.
  </p>
  <p>
    In his melancholy, Torgut drove Giulia from the tent, and I asked her
    anxiously, “How are you, Giulia, and what dreadful things have happened?
  </p>
  <p>Has that repulsive Torgut offended you?”</p>
  <p>Giulia withdrew her hand from mine and answered, “No.</p>
  <p>
    When he had assured himself as to whether or not I was a virgin he never
    molested me; on the contrary he has behaved to me like any wellmannered
    captain and even allows me the same food as himself.”
  </p>
  <p>I only half believed her, and asked yet again, “Is that true?</p>
  <p>Has he really not molested you?”</p>
  <p>
    Giulia wept, and said, “I was ready to plunge a dagger into my breast—or so
    I fancy, but I was very much confused when I was led into his tent.
  </p>
  <p>
    He conquered my fears however, and was afterward careful not to insult me;
    from this I see that even infidels shun me for my eyes’ sake, although I had
    hoped, when once beyond the borders of Christendom, that I should no longer
    have to suffer for what I cannot help.”
  </p>
  <p>
    Despite my relief that no evil had befallen her, I was shocked, and said
    reproachfully as I took her hand again, “Giulia, Giulia, what are you
    thinking of, to mourn and complain because this ruthless man spared your
    virtue?”
  </p>
  <p>
    She snatched away her hand to dry her tears, and her eyes of different
    colors blazed with rage as she retorted, “Like all men you’re denser than
    you look.
  </p>
  <p>If he’d touched me I would have stabbed myself—I think.</p>
  <p>
    I weep because he never even tried, but withdrew at once and began mumbling
    his prayers.
  </p>
  <p>
    I can only suppose that he feared my eyes, and his revulsion wounds me
    deeply.
  </p>
  <p>It seems I am unfit even for heathens.”</p>
  <p>
    I could make nothing of this, except that she was out of her mind with
    horror at having fallen into slavery, and I consoled her as best I could,
    saying that to me she was lovely and desirable and that her eyes did not
    repel me at all; that on the contrary I bitterly regretted my stupidity in
    drawing back from her at so auspicious a moment.
  </p>
  <p>
    She grew calmer and at last said hopefully, “Captain Torgut expects to get a
    good price for me in Jerba, and he told me that was why he spared my virtue.
  </p>
  <p>
    But it must have been mere politeness, for if he had really liked me he
    would have kept me for himself.”
  </p>
  <p>
    I was enraged at her unreasonable attitude, and more maddened still to think
    that I must lose her and perhaps never see her again.
  </p>
  <p>
    The blue and brown brilliance of her eyes was so enchanting that I could not
    imagine how I had ever feared her.
  </p>
  <p>g “Giulia, Giulia!</p>
  <p>Only old men are rich, and some repulsive graybeard will buy you.</p>
  <p>
    Why didn’t I take you while I could, so that at least we could have shared
    the memory ?
  </p>
  <p>Now there will never be anything for us to share.”</p>
  <p>But at this she stared at me in amazement.</p>
  <p>“You take too much for granted.</p>
  <p>If you’d tried to do any such thing I would have scratched your face.”</p>
  <p>
    “Then why did you come with me to that lonely place, and why were you so
    angry when your eyes aroused in me the feelings rather of a brother than of
    a lover?”
  </p>
  <p>Giulia shook her head slightly and sighed.</p>
  <p>“If I talked till doomsday you’d never understand.</p>
  <p>
    Of course I hoped that you would try, and perhaps you might have succeeded,
    as the place was deserted and you are stronger than I.
  </p>
  <p>But you did not try, Michael, and that is what I can never forgive you.</p>
  <p>I hope you will come to suffer bitterly for my sake.</p>
  <p>
    My dearest wish is that you may see others pay sacks of gold for what you
    might have had for nothing.
  </p>
  <p>This may give you something to think about for a long time to come.”</p>
  <p>I perceived that I understood little of feminine logic.</p>
  <p>
    She veiled her face once more and left me alone, a prey to exceedingly
    confused ideas.
