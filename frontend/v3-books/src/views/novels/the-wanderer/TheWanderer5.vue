  </p>
  <p>
    Why should we buy a lazy eunuch and feed him at our expense when he would
    bring us nothing but trouble?
  </p>
  <p>
    Eunuchs are the most expensive of all slaves, and even distinguished ladies
    are content with a slave girl to attend them.
  </p>
  <p>
    ” Giulia was much dashed at my cool response and said, “I’m worn out with
    running about the city; my feet ache and I was laughed at when I haggled in
    the bazaar and with the greedy porter who carried the things home for me.
  </p>
  <p>
    And is this my thanks for trying to lay out your money to the best
    advantage?
  </p>
  <p>Of course eunuchs are expensive.</p>
  <p>But you could buy a Russian boy quite cheaply and make him into one.</p>
  <p>” “How can you suggest such a thing, Giulia!</p>
  <p>
    Never would I allow any man, whether Christian or Moslem, to be castrated
    just to gratify your vanity.
  </p>
  <p>
    Besides, the operation is dangerous; that’s why the price of eunuchs is so
    high.
  </p>
  <p>We might lose our money.</p>
  <p>I must say I’ve never heard a sillier suggestion.</p>
  <p>” Giulia flared up.</p>
  <p>“Indeed!</p>
  <p>
    Even the Holy Father in Rome has a number of boys castrated every year for
    his choir, and many conscientious Italian parents send their boys to Rome of
    their own accord for that purpose, to secure for them a better future than
    their homes can offer.
  </p>
  <p>
    And it’s not as dangerous as you say; you’re only trying to annoy me and you
    don’t love me at all.
  </p>
  <p>
    ” She broke into bitter weeping and declared herself the unhappiest of
    women, since no one appreciated her good intentions.
  </p>
  <p>
    And because I saw that she was sincerely mourning our poverty and her broken
    dreams I sat down beside her with my arm about her neck, to console her by
    recounting my success with Piri-reis.
  </p>
  <p>Wiping away her tears she stared at me in utter amazement.</p>
  <p>“Michael Carvajal!</p>
  <p>
    You, who have knelt before Allah’s shadow on earth—can you have been so mad
    as to accept twelve aspers a day, and that for meekly serving a senile old
    creature like Piri-reis?
  </p>
  <p>Then y g you’re no longer responsible for your actions.</p>
  <p>
    If there’s a scrap of manhood in you, Michael, you’ll go at once to the
    Grand Vizier and complain of such unfair treatment.
  </p>
  <p>
    ” Deeply hurt, I replied, “Try to understand, Giulia, that my brains are my
    only fortune, and I shall be humbly thankful if by their help I can secure a
    comfortable income for us both without having to take risks.
  </p>
  <p>
    I never forced you to be my wife; you might have gone where you pleased.
  </p>
  <p>It’s not yet too late.</p>
  <p>
    If you’re as disillusioned as you’d have me believe, nothing need prevent us
    from going to the Cadi tomorrow.
  </p>
  <p>
    For a small sum he will dissolve our marriage, and you may use those
    different-colored eyes of yours to look about for a better man than I am.
  </p>
  <p>
    ” It was unkind of me to remind her of the blemish which, though to me it
    was her chief charm, made every sensible man avoid her after the first
    glance; and she was greatly cast down.
  </p>
  <p>
    She sobbed and protested that she loved me, though she could not think why
    she had attached herself to a man so devoid of any trace of ambition.
  </p>
  <p>
    We wept and kissed until Abu el-Kasim felt it was time for him to withdraw,
    and soon we were planning together in all harmony how best to live on an
    income of twelve aspers a day.
  </p>
  <p>
    Giulia had to confess that the sum was at least twice as much as could be
    earned in Christian countries by a fully trained and experienced mercenary
    with a large family.
  </p>
  <p>
    At length she laid her white arms about my neck and said tenderly, “Ah,
    Michael!
  </p>
  <p>
    I love you more than I can say, but at least let me dream of the fife we
    might have.
  </p>
  <p>
    By gazing into sand I can earn quantities of money as soon as my fame has
    spread in the city.
  </p>
  <p>Let me dream!</p>
  <p>I don’t care about the eunuch.</p>
  <p>Perhaps I can train our deaf- mute to carry things for me.</p>
  <p>
    I won’t ask you for anything more, Michael, if only I may have one or two
    cats.
  </p>
  <p>
    The cats here have wonderfully bushy tails and a blue sheen to their coats;
    every fine lady has one, and the Prophet loved them.
  </p>
  <p>
    It’s only fair that I should have a cat or two, since you have your dog.
  </p>
  <p>” She kissed me fervently and I was induced to consent.</p>
  <p>
    But a day or so later I was saddened to see how hurt my dog was, when two
    cats with bushy tails appeared and took possession of our two rooms.
  </p>
  <p>
    Rael thenceforward had to keep to the courtyard and hardly dared show
    himself in the kitchen, even for his meals.
  </p>
  <p>
    Giulia bought these prodigiously expensive creatures with the money that
    Piri-reis had given me, and even then remained in debt for part of the
    price.
  </p>
  <p>
    One evening at dusk Andy arrived flushed with his potations, roaring German
    soldiers’ songs and bringing a greeting from Master Eimer in whose tavern he
    had been celebrating his successes at the arsenal.
  </p>
  <p>
    The commander of the Sultan’s artillery had been pleased to give Andy his
    hand to kiss, and to examine him as to Imperial g y p armaments.
  </p>
  <p>
    He then appointed Andy foreman at the foundry with a wage of twelve aspers a
    day.
  </p>
  <p>
    Andy had met there a number of skilled Italians and Germans who worked
    either as free renegades or as slaves of the Sultan, and who all declared
    that they had learned much from the Turks and respected the artillery
    commander and his lieutenants.
  </p>
  <p>
    Andy was now to lodge at the arsenal and might not leave it without
    permission, because of the military secrets involved.
  </p>
  <p>
    I was reassured to learn that Andy received the same pay as myself, for it
    showed that these rates were laid down by statute and that it would
    therefore be useless to complain of them.
  </p>
  <p>
    It was certainly mortifying to think that Andy, a single man and unable even
    to write his name, should receive as much as I did, but I was glad of his
    success and felt no resentment.
  </p>
  <p>
    So began our life in Istanbul, and it continued thus throughout the
    winter—if winter it could be called.
  </p>
  <p>
    Snow fell very seldom and melted at once, though there was much wind and
    rain.
  </p>
  <p>
    Not long after our official reception, Torgut-reis received at the Grand
    Vizier’s hands a gold-mounted horsehair switch, to be delivered to
    Khaireddin as the outward symbol of his new dignity as beylerbey.
  </p>
  <p>
    Torgut took back also a letter from the Sultan and three kaftans of honor.
  </p>
  <p>
    During my life with Giulia I believe I developed more and acquired a greater
    knowledge of life than in all my former years of wandering.
  </p>
  <p>
    Compared with her, my first wife Barbara had been a straightforward,
    unpretentious woman, albeit a witch—or at least infected with witchcraft to
    some degree.
  </p>
  <p>
    Barbara had been content for us to live like two little mice in our hole and
    earn a bare crust, so long as we could be together.
  </p>
  <p>
    But Giulia was not afraid of life, nor did peace and quiet appeal to her.
  </p>
  <p>
    Idleness made her ill, and to satisfy her craving for action she committed
    the wildest follies, convinced that all she did was well considered and
    undertaken with the most praiseworthy motives.
  </p>
  <p>Moreover she was never satisfied.</p>
  <p>No sooner were the cats in the house than she disliked their color.</p>
  <p>
    When without my permission she bought a costly necklace, she found she had
    no gown suitable to wear with it, and wanted to renew her wardrobe, or at
    least buy some slippers sewn with the same kind of stones as were in the
    chain.
  </p>
  <p>
    She was amazed when I attempted to reason with her, and she explained
    patiently, as to a child, “You see, Michael, the necklace by itself is
    useless.
  </p>
  <p>And it would be waste to lock it up and never wear it.</p>
  <p>I’m only considering how to display it to the best advantage.</p>
  <p>” “Then why in the devil’s name did you buy the thing?</p>
  <p>” I roared, infuriated.</p>
  <p>
    She looked at me indulgently and with a shake of her golden locks replied,
    “It was a unique opportunity and I was so fortunate as to have your month’s
    salary in my purse.
  </p>
  <p>
    In Venice such a chain would cost three or four times as much; I should have
    been mad not to take it, especially as such things never lose their value
    and are an excellent investment.
  </p>
  <p>” “Allah help me!</p>
  <p>” I groaned.</p>
  <p>
    “I’m no miser, but neither am I a galley slave to live for days on end on
    pea soup and crusts, and all because of your extravagance.
  </p>
  <p>” Giulia raised her hands to heaven in a prayer for patience.</p>
  <p>Then she screamed, “Extravagance!</p>
  <p>
    When I think only of our future and place our money in valuables which
    neither moth nor rust doth corrupt!
  </p>
  <p>If you want better food, then in God’s name earn a better wage.</p>
  <p>” “Allah!</p>
  <p>Allah!</p>
  <p>” said I.</p>
  <p>
    “I never spy upon you, Giulia, but I know you have many good things in your
    larder—expensive fruit juices, for instance, fruits preserved in honey, and
    sweet cakes from the pastry cook.
  </p>
  <p>
    That sort of food’s no good to a man, but I can’t endure your habit of
    inviting crowds of gossiping women to eat it and of prattling with them from
    morning till night, while your husband when he comes home after a hard day’s
    work must put up with pea soup and stone-hard crusts.
  </p>
  <p>
    ” Giulia flushed and cried tearfully, “I’ve never in my life known a more
    ungrateful man than you!
  </p>
  <p>
    Of course I must offer my neighbors as good cakes as I taste in their homes,
    if not better.
  </p>
  <p>It’s the only way to sustain your reputation among them.</p>
  <p>You don’t love me any more or you’d never treat me like this.</p>
  <p>
    ” Our quarrels most often ended by my humbly begging Giulia’s pardon and
    assuring her that she was the dearest and kindest and cleverest wife that
    ever a man had.
  </p>
  <p>I would also reproach myself for my bad behavior.</p>
  <p>
    But such phrases came ever more frequently from the lips only and not from
    the heart, and I stooped to utter them because my body craved for her and
    could not bear the abstinence that she would otherwise impose.
  </p>
  <p>
    An invisible rift widened between us and sometimes I would sicken of it all
    and join my dog in the courtyard under the cold winter sky, with his warmth
    as my only Comfort.
  </p>
  <p>
    At such lonely moments I felt once more a stranger in the world, and
    wondered for what strange pattern the great Weaver could use so patchy and
    brittle a thread as myself.
  </p>
  <p>
    Giulia’s irritability arose partly from her ill success as a soothsayer, for
    although her neighbors politely clapped their hands and admired her powers
    she earned nothing.
  </p>
  <p>
    The capital abounded in so many fortunetellers, astrologers, and throwers of
    chicken bones, of all races and creeds, besides heiromancers who practiced
    divination by means of blood and entrails, that it was hard for a newcomer
    to compete with them.
  </p>
  <p>
    Though Abu el-Kasim diligently sang her praises in the bazaar, he was not a
    man to inspire confidence.
  </p>
  <p>
    We began to feel shut out again from this mysterious city, where success
    depended less on reasoned action than on chance.
  </p>
  <p>
    I slipped imperceptibly into the Ottoman way of life and soon ceased to be
    regarded as a foreigner; with my gift for languages I combined the faculty
    of changing my skin, as it were, and assuming a new identity.
  </p>
  <p>
    Piri-reis’s old sea janissaries were friendly, while his clerks and
    cartographers grew accustomed to seeing me among them every day.
  </p>
  <p>
    Now and again I would be given a task suited to my talents—some errand to
    the Seraglio library, perhaps, where learned Mussulmans and Greeks were busy
    with the translation and copying of ancient manuscripts.
  </p>
  <p>But among these scholars I found no one to be my friend.</p>
  <p>I saw the Sultan once, at a distance, attended by a brilliant throng.</p>
  <p>
    A party of bowmen surrounded him as he rode> and as they might not turn
    their faces from him, those in front had to run backward.
  </p>
  <p>
    When on Fridays the Sultan rode to his father’s mosque, anyone— even the
    poorest—might present a petition to him at the end of a long, cleft stick.
  </p>
  <p>
    Many of these petitions were actually read and were dispatched by the Divan
    to the appropriate officials, for the wrongs recorded therein to be
    redressed.
  </p>
  <p>
    The more I thought about this vast empire, built up by the Ottomans from
    small beginnings and now comprising within its borders more races than I
    could name, the more deeply impressed was I by the remarkable statesmanship
    that held it together and made life there agreeable and safe.
  </p>
  <p>
    This realm was governed by milder, juster laws than those of Christendom,
    and the moderate taxes were not to be compared with the merciless extortions
    practiced by so many Christian princes.
  </p>
  <p>
    And further, the tolerance shown by Ottomans toward other _ religions was
    something unheard-of elsewhere; no one was persecuted for his faith save the
    Persian Shiites, the heretics of Islam.
  </p>
  <p>
    Christians and Jews had their own places of worship and might even observe
    their own laws if they so chose.
  </p>
  <p>
    Christians indeed had one heavy tribute to pay, in that every third year
    they must hand over their sturdiest sons to be trained from their eleventh
    year upward as the Sultan’s janissaries.
  </p>
  <p>
    But these boys did not complain; they were proud of the honor and became
    more vigorous champions of Allah than Moslems born and bred.
  </p>
  <p>The High Porte was indeed the Refuge of all Peoples.</p>
  <p>
    Not only did the core of the Sultan’s army consist of professional soldiers
    born of Christian parents, and adopted, brought up, and trained by Turks;
    the highest appointments in the Empire were held by men of every race who
    were slaves of the Sultan.
  </p>
  <p>
    To him alone they owed advancement, to him their heads were forfeit if they
    failed in prompt y p p and meticulous execution of his commands.
  </p>
  <p>
    The Sultan bestowed great power on these men, but his incorruptible agents
    constantly toured every district of every province and listened to the
    people’s complaints; thus the local governors were prevented from
    overstepping the limits of the authority vested in them by custom and the
    Sultan’s laws.
  </p>
  <p>
    My life was now bound up with the welfare and success of this empire, and so
    at first I strove to see everything in the most favorable light.
  </p>
  <p>
    There were signs that the Sultan was preparing for a great campaign, and
    without wishing ill to anyone I was keenly curious to know what would become
    of the King of Vienna.
  </p>
  <p>
    I had had experience of the Emperor’s poverty and did not believe he could
    send much help to his brother; moreover an inherent feature of the Ottoman
    Empire was its tendency to expand.
  </p>
  <p>
    In this it followed the doctrines of Islam, which preached unceasing war
    against the unbeliever.
  </p>
  <p>
    Also the janissaries grew restless and discontented if the Sultan failed to
    lead them at least once a year into a war in which plunder and fresh honors
    were to be gained.
  </p>
  <p>
    Whereas the Emperor Charles’s campaigns cost enormous sums and far exceeded
    his economic resources, the Sultan’s wars by an ingenious and farsighted
    arrangement paid for themselves.
  </p>
  <p>
    His regular cavalry, the spahis, drew their income from farms which they
    held from the Sultan and which were worked by slaves taken in battle.
  </p>
  <p>Thus these spahis served their sovereign for almost no wages.</p>
  <p>
    In districts bordering the Christian countries light cavalrymen, known as
    akindshas, lived on a war footing; their traditional banditry inclined them
    to enter the service of the Sultan.
  </p>
  <p>
    Similar tastes brought a vast number of idle men to the Sultan’s colors as
    auxiliary troops, which were commonly thrown in as cannon fodder at the
    forefront of any attack.
  </p>
  <p>
    The Sultan therefore found himself in a far more advantageous position than
    the Christian leaders and could, even while sustaining losses, slowly but
    surely wear down enemy resistance.
  </p>
  <p>
    And so, when like Giulia I indulged in dreams of a splendid future, I saw
    nothing fantastic in the idea that one day I might find myself governor of
    some wealthy German city, in reward for my services.
  </p>
  <p>
    But when I discussed Seraglio affairs with Giulia she warned me against
    relying too much on Ibrahim’s favor, and asked in some derision what it had
    done for me so far.
  </p>
  <p>
    From our neighbors and at the baths she heard gossip enough, and knew that
    the Sultan’s favorite slave, Khurrem the Russian, had already borne him
    three sons.
  </p>
  <p>
    This young and ever vivacious woman had so captured her lord’s heart that he
    paid not the least attention to the rest of his harem, and had even
    shamefully dismissed the m®ther of his first- y born son.
  </p>
  <p>
    It was now this underbred Russian woman on whom foreign envoys showered
    their presents; they called her Roxelana and sought by every means to gain
    her favor.
  </p>
  <p>
    Such was her influence over the Sultan that he would do anything to gratify
    her smallest wish, and envious voices in the harem had begun to hint at
    sorcery.
  </p>
  <p>
    Giulia said, “Grand viziers come and go, but woman’s power over man is
    eternal and her influence stronger than that of even the closest friend.
  </p>
  <p>
    If in some way I could win Sultana Khurrem’s favor I know I might do a great
    deal more for both of us than the Grand Vizier ever could.
  </p>
  <p>
    ” I smiled at her simplicity, but warned her, “Speak low, woman, for in this
    city walls have ears.
  </p>
  <p>
    I came here to serve the Grand Vizier and through him Khaireddin, lord of
    the sea.
  </p>
  <p>
    And you’re mistaken— nothing in the world is so fleeting as sensual passion.
  </p>
  <p>
    How can you suppose that the Sultan will be bound to one woman forever, when
    the choicest virgins of every race and country wait to obey his slightest
    sign?
  </p>
  <p>
    No, Giulia, women have no place in high politics; no future can be founded
    on a wayward houri of the harem.
  </p>
  <p>
    ” Giulia retorted with some asperity, “I’m edified to learn from you that
    love and passion are such ephemeral things.
  </p>
  <p>I shall not forget.</p>
  <p>But perhaps some men are less fickle than you.</p>
  <p>
    ” A few days later the Sultan held a Divan on horseback at which, according
    to ancient Ottoman custom, questions of peace and war were debated.
  </p>
  <p>
    He appointed Ibrahim commander in chief, or seraskier, of the whole Turkish
    army and once again confirmed Ibrahim’s position as Grand Vizier, whose
    commands and ordinances were to be obeyed by high and low, rich and poor, as
    if they had been the Sultan’s own.
  </p>
  <p>
    The proclamation was so comprehensive and detailed as to convince everyone
    that from now on Seraskier Ibrahim was, next the Sultan, the highest
    authority in the Empire.
  </p>
  <p>
    In token of his favor the Sultan gave him, besides a great quantity of
    splendid presents, seven horsehair switches instead of the four with which
    he had previously been honored, and also seven banners —one white, one
    green, one yellow, two red, and two striped ones— to be borne before him
    always.
  </p>
  <p>
    The Sultan had further granted him a salary of ten thousand aspers a day:
    ten times that of the Aga of Janissaries, who held the highest rank of all
    the agas.
  </p>
  <p>
    In my lowly position I never caught so much as a glimpse of the Grand
    Vizier, but was delighted to find that my faith in him was justified.
  </p>
  <p>
    When I mentioned this to Giulia she answered, “Have it your own way,
    Michael.
  </p>
  <p>
    Pin your faith on the Grand Vizier, who has remembered you so often and to
    such purpose!
  </p>
  <p>But allow me to seek my fortune elsewhere.</p>
  <p>
    ” Three days later the Sultan released King Ferdinand’s envoys who had been
    imprisoned in the Fort of the Seven Towers, and bestowed upon each a
    well-filled purse in compensation for what they had endured.
  </p>
  <p>
    I was told that he addressed these words to them: “Salute your master, and
    tell him he does not yet know all that our mutual friendship can achieve.
  </p>
  <p>
    But he will soon find out, and I mean to give him with my own hand all that
    he desires of me.
  </p>
  <p>Bid him make timely preparation for my coming.</p>
  <p>
    ” To these playful words King Ferdinand’s envoy replied, in a manner quite
    devoid of finesse, that his sovereign would be most happy to welcome the
    Sultan if he came as a friend, but that he would also know how to receive
    him as an enemy.
  </p>
  <p>Thus war was declared.</p>
  <p>
    But both official and secret agents of Christian states in Istanbul had
    already sent dispatches flying to their princes as soon as they heard that
    the Divan had met on horseback.
  </p>
  <p>
    Spring advanced to the sound of drums and trumpets, and ceaseless rain
    turned the ground to mud.
  </p>
  <p>
    It was the custom, when the seraskier had set forth in advance to mobilize
    his troops, for the Sultan to march somewhat later at the head of his
    janissaries.
  </p>
  <p>
    But now every day small detachments started for the frontier in a
    prearranged order, and with the creaking, lumbering gun carriages went my
    brother Andy.
  </p>
  <p>
    For the second time in his life he found himself on the road to Hungary,
    though this time to fight for the Mussulmans instead of against them.
  </p>
  <p>
    He seemed dubious of the enterprise and wondered how the guns were to be
    conveyed along pulpy roads and across rivers swollen with the spring
    freshets.
  </p>
  <p>
    But, he thought, the Mussulmans had perhaps found some method of overcoming
    these obstacles, since they marched regardless of bad weather.
  </p>
  <p>
    There was also great activity in Piri-reis’s department, for the fleet was
    making ready for war.
  </p>
  <p>
    It was to patrol the coasts of the Black Sea and the Aegean, and some
    vessels were to make their way up the Danube in support of the advancing
    army.
  </p>
  <p>
    At this time I was sent on many errands to the arsenal and the forecourt of
    the Seraglio.
  </p>
  <p>
    One exceptionally fine and sunny day after a long period of rain I was
    sitting and waiting in the Court of Peace, for my chief duty as messenger
    was to wait.
  </p>
  <p>
    I was by now familiar with the different dresses worn by the Seraglio
    servants—their materials, colors, badges, and headdresses—and no longer
    gaped about me like a stranger.
  </p>
  <p>Suddenly I saw a eunuch stumbling toward me.</p>
  <p>
    His fat face was swollen with weeping and he wrung his hands in despair as
    he asked me, “In Allah’s name, are you not that slave of Khaireddin’s, who
    brought the monkey?
  </p>
  <p>You may yet save me from the noose and the pit.</p>
  <p>
    Come with me quickly and I’ll beg the Kislar-Aga to allow you into the Court
    of Bliss, to coax the monkey down from the tree where it has been all night.
  </p>
  <p>A young eunuch has already broken his leg in trying to reach it.</p>
  <p>
    ” “I cannot leave my important business to play with monkeys,” I told him.
  </p>
  <p>“Are you out of your mind?</p>
  <p>
    Nothing can be more important than this, for little Prince Jehangir is
    weeping and we shall all lose our heads if he continues.
  </p>
  <p>
    ” “Perhaps Koko the monkey will remember me,” I reflected, “for I tended her
    when she was seasick on the long voyage from Algeria.
  </p>
  <p>She will certainly remember my dog.</p>
  <p>
    ” Rael lay curled up beside me, enjoying the warm sunshine, and when he
    heard Koko’s name he pricked his shaggy ears.
  </p>
  <p>
    We hurried with the eunuch through the second and third courtyards where
    more eunuchs surrounded us and beat on little drums as a signal to the women
    to hide themselves.
  </p>
  <p>
    We reached the shining copper gates of the gardens where the Kislar-Aga
    awaited us—the highest official in the harem and commander of the white
    eunuchs.
  </p>
  <p>
    It was in vain that he sought to conceal his anxiety behind a dignified
    demeanor.
  </p>
  <p>
    I threw myself on the ground before him, and he gave orders for me to be
    admitted instantly to the gardens of the harem.
  </p>
  <p>
    To enter it without permission spelled death to any but eunuchs, and only
    with an escort of these and by the Sultan’s command might merchants come in
    to display their wares.
  </p>
  <p>
    Not even a physician might pay a professional visit here without the
    Sultan’s consent.
  </p>
  <p>
    But I was now hustled with such frantic speed into the most closely guarded
    gardens in the world that the eunuchs had no time to bathe me or give me
    clean clothes, as was the custom, and to my annoyance I had to appear as I
    was.
  </p>
  <p>
    We raced along winding gravel paths, while my escort beat their little drums
    unceasingly and forbade me to look about me.
  </p>
  <p>
    At last we reached a huge plane tree in which four or five eunuchs with the
    courage of despair were clambering about in pursuit of the monkey.
  </p>
  <p>The monkey clung with hands, feet, and tail to the topmost branch.</p>
  <p>
    With cries and lamentations and kind words the eunuchs sought to coax it
    down to them, and exhorted one another shrilly not to let it fall and hurt
    itself.
  </p>
  <p>
    Just as I came up, one of these clumsy creatures slipped and tumbled
    shrieking from a considerable height.
  </p>
  <p>
    The top of the tree swayed as he crashed downward, head first, and lay
    senseless on the ground among the spring flowers.
  </p>
  <p>
    Lamentable though this incident was, it was not without its comic side, and
    three well-dressed boys of whom the eldest was perhaps eleven years old
    burst into uproarious laughter at the spectacle.
  </p>
  <p>But the fourth wept softly.</p>
  <p>He was not more than five.</p>
  <p>
    He sat on the arm of a man in a flowered silk kaftan in whom to my amazement
    I recognized Sultan Suleiman himself.
  </p>
  <p>
    There was no mistaking his smoke-colored complexion, though in his plain
    dress and low turban p g p he looked strangely short in stature.
  </p>
  <p>
    I at once threw myself down and kissed the ground before him and his sons.
  </p>
  <p>All was confusion round the tree.</p>
  <p>
    Ropes lay about, ladders were propped against the trunk, and efforts had
    clearly been made to bring the monkey down by squirting water at it.
  </p>
  <p>
    Even at this distance I could see that it was sick, and it moaned as it
    clung helplessly to the bough.
  </p>
  <p>
    The Kislar-Aga bent low before the Sultan and suggested that I should be
    sent up the tree since I knew the monkey, and indeed had brought the
    bewitched animal to the Seraglio.
  </p>
  <p>
    If I failed he would have me beheaded, and so no harm would come of my
    admission to the forbidden gardens.
  </p>
  <p>
    His harsh words so wounded me that I rose at once and said, “I never asked
    to come here; I was induced with tears and prayers to offer my help.
  </p>
  <p>Call down those blockheads.</p>
  <p>They scare the poor beast.</p>
  <p>And stop that drumming.</p>
  <p>Then give me a little fruit and I’ll try to coax it down.</p>
  <p>” The Kislar-Aga said, “Is it thus you speak to me, miserable slave?</p>
  <p>
    And know that since early morning we have tried to fetch it down with fruit.
  </p>
  <p>
    ” But Sultan Suleiman said curtly, “Call them down and send everyone away.
  </p>
  <p>You, too, have my leave to go.</p>
  <p>
    ” When the chattering eunuchs had disappeared with their ropes, ladders, and
    syringes, complete stillness reigned.
  </p>
  <p>
    The little boy in the Sultan’s arms had ceased sobbing, and only the moans
    of the monkey could be heard.
  </p>
  <p>
    Not venturing to address the Sultan I turned to his eldest son and said,
    “Noble Prince Mustafa, the monkey is sick.
  </p>
  <p>That’s why it fled up the tree.</p>
  <p>I shall try to coax it down.</p>
  <p>” The dark, handsome boy nodded haughtily.</p>
  <p>
    I sat down upon the ground with Rael in my arms and called softly and
    coaxingly, “Koko!
  </p>
  <p>Koko!</p>
  <p>
    ” The monkey peeped suspiciously through the branches and gave a few faint
    cries, but would not move.
  </p>
  <p>
    Then I said to Rael, hoping that the Sultan would hear, “Dear, faithful dog!
  </p>
  <p>
    Koko doesn’t know me in my new clothes, and thinks I’m one of the eunuchs.
  </p>
  <p>You call her.</p>
  <p>Perhaps she’ll remember playing with you on board ship.</p>
  <p>Try to call her down from the tree.</p>
  <p>
    ” Rael looked up into the treetop, pricked his ears, and whined softly, then
    barked twice.
  </p>
  <p>
    The monkey climbed down a little way to get a clearer view, and Prince
    Jehangir still on his father’s arm held up his little hands and called,
    “Koko!
  </p>
  <p>Koko!</p>
  <p>
    ” The monkey hesitated, but as Rael went on whining she made up her mind and
    climbed swiftly down.
  </p>
  <p>
    She sped up to me, sprang into my arms, and hugged me with her
    white-whiskered cheek against mine, her whole spindly body shaking with
    fever.
  </p>
  <p>
    Koko stretched out one arm and stroked Rael, then pulled his ears and tail,
    whereupon Rael gently caught her hand in his teeth and growled warningly.
  </p>
  <p>
    At the beginning of our voyage the monkey had tormented the life out of
    Rael, pinching him at every opportunity and then taking flight to the top of
    the mast, leaving the dog barking furiously below.
  </p>
  <p>
    But later they both enjoyed the game and became friends; sometimes they
    basked together on deck and Koko lay with her arms about Rael’s neck, or
    with nimble fingers plucked fleas from his coat.
  </p>
  <p>
    But now she broke off her play in a terrible fit of coughing, one tiny hand
    pressed to her chest.
  </p>
  <p>
    Tears flowed from the haggard eyes, and between paroxysms she uttered
    heartrending cries as if to tell me how wretched and lonely she felt.
  </p>
  <p>
    Rael too began to whine piteously, and licked Koko’s limp hand as if he
    understood.
  </p>
  <p>
    The princes came to stroke the sick monkey and to my surprise the Sultan
    drew near too, and spreading out the skirts of his kaftan sat down beside me
    on the ground so that Prince Jehangir could touch the little creature.
  </p>
  <p>
    The Sultan said to me, “You must be a good man, since animals trust you.
  </p>
  <p>Is the monkey sick?</p>
  <p>
    ” I replied, “I have studied medicine both in Christian countries and among
    Moslems, and I know that this poor beast has fever.
  </p>
  <p>It will die, if Allah so wills.</p>
  <p>
    It could not survive this climate, and the night spent in the tree has
    worsened the chill.
  </p>
  <p>
    I think it fled up the tree to die there alone, for most of the creatures we
    try to tame prefer to die in solitude, away from humans.
  </p>
  <p>
    ” Prince Mustafa said hotly, “The monkey has lived in warm rooms and worn
    warm clothes every day, for it’s my brother Jehangir’s pet.
  </p>
  <p>
    The slave who’s to blame for its illness shall pay for it with his head.
  </p>
  <p>
    ” I answered, “No one is to blame for this illness, for monkeys are most
    sensitive to changes of climate, and even in the palaces of sunny Italy they
    sicken and die.
  </p>
  <p>
    If this little monkey should perish too, it will be by the will of Allah and
    we cannot prevent it.
  </p>
  <p>Nevertheless I’ll prepare a cough mixture to soothe the pain.</p>
  <p>” The Sultan said, “Will you indeed give this poor beast medicine?</p>
  <p>Most physicians consider it beneath their dignity to treat animals.</p>
  <p>Yet the Prophet loved them, especially camels and cats.</p>
  <p>
    Indeed animals, unlike men, are without guile and I hate to see them suffer.
  </p>
  <p>
    But I have many animal doctors in my service and shall need you no longer.
  </p>
  <p>Selim, give him the monkey’s clothes.</p>
  <p>Mustafa, give him the chain.</p>
  <p>
    And you, Michael, dress the monkey and fasten the chain about its neck; then
    leave us.
  </p>
  <p>
    ” The boys handed me a little wool-lined velvet kaftan and a thin silver
    chain, but Koko struggled when I tried to put them on her.
  </p>
  <p>
    At last I succeeded, and laying the end of the chain in Prince Jehangir’s
    little hand I told the boys to give the monkey some warm milk.
  </p>
  <p>Then I rose, called my dog, and prepared to leave the gardens.</p>
  <p>
    But at this Koko flew into a passion, kicked and struggled and tried to bite
    the princes, then tore loose and ran after me.
  </p>
  <p>
    With the chain rattling behind her she scrambled up into my arms and clung
    there.
  </p>
  <p>The Sultan was at a loss.</p>
  <p>
    He put down the little boy, who ran up to me crying, flung one arm around my
    leg and raised the other to stroke the monkey.
  </p>
  <p>
    It was then I noticed that the poor little fellow was club- footed, and that
    a hump was beginning to show beneath his silken coat.
  </p>
  <p>
    His sallow p g g face was as ugly as the monkey’s, and he was almost stifled
    with his sobs.
  </p>
  <p>
    Then Selim, the third prince, clutched his head and cried in a shrill voice
    that he was going to faint.
  </p>
  <p>The Sultan shouted, “Mustafa and Muhammed!</p>
  <p>Take Jehangir in at once and this man too, to look after the monkey.</p>
  <p>Send the Kislar-Aga to me and call the tselebs.</p>
  <p>
    ” As I bent to rub Prince Selim’s temples the Sultan signed to me to go, no
    doubt meaning the boys to take me out of the harem gardens to their own
    quarters in the inner court.
  </p>
  <p>
    But the young princes misunderstood him and instead led me to Prince
    Jehangir’s rooms where the monkey’s cage was.
  </p>
  <p>
    I could feel the eyes of agitated eunuchs following me from behind the
    bushes, but did not then know enough to be afraid.
  </p>
  <p>
    Carrying the monkey and leading Prince Jehangir by the hand I followed the
    boys to Sultana Khurrem’s pavilion of many-colored tiles, thus committing
    all unawares the gravest possible offense.
  </p>
  <p>
    Mustafa, Muhammed, and Selim, all being over seven years old, lived with
    their tselebs, or tutors, in the third courtyard; but the sickly Jehangir,
    who was only five, remained in his mother’s pavilion and was allowed to keep
    his monkey there.
  </p>
  <p>
    I was certainly somewhat startled to see the women attendants hastening
    toward us unveiled, in the belief that I was a eunuch, yet I still had no
    misgivings and entered Prince Jehan- gir’s spacious room in which stood the
    monkey’s gilded cage and its bed.
  </p>
  <p>
    I ordered the women to fetch some hot milk at once for the sick animal,
    while the boys sat down on cushions to watch all I did.
  </p>
  <p>
    Rael hurried round the room sniffing into every corner, and Prince Jehangir,
    like any other tear-stained and weary child, began crying for his mother.
  </p>
  <p>
    All that had happened so far seemed purely the result of chance, and only
    later did I learn that Prince Selim was an epileptic.
  </p>
  <p>
    During his childhood the attacks could be controlled and suppressed by means
    of sedatives.
  </p>
  <p>
    They did not become serious until as a youth he began to drink too much
    wine.
  </p>
  <p>
    The Sultan naturally wished to keep this terrible disorder secret, and that
    was why he sent me so hastily out of the garden; he feared that the
    excitement might bring on an attack.
  </p>
  <p>
    Prince Mustafa was born of the Circassian slave whom Suleiman had dismissed
    in favor of Khurrem.
  </p>
  <p>
    He was thus only half-brother to Jehangir, and it had evidently been the
    Sultan’s intention that Mustafa should take me to his own rooms; but Mustafa
    was generous hearted and of course thought it best for the monkey to be
    taken straight to its warm cage.
  </p>
  <p>
    I did become exceedingly agitated, however, to hear a rippling laugh and see
    coming toward us an unveiled and richly dressed woman with a jeweled net
    over her hair.
  </p>
  <p>
    She put her arms about Prince Jehangir, and I at once flung myself to the
    ground, hiding my face in my hands.
  </p>
  <p>
    Yet I was as usual unable to master my curiosity, and stole a glance at her
    between my fingers, feeling that since in any case my punishment for
    entering the Sultana’s pavilion must be death, it could make little
    difference if I had a glimpse of the woman of whom so many tales were told
    and whom Christian princes overwhelmed with presents.
  </p>
  <p>
    My first impression was disappointing, for I had expected a ravishing
    beauty.
  </p>
  <p>
    This was the woman who, alone among countless lovely maidens from all
    corners of the world, had received the Sultan’s handkerchief and for years
    after their wedding night had kept his favor.
  </p>
  <p>
    She was a fairly tall, plump woman, still young, but her face was unusually
    round and her nose anything but aristocratic.
  </p>
  <p>
    It was in her lively play of feature and continual laughter that her charm
    lay, though it seemed to me that her blue eyes had no part in that mirth.
  </p>
  <p>
    As she regarded the deeply bowing Mustafa over Jehangir’s head I saw in them
    a singular coldness.
  </p>
  <p>
    Prince Mustafa explained that he had been commanded to bring me with him to
    tend the sick monkey and prepare a draught for it.
  </p>
  <p>
    My dog now rose smartly on his hind legs and stretched forth his nose toward
    the Sultana, in whom he plainly saw the dispenser of titbits.
  </p>
  <p>
    Prince Jehangir giggled, and at once the Sultana sent her women after
    sweetmeats, which she then gave the dog herself, laughing her silvery laugh.
  </p>
  <p>
    Meanwhile a cup of warm milk had been brought and I was able to induce the
    monkey to drink a little of it; but it would not leave me and kept one arm
    tightly round my neck while with the other it tried to coax the dog to come
    to it.
  </p>
  <p>
    Sultana Khurrem now turned to me and asked in Turkish, “Who are you, and how
    can a eunuch have a beard?
  </p>
  <p>Can you really treat sick monkeys?</p>
  <p>
    ” I pressed my forehead to the ground before her, while the monkey sat on
    the back of my neck and tried to snatch off my turban.
  </p>
  <p>
    “Sovereign lady,” I said, “I have not ventured so much as a glance at your
    radiant beauty.
  </p>
  <p>
    For the sake of my little dog and the sick monkey, protect me, for I’m no
    eunuch.
  </p>
  <p>
    Through no fault of mine I was brought to the gardens to coax the monkey
    down from the top of the plane tree, and I’ve not the least idea how I come
    to be in your presence, most lovely of all women in the world.
  </p>
  <p>
    ” She answered, laughing, “Lift your head and look at me, you simple man,
    now that you’re here.
  </p>
  <p>You’ve made my son Jehangir smile and he loves your dog.</p>
  <p>
    But the Kislar-Aga will no doubt receive the silken cord for his negligence,
    so you’ll die in good company.
  </p>
  <p>Prince Mustafa deserves punishment for his stupidity.</p>
  <p>” In deep dejection I replied, “I welcome death, if it be Allah’s will.</p>
  <p>
    But allow me first to give my dog to Prince Jehangir, if he is fond of him.
  </p>
  <p>After my death there will be no one else to care for the poor beast.</p>
  <p>I will also prepare a draught for the monkey and relieve its suffering.</p>
  <p>
    I’m not aware of having in any way offended against yourself or the Lord of
    all Nations, for it was not of my own will or with evil intent that I
    entered your presence.
  </p>
  <p>
    Nor can your beauty put me in a state of impurity, for how could one of my
    lowly estate raise his eyes to you?
  </p>
  <p>
    ” The poor monkey, still sitting on the back of my neck, was now overcome by
    another fit of coughing.
  </p>
  <p>I had to sit up again and take her into my arms.</p>
  <p>
    She coughed so violently that froth tinged with blood appeared at the
    corners of her mouth and she could offer no resistance when I laid her on
    the soft cushion in the cage, which was warmed by a charcoal brazier.
  </p>
  <p>
    Rael, replete with sweetmeats, jumped into the cage too, and curled up
    beside the monkey, which put an arm about his neck and tugged his ears.
  </p>
  <p>
    Prince Jehangir crept from his mother’s arms, drew a cushion forward to the
    cage, and sat down cross legged to gaze with his big, sad eyes at his pet.
  </p>
  <p>I could see that he was a gentle boy, who would not ill treat my dog.</p>
  <p>
    I then rapidly recited the first sura and said, “Prince Jehangir, my dog is
    the cleverest dog in the world and has seen many countries.
  </p>
  <p>
    I bequeath him to you, since I am to meet the One who severs the bonds of
    friendship and silences the voice of happiness.
  </p>
  <p>
    Take care of Rael and be a good master to him, and Allah will surely reward
    you.
  </p>
  <p>
    ” I was convinced that by the merciless laws of the Seraglio I must die.
  </p>
  <p>
    But the princes cared nothing for my melancholy fate; they clapped their
    hands and began to make much of their grieving brother Jehangir, in the hope
    that they too might play with my dog.
  </p>
  <p>
    Sultana Khur- rem said, “Such an animal is no very becoming gift for the son
    of the Sultan; but he himself is not without blemish and perhaps the animal
    will be a comfort to him if the monkey dies—as I hope it will, for the smell
    of its cage pollutes the room.
  </p>
  <p>
    But I’m not hardhearted, and I will speak to the Sultan, if I should be so
    favored as to meet with him before the mutes have put the rope about your
    neck.
  </p>
  <p>
    But your entry into this pavilion without permission is so abominable a
    disgrace for the Kislar-Aga that he will hardly spare your life, and as the
    Sultan’s slave I am bound to obey the Kislar-Aga in all things.
  </p>
  <p>
    ” I knew enough of the laws of the Seraglio to realize that she was speaking
    the truth, and that without the Kislar-Aga’s mediation she had not the least
    chance of approaching the Sultan.
  </p>
  <p>
    Suleiman himself had to submit to that official’s complex ceremonial when
    wishing to visit the house where his slave women lived, and if one of these
    had dared to address him without leave it would have constituted an insult
    to the Sultan’s majesty.
  </p>
  <p>
    For the same reason Suleiman could not visit his favorite without previously
    making known his intention.
  </p>
  <p>
    He could send for his children, to walk with them in the gardens, but at
    such times all the women had, on pain of disfavor and dismissal, to stay
    within doors and out of sight.
  </p>
  <p>
    Only by this strict rule could the Sultan be at peace, for without it his
    women would have been constantly slinking after him in an attempt to win his
    favor.
  </p>
  <p>
    Having reflected as coolly as I could upon my unenviable plight, I said, “I
    was commanded by the Sultan himself to tend the monkey, so I must now fetch
    the necessary remedies.
  </p>
  <p>
    Should anyone slay me while I’m on this errand he will be acting against the
    Sultan’s express command.
  </p>
  <p>
    I will go g g p g now; when—and if—I return, the Kislar-Aga may do with me
    as he pleases.
  </p>
  <p>
    ” The Sultana broke again into a cooing laugh, and this continual mirth
    began to make me strangely uneasy.
  </p>
  <p>She said, “Don’t think for a moment that you can escape.</p>
  <p>
    By looking into my face you have broken the strictest rule of the harem.
  </p>
  <p>
    For his own sake the Kislar-Aga will be forced to have you strangled as soon
    as he catches you, and even now, no doubt, is awaiting you eagerly at the
    Seraglio gates.
  </p>
  <p>” Prince Mustafa cried excitedly, “This should be good sport!</p>
  <p>Let’s follow him and see what happens.</p>
  <p>
    My father the Sultan entrusted this man to my care, but if I can’t save his
    life I should at least like to watch him die.
  </p>
  <p>Although I’m the Sultan’s eldest son I have not seen many men die.</p>
  <p>Come, Muhammed!</p>
  <p>
    ” The smile faded from Sultana Khurrem’s lips and her eyes turned ice blue,
    as if the shadow of death had glided through the room.
  </p>
  <p>
    Perhaps danger had quickened my wits, for I understood at once that Mustafa,
    on ascending the throne after his father’s death, would have his brothers
    slain.
  </p>
  <p>
    It would be but in accordance with the law, for the gravest menace to the
    Ottoman Empire had ever been that of civil war between brothers.
  </p>
  <p>I had strayed into the gardens of death; what hope remained?</p>
  <p>
    I believe only Prince Mustafa’s arrogant manner saved me, for, since it
    stung Khurrem to hear him boast of his age compared with that of his
    halfbrother, she made it a point of honor to protect me.
  </p>
  <p>
    She said, therefore, “Mustafa and Muhammed, go at once and find the Kislar-
    Aga.
  </p>
  <p>Tell him to come here instantly on pain of my most severe displeasure.</p>
  <p>
    ” The princes were thus compelled to renounce the exciting game in which I
    was to be the bait.
  </p>
  <p>They tossed their heads and muttered, but at length obeyed.</p>
  <p>
    As soon as they had gone Khurrem turned to me and asked quickly, “Who are
    you and what is your profession?
  </p>
  <p>I hope I do not compromise myself by shielding an unworthy man.</p>
  <p>
    ” Rapidly I told her of my travels and of how I took the turban, of how
    Khaireddin of Algiers had sent me to be the Sultan’s slave because of my
    languages and my familiarity with conditions in Christendom.
  </p>
  <p>
    At this point the Kislar-Aga arrived in a state of unspeakable agitation,
    and pressing his forehead to the floor in repeated prostrations he said,
    “Sovereign lady!
  </p>
  <p>Most high Sultana!</p>
  <p>
    I cannot tell how the error occurred, but mutes await this impudent slave at
    the copper gates.
  </p>
  <p>The matter shall remain a secret and your fair fame unsullied.</p>
  <p>Not even the Lord of all Nations need hear what has occurred.</p>
  <p>
    ” The flabby, ashen-faced eunuch stood there in his resplendent official
    dress and his eyes as he glared at me were dark with fury.
  </p>
  <p>
    But Sultana Khurrem said, “This slave was commanded by the Sultan himself to
    tend Prince Jehangir’s monkey.
  </p>
  <p>
    See that he is given the drugs he requires and that he returns safely to my
    pavilion, unless you should receive contrary orders from the Sultan.
  </p>
  <p>” The Kislar-Aga was compelled to obey.</p>
  <p>
    He escorted me from the pavilion, and two strong eunuchs seized me and
    hustled me out of the gardens even more speedily than I had entered them.
  </p>
  <p>
    The Kislar- Aga, pouring forth an unbroken stream of abuse, never let me out
    of his sight for an instant until we reached the apothecary’s shop in the
    forecourt.
  </p>
  <p>
    Here the Sultan’s Jewish physician Solomon quickly mixed the medicine I
    asked for, though he seemed jealous because I was accompanied by the
    Kislar-Aga and asked spitefully at which learned university I had taken my
    medical degree.
  </p>
  <p>
    The Sultan’s physicians were chosen from among the foremost specialists in
    the world and would suffer no outside competition.
  </p>
  <p>
    Humbly I explained that I was in attendance on a mere soulless beast which
    no man of distinction would deign to treat, and that I had studied medicine
    under eminent professors although I had never taken the diploma.
  </p>
  <p>
    The Kislar-Aga suddenly put both hands to his head and cried, “Blessed be
    Allah!
  </p>
  <p>Tell me again where you studied and graduated.</p>
  <p>
    If you’re a physician you may of course practice in the harem itself, in the
    presence of eunuchs, if the Sultan so commands.
  </p>
  <p>
    ” He offered me here the opportunity for a convenient lie, for I could have
    named any university and explained that I had lost my papers when taken into
