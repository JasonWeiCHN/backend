    with two worn gold ducats which I perceived at once to be underweight.
  </p>
  <p>
    The coins on the table were all newly-minted and whole, however, and so I
    willingly forgave him this slight deception.
  </p>
  <p>
    Andy asked me to read the contract aloud to him, and we found it to be fully
    in accordance with what had been agreed, and though nothing was mentioned as
    to the care of the livestock, the Jew pointed out that it would be to his
    own interest to take good care of the farms, for he hoped that next year the
    contract might be renewed on slightly better terms for another five or ten
    years.
  </p>
  <p>
    Our eyes were by this time fully open to the excellent stroke of business
    Andy had unwittingly done in dragging that poor little wench from the
    gutters of Vienna and making her his wife.
  </p>
  <p>
    But our dealings with the Jew were not yet over, for though I guessed that
    King Zapolya and Master Gritti would prefer ready money, the Grand Vizier at
    least must be offered something more personal, and for this no one could
    have given us better advice than the sagacious Jew.
  </p>
  <p>
    Such things as precious stones, ornaments, saddles, and damascened gold
    harnesses the Grand Vizier already had in abundance, so something entirely
    special must be found.
  </p>
  <p>
    At last Andy bought from the Jew a most marvelous clock of Niirnberg make,
    which struck both hours and quarters.
  </p>
  <p>
    It showed also the day, the month, and the year, and would thus be of great
    advantage to an absent-minded person, though unfortunately it was based on
    the p g y Christian measurement of time.
  </p>
  <p>
    However, we guessed that because of his wars and other dealings with
    European countries the Grand Vizier might be glad to keep count of the
    Christian reckoning.
  </p>
  <p>
    This clock was so complicated and so ingeniously built that I could not
    understand how a human brain could have devised it.
  </p>
  <p>
    The case was of beautiful workmanship, and the Jew contrived to set it going
    long enough for us to see how at each hour a little secret door flew open
    and a smith, followed by a priest and a knight in full armor, stepped forth
    to strike the hour on a little silver bell, and then disappeared through
    another door on the opposite side of the clock.
  </p>
  <p>
    Its only fault was that it did not go, and the watchmaker to whom it had
    been sent for repair had been sold into slavery by the Turks.
  </p>
  <p>
    Nevertheless the Jew hoped he might yet be found, and we could then give him
    to the Grand Vizier with the clock, to regulate it and keep it going.
  </p>
  <p>
    Because the mechanism was for the present out of order the Jew sold us this
    treasure for only twelve hundred ducats, which Andy gladly paid, and we then
    took a cordial leave of this wealthy man.
  </p>
  <p>
    When after a great deal of difficulty we were able to trace the clock-
    maker, Andy paid no less than sixty ducats for him, without bargaining,
    though there was little left of him but skin and beard.
  </p>
  <p>
    In his free-handedness he even gave the old fellow new clothes, and after a
    bellyful of food the man wept copious tears, tried to kiss Andy’s hands, and
    blessed him as his benefactor.
  </p>
  <p>
    He at once set to work on the clock, declaring that he knew its caprices,
    and even without the necessary instruments and parts was able to make it
    work well enough to convince the Grand Vizier of its surpassing excellence.
  </p>
  <p>
    He swore by the saints that once in Istanbul he would make this timepiece
    the marvel of the Seraglio and would devote the rest of his life to tending
    it.
  </p>
  <p>
    Thus it was that the clock secured him a carefree existence as the Grand
    Vizier’s slave.
  </p>
  <p>
    We now ordered four strong slaves to carry it carefully to Ibrahim’s tent,
    where the clockmaker set it going; the Grand Vizier marveled greatly at it
    and thanked Andy for the princely gift.
  </p>
  <p>
    I fancy Andy rose notably in the Grand Vizier’s esteem, for as a further
    mark of favor Ibrahim sent him and his wife two magnificently caparisoned
    saddle horses and provided an escort of a hundred spahis to attend them to
    their estate.
  </p>
  <p>
    Andy, having now done everything possible to secure his position, made ready
    to leave for the Transilvanian border.
  </p>
  <p>
    When I saw that in his black ingratitude he was forgetting me, his unmerited
    successes smarted within me and I said, “The frog puffed himself up until he
    burst.
  </p>
  <p>The money is yours; you can throw it down the jakes if you wish.</p>
  <p>
    But your coldness to me is very wounding, and I think you owe a morsel of
    consideration to one who is all but your own brother, and whom alone you
    have to thank for your prosperity.
  </p>
  <p>
    ” My words and my unfeigned tears brought Andy to a better frame of mind,
    and as icy winds chased the snow clouds over the towers of Buda, we seemed
    transported to our own land.
  </p>
  <p>
    We wept on one another’s breast; we swore that nothing in the world should
    sever our friendship and that we should be godfathers to one another’s
    children.
  </p>
  <p>
    When at last we parted Andy pressed upon me a thousand ducats, saying that
    even this sum was but slight acknowledgment of my long and faithful
    friendship.
  </p>
  <p>We had by now reached the end of October.</p>
  <p>
    The Sultan ordered the camp to be struck and the janissaries with many
    forebodings began the long march home.
  </p>
  <p>
    Before we left Buda, the Grand Vizier summoned me and Father Julianus once
    more to a nocturnal discussion and said, “It may be that you’re right,
    Michael el-Hakim, and are more familiar with German religious questions than
    I am.
  </p>
  <p>
    King Zapolya’s secret representative at the court of Margrave Philip reports
    that the heretic prophets have met in Marburg, the capital of Hesse, but
    that after a couple of days’ debate they separated in open hostility without
    having reached a single agreement.
  </p>
  <p>
    It seems that Luther and Zwingli did nothing but accuse one another of error
    and arrogance.
  </p>
  <p>
    I therefore agree to your plan, Michael el-Hakim, and will send you to the
    German states to sow even more bitter dissension among the Protestants and
    so draw them nearer Islam.
  </p>
  <p>
    ” I was aghast at his words, and made haste to reply, “You have quite
    mistaken my meaning, noble Grand Vizier, for I’m no orator.
  </p>
  <p>
    No, no, it is Father Julianus who should be sent to Germany, for he is an
    experienced preacher who can smell out heresy afar off.
  </p>
  <p>
    He will choose the right man in every town for the work; he will sow the
    seeds of Islam in people’s minds so that in their enthusiasm for the new
    ideas they will forget all that Christendom holds in common and rally round
    separate articles of faith.
  </p>
  <p>
    One will preach the one God, another the sinfulness of idolatry, a third
    predestination, and a fourth polygamy as justified by Scripture.
  </p>
  <p>
    I believe that Father Julianus knows his Bible so well that within a few
    days he could find texts to support all these arguments.
  </p>
  <p>
    ” Father Julianus stared at me as if the ground had opened at his feet and
    the devil in all his hideousness had appeared.
  </p>
  <p>“Get thee behind me, Satan!</p>
  <p>” he cried.</p>
  <p>“Would you make a heretic of me?</p>
  <p>
    Never will I consent to it; I will choose rather the glorious death of the
    martyrs.
  </p>
  <p>
    ” “But can you not see, Father Julianus,” I said, “that in sowing dissension
    among the heretics you do Holy Church the greatest possible service?
  </p>
  <p>
    I am persuaded that the Grand Vizier will furnish you with money enough to
    keep you well provided with wine and y y g p y p beer in the German
    lands—well enough even to invite others to share it with you.
  </p>
  <p>
    Should you be suspected of disseminating false doctrines, you have only to
    deny all you’ve said and blame your imperfect knowledge of the language for
    any errors there may have been.
  </p>
  <p>
    But if all goes well—you should need no more than a couple of years to
    complete the task—and if you send me details of all those, young or old,
    learned or simple, poor or rich who are in any way inclined to embrace and
    proclaim the new teaching, I am sure the Grand Vizier will so reward you as
    to enable you to spend the remainder of your days in peace and comfort
    before a never failing wine jar.
  </p>
  <p>
    ” Conflicting thoughts were mirrored on Father Julianus’s puffy face, and I
    could read there lingering fears for his immortal soul.
  </p>
  <p>
    Persuasively I went on, “Who knows but that the Grand Vizier might approach
    the Curia through a Venetian banking house, and buy you a bishopric in some
    retired corner of France or Italy?
  </p>
  <p>
    There you might enjoy a well-earned rest without molestation by inquisitive
    people.
  </p>
  <p>
    ” A warm gleam came into Father Julianus’s eye; he gazed dreamily into the
    distance, and at length exclaimed with a sob, “How devoutly I would serve in
    that exalted office, wretch and sinner that I am!
  </p>
  <p>
    Truly, Michael, from now on I will reform and do all I can to be worthy of
    the blessed task entrusted to me.
  </p>
  <p>
    ” Falling on his knees he kissed the Grand Vizier’s hand and watered it with
    his tears.
  </p>
  <p>
    I feared that Ibrahim would recoil from the expense of my plan, and I said
    quickly in Turkish, “Take no thought of the outlay, noble Grand Vizier, for
    Father Julianus will hardly get out of Germany alive to claim his bishopric.
  </p>
  <p>
    These new prophets are at least as fanatical in the defense of the purity of
    their faith as the Holy Office.
  </p>
  <p>
    Yet if by any miracle he should survive, it would be no bad thing for you if
    a Christian bishop were to owe his position to Islam.
  </p>
  <p>” Ibrahim nodded and said, “As you will, Michael el-Hakim.</p>
  <p>I trust you, and leave you a free hand with the details.</p>
  <p>
    If it fails, tsaushes will bring you the black kaftan and the silken noose,
    and will stay to see that you do not misinterpret the significance of the
    gift.
  </p>
  <p>” Perhaps this reminder of my mortality was wholesome.</p>
  <p>
    At any rate I began to see that I was meddling very rashly with matters that
    in no way concerned me.
  </p>
  <p>
    Nevertheless the plan was adopted and the details arranged by which Father
    Julianus could transmit his reports secretly to Istanbul.
  </p>
  <p>
    By degrees Buda was emptied of Turkish troops and Father Julianus set forth
    in good heart upon his return to Vienna, where he meant to preach about his
    miraculous rescue from the hands of the Turks.
  </p>
  <p>After that he would make for the German states.</p>
  <p>
    Having seen him safely off, I bought some war mementos and other presents
    for Giulia and then embarked on one of the transport vessels.
  </p>
  <p>
    This carried me down the Danube until I was able to join Sinan the Builder
    once more, in whose litter I completed the journey to Istanbul in comfort.
  </p>
  <p>
    Of the obstacles and hardships encountered by the army on its homeward march
    I will say only that the losses sustained were greater than those of the
    siege itself, and that at least ten thousand Hungarian and German slaves
    perished on the way.
  </p>
  <p>
    For my part I could think only of Giulia and of our future home on the
    shores of the Bosphorus.
  </p>
  <p>
    Sinan the Builder, thin, worn, and unable to sleep after his unremitting
    toil and anxieties, was weary of my chatter long before we reached Istanbul
    and at length seized his hammer and threatened to crack my skull if I would
    not be quiet.
  </p>
  <p>
    The nearer we came to Istanbul the more impatiently did I long to hold
    Giulia in my arms again, as I had done in the moments of our greatest
    happiness.
  </p>
  <p>
    I longed to tell her how well I had prospered, for with my two hundred
    aspers a day she could no longer look upon me as an inept and unenterprising
    man.
  </p>
  <p>
    By a singular irony of fate, the weather improved as we came nearer home.
  </p>
  <p>
    The rain ceased, wintry chill gave place to springlike warmth, and our eyes,
    weary of bleak mountains and heavy cloud, were now dazzled by the fresh
    green of countless gardens, although plane trees and acacias had long since
    shed their leaves.
  </p>
  <p>
    The air was like well-cooled wine, the sun shone from a cloudless sky, and
    the smell of the sea came to us on the wind as the Sultan rode into his city
    at the head of his janissaries amid deafening acclamation.
  </p>
  <p>
    Drums and cymbals clashed and the captive slaves dragged themselves along,
    glancing sullenly to right and left as they beheld the vast extent of the
    Ottoman capital.
  </p>
  <p>Bonfires blazed everywhere that night.</p>
  <p>Even in Pera, the Venetian quarter, they glowed like strings of pearls.</p>
  <p>
    On fire myself with longing I rode straight to Abu el-Kasim’s house, mounted
    on the horse given me by the Sultan.
  </p>
  <p>
    On my head I wore a broad turban adorned with a tuft of feathers in a
    jeweled clasp.
  </p>
  <p>
    From my belt under the kaftan of honor jingled a heavy purse and, besides my
    copper pen case, I wore a saber in a silver scabbard.
  </p>
  <p>
    I had hoped to find the gate flung wide and Giulia, warned by the sound of
    music, trembling in the doorway and blessing the day that brought me safely
    home from all the perils of war.
  </p>
  <p>Such was my fancied homecoming.</p>
  <p>
    But having waited so long outside the closed gate that inquisitive neighbors
    began to gather about me, I drew my sword, leaned forward in the saddle, and
    hammered on the gate with the hilt.
  </p>
  <p>
    My horse was whinnying and dancing about and I had great difficulty in
    keeping my seat when at last I heard the clash of bolts and Abu el-Kasim’s
    deaf-mute stood before me in the gateway.
  </p>
  <p>
    When he recognized me he quite lost his head and flung the gate open with a
    crash, uttering many incomprehensible sounds meanwhile.
  </p>
  <p>
    The horse reared, shied, and bolted into the courtyard, whence Giulia’s
    furry blue cat dashed away in a fright with bushy tail erect.
  </p>
  <p>
    At this my mount kicked and leaped sideways, throwing me headfirst to the
    ground.
  </p>
  <p>
    It was a marvel that my neck was not broken, though as it was my drawn sword
    made a deep gash in one calf— my first and only wound of the campaign.
  </p>
  <p>
    The deaf-mute flung himself to the ground in remorse, beating his brow and
    breast with clenched fist so that I had not the heart to chastise him.
  </p>
  <p>
    At this moment a dark-skinned Italian appeared in the doorway with coat
    unbuttoned and striped breeches open at the waist.
  </p>
  <p>
    Smoothing his gleaming black hair he demanded angrily who dared to disturb
    his noble mistress’s siesta.
  </p>
  <p>
    He was young and well built, though his dark complexion hinted at low birth;
    his features were as faultless as those of a Greek statue, and as
    expressionless.
  </p>
  <p>
    His brilliant eyes seemed light in comparison with his skin, and his thin
    lips showed determination, though at the moment they were distorted by a
    -sneer.
  </p>
  <p>
    I have described him thus fully to show that outwardly he was in no way
    repellent; yet from the first I felt a deep distrust of him.
  </p>
  <p>
    For this his arrogant behavior was not to blame, for when he realized who I
    was he displayed a flattering awe, and having restored some order to his own
    dress began respectfully shaking the sand from my kaftan.
  </p>
  <p>He then addressed me in well-chosen words.</p>
  <p>“I beg you not to be offended at so miserable a welcome, noble sir.</p>
  <p>We could not guess that you would return so soon.</p>
  <p>
    Regard for your lady should have impelled you to send word of your coming,
    that she might have prepared the house for celebration and received you in a
    fitting manner.
  </p>
  <p>Just now she is taking her midday rest, but I will rouse her instantly.</p>
  <p>
    ” This I sternly forbade him to do, saying that I preferred to wake her
    myself and so afford her a pleasant surprise.
  </p>
  <p>
    I then demanded in some irritation who he was and how he dared give himself
    the airs of the master of the house and try to prevent me seeing my own
    wife.
  </p>
  <p>
    He changed his tone at once and said on a note of humility, “Ah, Master
    Michael, I’m but Alberto the slave, from the city of Verona where my father
    still works as an honest tailor.
  </p>
  <p>
    I ought to have chosen his trade, but I was lured away by my craving for
    adventure and later captured by Turkish pirates.
  </p>
  <p>
    For a while I toiled as a galley slave and was then offered for sale in the
    bazaar here in Istanbul.
  </p>
  <p>
    Mistress Giulia took pity on my wretchedness, bought me, and installed me
    here as major-domo.
  </p>
  <p>
    Yet I have no servants under me save this feebleminded deaf-mute, who is not
    worth the salt in his broth.
  </p>
  <p>
    ” I asked how Abu el-Kasim could have approved of this purchase, since both
    house and slave were his.
  </p>
  <p>
    Alberto looked surprised and answered, “I have never seen this Abu el-Kasim,
    though the neighbors mentioned some shady drug dealer of that name.
  </p>
  <p>I believe he left here in the summer for Bagdad.</p>
  <p>Who knows if he will ever return?</p>
  <p>
    ” I perceived that many changes had taken place since my departure, and
    snapping at the Italian to keep his eyes lowered when addressing me as
    befitted a slave, I stepped into the house.
  </p>
  <p>
    He kept close at my heels and strove to push past me when I paused to look
    about.
  </p>
  <p>
    I hardly recognized the rooms, so cluttered were they with trash from the
    bazaar, and I constantly stumbled over stools, cushions, censers, and bird
    cages.
  </p>
  <p>
    When at last I reached the curtains concealing the entrance to Giulia’s room
    Alberto thrust himself in front of me, and falling on his knees he cried
    out, “Do not wake her too abruptly, noble sir!
  </p>
  <p>Let me give her a little warning by banging on a tray!</p>
  <p>
    ” Touched by his consideration for the lady of the house, I was nevertheless
    resolved to give myself the joy of surprising her.
  </p>
  <p>
    I pushed aside the agitated Italian, drew the curtains and tiptoed into the
    room.
  </p>
  <p>
    And there, once my eyes had grown accustomed to the half-darkness, the sight
    of Giulia abundantly rewarded my starved senses.
  </p>
  <p>
    She must have been tossing restlessly in her sleep, for she lay quite naked
    amid the tumbled bedclothes.
  </p>
  <p>
    Her face looked thin and there were dark rings beneath her eyes, but her
    golden hair lay in abundance over the pillow, her breasts were like
    rosebuds, and her limbs like musk and amber.
  </p>
  <p>Never in my most amorous dreams had I seen her so alluringly fair.</p>
  <p>
    I gasped and praised Allah for according his champion so glorious a
    homecoming.
  </p>
  <p>
    Then I bent over her, caressed her gently with my finger tips, and whispered
    her name.
  </p>
  <p>
    Without opening her eyes she stretched herself voluptuously, wound her white
    arms about my neck and sighed in her sleep, “No more, no more, you cruel
    man!
  </p>
  <p>
    ” Nevertheless she made room for me beside her, groped for me with her
    hands, and whispered, “But you may take off your clothes and lie beside me!
  </p>
  <p>
    ” I was startled at her readiness until I realized that she was enjoying
    some delightful dream and talking in her sleep.
  </p>
  <p>
    With a smile I did as she asked, flung off my clothes, and crept onto the
    bed beside her.
  </p>
  <p>
    Throwing her arms about my waist she pressed me to her and begged me
    sleepily to caress her.
  </p>
  <p>
    The depth of her slumber gg p y p surprised me, but I could see that she did
    not want to cut short her dream and was putting off the moment of waking.
  </p>
  <p>
    I did as she asked until in my excitement I made too abrupt a movement and
    woke her.
  </p>
  <p>Her wonderful eyes flew open.</p>
  <p>
    If I had had the smallest doubt of the soundness of her sleep I could have
    asked no better proof of it than her behavior now, for when she discovered
    what was happening she was quite beside herself with fright; she tore
    herself away and hid her nakedness beneath the covers.
  </p>
  <p>
    Then she burst out sobbing with her face in her hands, thrusting me away
    when I sought to comfort her.
  </p>
  <p>
    Filled with compunction for the trick I had played, I humbly begged her
    forgiveness.
  </p>
  <p>
    When at last she could speak she asked in a quavering voice, “Is it really
    you, Michael?
  </p>
  <p>When did you come, and where is Alberto?</p>
  <p>
    ” Hearing this, Alberto, who was standing behind the curtain, called out
    reassuringly and told her not to be alarmed by the blood on my trousers; the
    wound was slight, he said, and had occurred when I fell off my horse in the
    courtyard.
  </p>
  <p>
    His prattle so enraged me that I swore at him and told him not to stand
    there spying upon us.
  </p>
  <p>
    But Giulia said, “In fear and trembling I have counted the weeks and months
    of your absence—and are oaths the first words I must hear from you when at
    last you return?
  </p>
  <p>
    Don’t insult that faithful servant who has so well protected me since Abu
    el-Kasim left me in the lurch.
  </p>
  <p>
    Have you no more regard for me than to force your way in and place me in so
    degrading a situation before my own servant?
  </p>
  <p>
    ” This was the Giulia I knew, but she had evidently been badly startled and
    it relieved her to scold me.
  </p>
  <p>
    Even such words as these rang pleasantly in my ears after so long an absence
    and I tried to embrace her, but once again she broke loose from me and
    snapped, “Don’t touch me, Michael.
  </p>
  <p>
    By the rules of your religion I ought first to wash myself, and you too are
    dusty from your journey.
  </p>
  <p>
    You never were considerate to me, but at least remember your duties as a
    Moslem and leave me alone until I have bathed and made myself beautiful.
  </p>
  <p>
    ” But I protested that she had never been lovelier than in her present
    disordered state, and so begged and pleaded with her that at last she gave
    in and surrendered herself to me, muttering the whole time about my
    thoughtless and insulting conduct and so depriving me of half my pleasure.
  </p>
  <p>
    Afterward she rose quickly, turned her back upon me, and began to dress
    without a word.
  </p>
  <p>
    Receiving no answer to any of my humble questions I too became angry and
    exclaimed, “So this is the homecoming I have so long awaited!
  </p>
  <p>But why should I have expected anything else?</p>
  <p>
    You’ve not even asked how I am—and as for that scoundrel Alberto, I mean to
    send him straight back to the galleys where he belongs.
  </p>
  <p>” Giulia spun round and spat like a savage cat.</p>
  <p>With blazing eyes she cried, “I see no change in you, either!</p>
  <p>You only talk like that about Alberto to hurt me.</p>
  <p>
    He’s as good a man as you—perhaps better, for at least he comes of honest
    parents and has no need to make a secret of his birth.
  </p>
  <p>And what may you have been up to in Hungary?</p>
  <p>
    I could never have guessed the sort of thing that goes on at these campaigns
    if I hadn’t heard about it at the harem.
  </p>
  <p>
    ” Hurt though I was by her unworthy suspicions, I understood that her
    jealousy had been aroused by the malicious gossip she had heard.
  </p>
  <p>
    For the women of the harem were in the habit of bribing the Defter- dar’s
    eunuchs to spy upon the Sultan and the Grand Vizier, so that these exalted
    gentlemen had to pay dearly for any little adventure they might engage in.
  </p>
  <p>
    I said, “The Sultan and the Grand Vizier are men of virtue and it’s unseemly
    to speak ill of them.
  </p>
  <p>
    But your groundless jealousy shows perhaps that you still love me and care
    what becomes of me.
  </p>
  <p>
    Therefore I will swear on the Koran—and on the Cross, too, if that will
    satisfy you—that I never went near a woman, much as I desired to at times.
  </p>
  <p>There is no one like you, Giulia.</p>
  <p>
    And if at any time I did forget you, a wholesome dread of the French pox
    restrained me from thoughtless conduct.
  </p>
  <p>
    ” Hearing my grave words Giulia regained a measure of composure, though she
    still sobbed and laughed by turns as if someone had been tickling her.
  </p>
  <p>Then she said, “Still the same old Michael!</p>
  <p>
