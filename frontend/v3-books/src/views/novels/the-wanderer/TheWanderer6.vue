    forces of nature were on their side, and I fancy also that they regarded
    themselves as the guardians of the last Christian stronghold.
  </p>
  <p>
    If this fell, nothing could hold back the tide of a victorious Islam from
    flooding in over Germany and all Europe.
  </p>
  <p>
    I felt this strongly as I looked out over Vienna from the Semmering heights,
    and renegade though I was I could not be sure for which side I desired
    victory.
  </p>
  <p>
    And when I saw the incredible valor of the besieged I felt very painfully my
    apostasy, though the understanding reader will appreciate my sincerity and
    singleness of heart in matters of faith.
  </p>
  <p>
    I had little time for profitless brooding, for Sinan the Builder soon set me
    to work in earnest.
  </p>
  <p>
    As his interpreter I had to interrogate every prisoner we had taken at the
    capitulation of Buda; he even sent me to the prison camp to question
    fugitives whom the akindshas had captured, and learn from them details of
    streets, houses, walls, towers, and new fortifications in Vienna.
  </p>
  <p>He gave me no rest.</p>
  <p>
    Panting, I dashed from one informant to another and noted on my map which
    houses were stone built and which were of timber, which had lost their roofs
    and which had been torn down to make room for artillery, where trenches had
    been dug, which streets were closed to horse traffic and who had command of
    the various gates, towers, and bastions.
  </p>
  <p>
    I became really exasperated at last and cried, “Allah preserve me, what
    trouble you give yourself!
  </p>
  <p>
    Make a breach in the walls, no matter where, and the janissaries will do the
    rest—if only for the sake of warming themselves at last in front of a good
    fire.
  </p>
  <p>” But Sinan the Builder replied, “No, no.</p>
  <p>
    First I must note the slope of the ground and discover any subterranean
    springs, establish the water table, and note the depth of soil, lest my saps
    become flooded or brought up short by a wall of rock.
  </p>
  <p>I must know all there is to know about Vienna.</p>
  <p>
    ” I was already so familiar with the plan of the city that I could almost
    have found my way about it blindfold.
  </p>
  <p>
    Thousands of its inhabitants who were unfit to bear arms had been driven
    forth most mercilessly, and fell an easy prey to the savage akindshas.
  </p>
  <p>
    Their numbers so increased that there was scarcely room for them in the
    slave pens; nor was it possible to maintain an effective guard.
  </p>
  <p>
    Thus it was that many of them succeeded in escaping and carrying back useful
    information to the defenders.
  </p>
  <p>
    If these defenders had been mindful of their small numbers and had followed
    the rules of war by waiting quietly behind their walls for our attack, life
    in our camp might have been bearable, despite the weather and the shortage
    of provisions.
  </p>
  <p>
    But these reckless Germans and Bohemians hindered us in every possible way.
  </p>
  <p>
    When after much reflection and calculation Sinan began at last to dig toward
    the Carinthian Gate, the German gunners in the city descended into the
    underground galleries beneath the walls.
  </p>
  <p>
    There they sat with ears pricked and eyes fixed upon the surface of water in
    a bucket and upon a handful of peas scattered on a drum.
  </p>
  <p>
    When the peas began to dance and the water to quiver with the vibration of
    our works, these godless men at once embarked on countermeasures.
  </p>
  <p>
    So, when at last we had tunneled right under the wall and there stacked g
    our powder until such time as we could explode all the mines at once, these
    impudent and thievish Germans dug through to our saps from inside, stole all
    our powder, and carried it back into the city, having first blown up and
    destroyed all that we had achieved in the course of weeks of hard and
    dangerous labor.
  </p>
  <p>
    One evening the Seraskier, impatient at the slowness of the sappers, brought
    his light fieldpieces into position before the Carinthian Gate and bombarded
    its towers throughout a night of torrential rain.
  </p>
  <p>
    There could have been no better demonstration of the incomparable skill of
    the Turkish artillery, for they kept up an unbroken fire, loading and
    discharging their pieces almost as rapidly as in dry weather and by
    daylight.
  </p>
  <p>
    The constant thunder of these guns greatly stiffened my courage, but Andy
    thought it useless to expose the gunners to the drenching rain and so worsen
    their chills.
  </p>
  <p>
    The coughing of a hundred thousand Moslems was more alarming than a
    cannonade, he said, and alone would shatter the walls of Vienna.
  </p>
  <p>
    I felt no desire to leave my comparatively dry quarters, for which Sinan the
    Builder had procured a brazier.
  </p>
  <p>
    I spent a cheerful evening with him there over a flask or two of wine, and
    as a result we fell into a deep sleep.
  </p>
  <p>Suddenly we were roused by a terrific explosion.</p>
  <p>
    Certain picked troops of the garrison—Germans, Spaniards, and Hungarians—had
    made a surprise sortie through the Salt Gate and hurled themselves upon our
    unsuspecting men.
  </p>
  <p>
    They set fire to all the brushwood we had so painfully collected, to Sinan’s
    store sheds, the slave pens, and all the tents they could reach.
  </p>
  <p>
    It would have gone ill with the whole camp had not everything been too wet
    to burn properly.
  </p>
  <p>
    The worst panic was caused by the grenades that the assailants flung into
    the tents and whose smoking, hissing fuses glowed in the darkness like the
    tails of comets.
  </p>
  <p>
    Their shells of earthenware or glass were filled with stones, nails, and
    other rubbish which at the moment of explosion flew in all directions,
    inflicting many wounds.
  </p>
  <p>
    Sinan and I were in a daze of sleep when the storm broke and would certainly
    have come to a melancholy end if we had not managed to creep into an attack
    trench and so along to a tunnel whose mouth was concealed by a bush.
  </p>
  <p>
    The roar of the battle overhead was so terrifying that I lay there quaking,
    but Sinan the Builder wrapped his cloak about his head and fell at once into
    profound slumber.
  </p>
  <p>
    When at dawn the janissaries began marching down the hillsides in close
    order for the counterattack, the enemy, as might have been expected, were
    panic stricken and at least five hundred of them were cut down.
  </p>
  <p>
    The janissaries, furious at losing their night’s sleep, pursued so closely
    on the heels of the rest that they would have p y y followed the fugitives
    into the city had not the Germans hurriedly closed the gates, thereby
    leaving a number of their officers outside.
  </p>
  <p>
    Heads by the hundred were borne on poles to the Sultan’s tent, while the
    janissaries played their music and the agas boasted of their great victory.
  </p>
  <p>
    But the destruction in the camp was many times greater than the German
    losses, and the agas allowed no one to count the Turkish dead, whose bodies
    were hastily thrown into the Danube.
  </p>
  <p>
    Preparations for our assault were delayed and the powder —stacked in
    readiness beneath the walls—became damp.
  </p>
  <p>Time was on the side of the defenders.</p>
  <p>
    The everlasting coughing of the Turks resounded through the camp night and
    day, disturbing the Sultan’s sleep and exasperating him, for he read it as a
    sign of rebellion.
  </p>
  <p>Who knows but that he had some grounds for his suspicion?</p>
  <p>
    We were now nearing the middle of October, and supplies were running very
    short when at last we succeeded in exploding two mines and bringing down
    part of the wall near the Carinthian Gate.
  </p>
  <p>
    Almost before the flying debris had reached the ground the agas with swords
    and whips drove their men to the assault.
  </p>
  <p>
    For three days these attacks were repeated, but the men no longer believed
    in victory; the fighting spirit was out of them and many confessed that they
    would rather be killed by the scimitars of their own leaders than by the
    frightful two- handed swords of the Germans which at one stroke could cleave
    a man in half.
  </p>
  <p>
    Even Sinan the Builder was threatened by the Seraskier’s displeasure, for
    too many mines had exploded ineffectually.
  </p>
  <p>
    However, further frantic efforts resulted in the widening of the breach, and
    the final decisive assault began.
  </p>
  <p>
    Company after company was flogged and goaded into the thick of the struggle
    until the ground before the Car- inthian Gate was strewn with fallen Turks.
  </p>
  <p>
    Fog lay over the ground and through it the tips of the Turkish tents stood
    up like the white columns of tombs.
  </p>
  <p>
    All noise was curiously muffled in this spectral sea, and it was as if
    legions of spirits were in conflict before the walls.
  </p>
  <p>No wonder that the janissaries had no heart for the enterprise.</p>
  <p>
    When at dusk their last attack failed, they streamed back in full retreat
    and began to strike their tents, that they might depart without delay from
    the neighborhood of this uncanny city.
  </p>
  <p>
    When the Germans became aware of this they rang all the church bells and
    fired salvos in celebration of their joyful and unlooked-for victory.
  </p>
  <p>
    But when darkness fell, bonfires of a different sort blazed up in our camp.
  </p>
  <p>
    The infuriated janissaries were burning all that came to hand—enclosures,
    store sheds, grain sacks, and a great part of the plunder that the roving
    akindshas had brought in from sixty miles around and which, because of the
    lack of pack animals, could not be carried away.
  </p>
  <p>
    They slew the prisoners, impaled them or threw them into the flames, and
    although scores escaped in the confusion and were hauled up into the city by
    ropes, yet p y y p y hundreds of Christians were burned alive in revenge,
    that their shrieks might reach the city and subdue the unseemly jubilation
    of the defenders.
  </p>
  <p>Thus ended our triumphal march into the German states.</p>
  <p>
    The hideous menace that had brooded over Christendom melted away, and
    instead I was fated to witness Sultan Suleiman’s first and sharpest defeat.
  </p>
  <p>It was not the will of God, it seemed, that Christendom should fall.</p>
  <p>
    Hitherto, experience had seemed to show that God concerned Himself but
    little with warlike operations, but recent events made me alter my opinion.
  </p>
  <p>
    On leaving Rome I had thought of Christendom as a plague-ridden and already
    doomed carcass, but now I understood that some good must have remained in it
    since God in His patience granted it a short period of grace, as He would
    have been willing to do for Sodom and Gomorrah had ten righteous men been
    found in them.
  </p>
  <p>
    I shared these solemn thoughts with Andy as we wandered quietly among the
    heaped-up bodies of the dead, emptying purses and collecting the jeweled
    daggers of officers.
  </p>
  <p>
    The superstitious Moslems dared not seek out the bodies of even their own
    dead after dark, but Andy and I had no such scruples, and even though our
    business might appear to some people a little unbecoming, it would have been
    worse to behave like Turks and burn Christian prisoners alive.
  </p>
  <p>
    We also tended the wounded as well as we could, and ended our work of mercy
    by helping a moaning subashi into camp.
  </p>
  <p>
    Having thus come safely past the guards we returned to Sinan the Builder’s
    quarters—and only just in time, for he was already preparing to leave, and
    one of the Sultan’s bodyguard had come thither to bring Andy and me before
    the Seraskier.
  </p>
  <p>
    So startled was I at this unexpected summons that the burden I bore beneath
    my kaftan fell to the ground with a crash.
  </p>
  <p>
    With or without reason my conscience pricked me; I feared that the Grand
    Vizier might have heard of our little excursion to the battlefield and would
    have us hanged for looting the dead.
  </p>
  <p>
    A moment’s reflection showed me that this was not possible, however, and
    having stowed away my booty in a chest I entrusted this to Sinan, who alone
    had porters at his disposal.
  </p>
  <p>
    Yet I might have saved myself the trouble, for before we reached even Buda
    on our homeward way all our baggage was lost in a bog.
  </p>
  <p>
    We had no time to remove our bloodstained garments, for the hour was late
    and the Grand Vizier was pacing impatiently up and down his tent.
  </p>
  <p>
    Seeing us he halted in surprise and cried bitterly, “By Allah, are there
    still men who do not fear to bloody their clothes in their sovereign’s
    service?
  </p>
  <p>Are renegades to restore my faith in Islam?</p>
  <p>
    ” It was clear that he put a wrong construction on our appearance, yet I
    would not venture to correct so exalted a lord.
  </p>
  <p>
    With almost unseemly haste he dismissed his servants from the tent, made us
    sit beside him and began speaking in a whisper.
  </p>
  <p>
    As he spoke he glanced about him continually, as if afraid of eavesdroppers.
  </p>
  <p>“Michael el-Hakim and Antar!</p>
  <p>
    Sultan Suleiman has come to the conclusion that Allah will not yet permit us
    to capture Vienna.
  </p>
  <p>
    Tomorrow therefore he will strike camp and start for Buda with the main
    army, leaving me with the five thousand spahis to follow as rear guard.
  </p>
  <p>” “Allah is Allah and so forth,” I said, with unfeigned relief.</p>
  <p>“May his angels Gabriel and Michael protect our flight.</p>
  <p>
    The decision is indeed wise and I cannot sufficiently praise the Sultan’s
    prudence.
  </p>
  <p>
    ” But the Grand Vizier ground his teeth and said, “How dare you talk of
    flight!
  </p>
  <p>
    Not even in error must you pronounce so loathsome a word, and any man who
    dares distort the truth about our great victory over the unbeliever shall
    receive a hundred strokes of the rod on the soles of his feet.
  </p>
  <p>
    But the game is not yet over, Michael el-Hakim; if Allah permits I will yet
    lay Vienna at the feet of the Sultan.
  </p>
  <p>” “And how in God’s name is this to be done?</p>
  <p>” “I shall send you and your brother Antar into the city!</p>
  <p>
    ” His brilliant gaze transfixed me as he went on threateningly, “If life is
    dear to you you’ll not return with your mission unfulfilled.
  </p>
  <p>I am giving you a unique opportunity to serve the cause of Islam.</p>
  <p>
    ” Believing that adversity had bereft him of his wits I answered soothingly,
    “Noble Seraskier, I know what faith you have in my talents and Antar’s
    valor, but how are we two to capture a city that two hundred thousand men
    and a hundred thousand camels have failed to take?
  </p>
  <p>
    ” Andy too looked doubtful, and said, “It’s true that I’ve been compared to
    Samson—though far be it from me to vie with any holy man of Scripture —and
    Samson, they say, brought down the walls of Jericho by blowing a horn.
  </p>
  <p>
    But I have no such horn and humbly beg you to find some worthier man for the
    task.
  </p>
  <p>
    ” But Grand Vizier Ibrahim said, “You shall not be alone in Vienna, for I’ve
    chosen and bribed a dozen men from among the German prisoners, and I shall
    send them two at a time into the city on the same errand as yourselves.
  </p>
  <p>
    You too must dress like German lands- hnechts and mingle with the others.
  </p>
  <p>
    On the third night from now you must set fire to the city as a signal to me
    that you’ve succeeded in your task, and then open the Carinthian Gate so
    that in the confusion of the fire my spahis may ride in.
  </p>
  <p>
    If I should see no fire I must submit to the will of Allah and ride after
    the Sultan, hoping one day to meet you and your bold brother Antar in
    Paradise.
  </p>
  <p>
    ” He paused for breath, but presently continued, “I’ve litde confidence in
    the Germans I have bribed, but you I trust and I shall ask a certain loyal
    Jew named Aaron to help you.
  </p>
  <p>
    You will find him in a quarter called by Christians the City of Affliction,
    where the Jewish inhabitants of Vienna are penned up behind boards and
    barricades.
  </p>
  <p>
    Embittered by Christian persecution Aaron pins his faith on the Sultan whom
    he regards as his deliverer.
  </p>
  <p>Therefore he will certainly help you if you show him this ring.</p>
  <p>
    ” The Seraskier raised his shapely hand and spread out his fingers to choose
    one of the splendid rings upon them.
  </p>
  <p>
    From his little finger he drew a diamond no larger than the tip of a child’s
    finger, but so pure and brilliant as to emit blue fires as he turned and
    twisted it in the light.
  </p>
  <p>“Aaron knows this stone.</p>
  <p>
    He can give you no active help for fear of injuring his fellows, for
    Christians commonly visit the fault of a single Jew on all others in the
    city and sometimes on those of other cities as well.
  </p>
  <p>But he will advise you and if need be hide you.</p>
  <p>Tell him I will gladly redeem the ring for two thousand ducats.</p>
  <p>
    You shall wear German clothes and be escorted with blows to the prisoners’
    enclosure.
  </p>
  <p>
    Go in peace, then, and be assured of my favor if you succeed and I find you
    alive among the charred ruins of Vienna.
  </p>
  <p>
    ” But now both Andy and I spoke with firmness, and I told Ibrahim that if he
    were so anxious to be rid of a faithful servant he would do better to strike
    off my head at once.
  </p>
  <p>
    Ibrahim saw he would gain nothing by that, and after vain attempts at
    persuasion he said, “Very well, let it be as you wish.
  </p>
  <p>
    But why do you think I spared you circumcision if not to send you on just
    such an errand as this?
  </p>
  <p>
    Since you refuse, I can no longer delay the fulfillment of my religious
    obligations and must have the matter attended to at once.
  </p>
  <p>
    ” So saying he clapped his hands for the guard and sent him to fetch a
    surgeon.
  </p>
  <p>
    He then expressed satisfaction that Sinan the Builder had drawn his
    attention to a circumstance which his many duties and cares had caused him
    to forget.
  </p>
  <p>
    Andy and I had barely time to exchange a despairing glance before the
    surgeon appeared with a tube and a knife, which he began to sharpen,
    assuring us meanwhile that all would be over in a moment and that we should
    find it no more painful than the extraction of a tooth.
  </p>
  <p>
    Yet I felt the most intense repugnance to the operation and to the loss of
    my last link with Christendom, where I might yet seek refuge were disaster
    to overtake me in the Sultan’s domains.
  </p>
  <p>
    Andy also fidgeted and at last said, “I think I prefer to serve Islam by
    going into Vienna, so long as there may be no further talk of mutilation if
    I survive.
  </p>
  <p>
    Good Moslem though I am, I cannot believe that on the Last Day Allah will
    have nothing better to look at than —” I said quickly that as ever I would
    share my foster brother’s fate for good or ill, and as for circumcision, I
    would defer it until I felt it a matter of conscience, and then submit to it
    of my own free will.
  </p>
  <p>
    The Grand Vizier dismissed the disappointed surgeon and said smiling that he
    relied upon us and was persuaded that we should do our best like honest men.
  </p>
  <p>
    He then handed each of us one hundred German and Hungarian gold ducats, in
    the worn leather purses common among mercenaries.
  </p>
  <p>
    In his presence we changed into clothes taken from fallen Germans, and as
    soon as Andy had drawn on the familiar striped breeches the old German oaths
    y p rose unbidden to his lips and he was aware once more of the
    unassuageable thirst of the mercenary.
  </p>
  <p>
    The wine that the Grand Vizier then offered us sustained us under the blows
    and buffets with which we were driven into the pen of captives, though our
    escort seemed to me overconscientious in obeying Ibrahim’s order to treat us
    like the rest and thus allay suspicion.
  </p>
  <p>
    So it was that I had a black eye and a swollen lip when in the raw morning
    mist we broke out and stumbled over the familiar battlefield to the
    Carinthian Gate, and there cried piteously in the name of God to be
    admitted.
  </p>
  <p>
    Many of our fellow prisoners had been almost too weak to stand, far less
    escape, but at least a dozen women pressed through the gap that Andy had
    made in the fence and followed us, screaming.
  </p>
  <p>
    Hearing the noise of these poor women, who seemed to imagine that the louder
    they shrieked the faster they could run, the sentinels on the walls made
    ready for us and lowered ropes and ladders, at the same time discharging a
    swarm of arrows at our pursuers, whom the mist concealed.
  </p>
  <p>
    Trembling and dizzy we crawled up the walls, and friendly hands helped us
    over the top.
  </p>
  <p>
    We were thumped on the back and offered bread and wine, and as we ate we
    helped to haul up the women who with screams and flapping petticoats emerged
    like distracted hens from the sea of mist.
  </p>
  <p>
    These women were fairly young and handsome, for the akindshas on their raids
    chose always the best for the slave market and slew the rest.
  </p>
  <p>
    Both Germans and Bohemians yelled with delight at the sight of them and
    welcomed them as a gift from heaven.
  </p>
  <p>
    Having helped them from the wall they at once threw them down on the bare
    ground, all breathless as they were from their flight, and raped them so
    quickly that they hardly understood what was happening.
  </p>
  <p>
    This lively scene was interrupted by a red-haired ensign who came dashing
    from the guardhouse to beat his men on their hinder parts with the flat of
    his sword and revile them shrilly for being worse whoremongers than the
    Turks.
  </p>
  <p>
    He then ordered them back to their posts lest the enemy should gain
    possession of the gate by a surprise attack.
  </p>
  <p>
    The seasoned veterans, with their bloodstained bandages, singed beards, and
    blackened cheeks, laughed in the stripling’s face and invited him to kiss
    this and that.
  </p>
  <p>
    But they let the women go and hauling up their breeches returned to the
    watch tower.
  </p>
  <p>
    The ensign now addressed us in harsh tones and threatened to hang us with
    his own hands if we proved to be Turkish spies.
  </p>
  <p>
    He pointed to a number of German-clad bodies dangling from gallows at the
    top of the wall, and declared we should suffer the same fate unless we at
    once made full confession.
  </p>
  <p>But Andy knew how to handle such young cockerels as this.</p>
  <p>
    He stepped up to him, belched wine fumes in his face and said he would teach
    him how p to treat the Emperor’s loyal servants, who escaped in peril of
    their lives and rescued a flock of Christian women from the fate awaiting
    them in Turkish harems.
  </p>
  <p>
    So convincing was his eloquence that the young man blenched, addressed him
    as sir, and assured us that for his own part he had no suspicions, but that
    his duty required him to be strict.
  </p>
  <p>
    He begged us therefore to comply with regulations by making water before him
    and giving our names, and the names of our regiment and commanding officer.
  </p>
  <p>
    When he had entered these particulars in his guard book we could obtain
    passes from the town hall.
  </p>
  <p>
    We could not refuse so moderate a request, and when we had given him the
    visible proof he asked for that we were not Moslems, Andy explained that we
    belonged to the advance guard of the lands\nechts who had been sent to
    Vienna’s relief from Italy and that our leader was the Emperor’s famous
    general Bock von Teufelsburg.
  </p>
  <p>
    It seemed best not to mention any well-known man for fear of detection, but
    I hastened to emphasize that the name spoke for itself, having won honor and
    glory in seventeen years’ campaigning, and it was not our leader’s fault if
    our troop had been surprised by the akindshas and dragged away for
    questioning.
  </p>
  <p>We two, I said, were the only survivors.</p>
  <p>
    The ensign listened open mouthed and protested eagerly that the name of Bock
    von Teufelsburg was familiar to him.
  </p>
  <p>
    He repeated his instructions to report at once to the town hall for further
    interrogation.
  </p>
  <p>
    Then he seemed to hesitate; he bit his lip in some embarrassment and said,
    “The prosecutor and provost marshal are somewhat severe, as is natural, in
    view of Turkish cunning.
  </p>
  <p>
    They would rather hang ten innocent men than allow one suspect to escape.
  </p>
  <p>
    Nor are deserters kindly received, and as a good Christian I warn you that
    you’ll be imprisoned in any case until you can find someone to vouch for
    you.
  </p>
  <p>Failing this you will be hanged.</p>
  <p>
    ” Then in a burst of candor he went on, “You and your comrade would be wise
    to shun the town hall and the provost marshal’s men like the plague until
    the Turks have withdrawn.
  </p>
  <p>
    You’ll have no difficulty, for there are many other deserters hiding in
    taverns and in the lodgings of softhearted women.
  </p>
  <p>Go in peace and fortune go with you.</p>
  <p>Drink a cup now and then to my health and success.</p>
  <p>
    ” With that the good-natured boy threw us a silver schilling and left us.
  </p>
  <p>Andy and I slipped away into the October mist.</p>
  <p>
    I was for seeking out Aaron at once, but Andy, holding my arm negligently
    between finger and thumb, trudged along the filthy streets under the blank
    gaze of charred, roofless houses, and as he went he sniffed the air.
  </p>
